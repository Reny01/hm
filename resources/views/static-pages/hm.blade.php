        <h1> Wprowadzenie - definicja Historii Magii </h1>
<p>Najogólniej mówiąc, jest jedną z dziedzin nauki zajmującą się zbieraniem informacji, opisywaniem, klasyfikowaniem oraz uporządkowaniem zdobytej wiedzy na temat ważnych wydarzeń, odkryć oraz znanych postaci, które znacząco odbiły się na kartach historii. To także nauka, która zajmuje się życiem, tradycjami i kulturą czarodziejów na przełomie dziejów.</p>

<p>To znaczy, że przedmiot ten zajmuje się życiem czarodziejów w różnych epokach, ich współistnieniem z mugolami i etapach w jakich się ono odbywało, mówi o ważnych wydarzeniach, które miały znaczący wpływ na rozwój kulturowy jak i sposób życia czarodziejów w danym okresie historycznym. Uczy o wydarzeniach, które na dobre odbiły się na kartach historii i wpłynęły na dzisiejsze odbieranie natury magii oraz samych czarodziejów. Historia sama w sobie ma bardzo szerokie znaczenie i ciężko ją jednoznacznie zdefiniować, zajmuje się zarówno ważnymi wydarzeniami historycznymi, odkryciami ale i życiem społecznym czarodziejów, rozwojem kultury i rozrywki, wszystkim tym co czarodziejów otacza i wpływa na ich życie.</p>


<p>Kiedyś przedmiot potocznie nazywany WoHP, który miał uczyć i sprawdzać wiedzę uczniów na temat wiadomości z książek o Harrym Potterze, teraz całkowicie zaniknął. Tylko nieliczne szkoły dalej go praktykują i wprowadzają w swoje oferty edukacyjne. Samo WoHP jest według mnie odłamem Historii Magii, bardzo specyficznym, bardziej rozbudowanym o czasem nieistotne kwestie typu ‘kształt kapelusza Dumbledore’, ale jednak odłamem. Historia Magii skupia się na dziejach czarodziejów od ich początków po czasy współczesne, natomiast WoHP to bardziej ograniczona dziedzina, skupiająca się na konkretnym miejscu, konkretnych bohaterach i konkretnym czasie.</p>

<p>Oczywiście, niektórzy mogą powiedzieć, że tak nie jest, że WoHP to przecież Wiedza o Harrym Potterze! Tyle tylko, że same dzieje Harry'ego Pottera zawierają się w dziejach czarodziejów, dziejach samej magii. I tak jak już wspomniałem wcześniej, staram się wykorzystać wszystkie dostępne wiadomości podane przez Rowling, które to mogą przydać się na lekcjach HM, oczywiście wybierając te najważniejsze.</p>

<p>Kończąc, chcę tylko napisać, że Ci którzy uważają, że WoHP nie jest odłamem samego HM dają również do zrozumienia, że HM nie opiera się albo nie musi się opierać, według nich samych, na informacjach i realiach od Rowling. To znowu kończy jakiekolwiek dyskusje, a czasem pretensje, jakie mają niektórzy do nauczycieli tego przedmiotu, o to, że nie trzymają się wyznaczonych zasad Świata Magii. Proszę to jednak potraktować jako pewną zagwozdkę i w wolnej chwili przemyśleć ten temat. Sam uważam, że trzeba się przede wszystkich skupić na tym co dała nam autorka, a to prowadzi do tych właśnie wniosków. </p>
