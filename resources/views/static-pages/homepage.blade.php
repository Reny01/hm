        <p>Czym tak właściwie jest magia? Według mnie nie ma jednoznacznej definicji, która mogłaby ją określać. Dla każdego magia jest czymś innym, kojarzy się z czymś innym. Jest jednym z podstawowych aspektów, towarzyszącym nam przez całe życie, to fundament na którym opieramy nasze istnieje jako czarodzieje. Ja osobiście porównuję magię do miłości, bez niej bardzo ciężko jest nam żyć, nie kochając nikogo tak naprawdę wiele tracimy, bez miłości nie jesteśmy w pełni sobą. Widać to na przykładzie Lorda Voldemorta, który nie zaznał w swoim życiu miłości i nie potrafił kochać. Ostatecznie stal się kimś kto ledwo przypominał człowieka. Tak samo jest właśnie z magią, dla czarodziejów jest czymś tak naturalnym, tak normalnym, że ciężko jest sobie wyobrazić swoje życie bez niej. W końcu można ją uznać za swojego powiernika, swoją przyjaciółkę, która nas nie zawiedzie jeśli odpowiednio o nią dbamy, w odpowiedni sposób rozwijamy swój magiczny potencjał.

  Magia jest sposobem na dokonywanie rzeczy niemożliwych, wykraczającym poza standardowe myślenie przeciętnego mugola. Za jej sprawą dzieją się rzeczy, które wykraczają poza standardowe prawa natury (prawa mugoli) i które nie powinny mieć miejsca. Wystarczy bowiem magiczny proszek Fiuu, by przenieśc się z miejsca na miejsce, jeden ruch różdżki, a dzieją się niesamowite rzeczy. Zwykły eliksir, a potrafi wpływać na czarodziejów silniej niż my sami, wywołując przy tym straszne, a nieraz i zabawne rzeczy. To dzięki magii jesteśmy w stanie pomagać, leczyć i chronić naszych najbliższych, to ona określa tak naprawdę nas, i naszą naturę.

  Krótko mówiąc, magia jest praktyką wpływania na ludzi, przedmioty czy zjawiska fizyczne za pomocą zaklęć czy innych jej form. Jest sposobem na kreowanie otaczającej nas rzeczywistości, a więc zasadniczo sposobem nie poddającym się weryfikacji za pomocą metod naukowych (metod mugoli). Zasadniczo, ponieważ jako czarodzieje jesteśmy z magią związani od najmłodszych lat i dla nas, magów, jest to rzecz codzienna, a więc jak najbardziej normalna. Mugole zaczęli nazywać rzeczy niewyjaśnione i nie dające się wytłumaczyć mianem ‘magii’ lub po prostu zjawisk paranormalnych, i przez to, ich naukowe podejście jest mocno ograniczone.
