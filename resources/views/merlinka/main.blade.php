        <h1>Podstawowe zasady:</h1>

<ol>
<li>Organizatorem konkursu jest <span class="important">Reny Boursier</span>, który sprawdza prace, wybiera sędziów i przyznaje dyplomy. Do niego należy ostateczny głos w sprawach spornych i takich, których wyjaśnia poniższy regulamin. </li>
<li>Wymagania techniczne podane zostaną zawsze przy ogłoszeniu konkursu. </li>
<li>Konkurs może trwać do miesiąca, dokładny termin na napisanie eseju zawsze jest podawany przy ogłaszaniu konkursu.</li>
<li>W Merlince może wziąć udział każdy uczeń i nauczyciel, pod warunkiem że nie jest sędzią w konkursie.</li>
<li>Prace mogą (choć nie muszą) zostać opublikowane w formie załącznika po zakończeniu konkursu, jako zbiór esejów z tej edycji konkursu, pod warunkiem że autorzy pracy się na to zgodzą. </li>
<li>Każdy uczestnik otrzymuje pamiątkowy dyplom.</li>
</ol>

        <h1>WYBIERANIE TEMATU:</h1>

<ol>
  <li>Tematy zostają podane wraz z ogłoszeniem konkursu.</li>
  <li>Uczestnicy wybierają wyłącznie jeden temat z podanych przez organizatora. </li>
  <li>Po wysłaniu jednej pracy nie ma możliwości na wysłanie kolejnej, na inny temat. </li>
  <li>Istnieje duże prawdopodobieństwo, że podany przez organizatora temat był kiedyś realizowany na jego zajęciach lub w konkursach, które organizował. W takim wypadku uczestnik nie może wysłać gotowej już pracy, będzie to sprawdzone przez organizatora i potraktowane jako oszustwo, a autor zostanie zdyskwalifikowany. Uczestnik może jednak czerpać wiadomości ze swojej pracy, cytować niektóre wypowiedzi czy zawierać przemyślenia i własne spostrzeżenia, które tam zapisał. Musi jednak pamiętać o zamieszczeniu tego w bibliografii i dodaniu odpowiednich odnośników.</li>
</ol>

        <h1>PISANIE PRACY:</h1>

<ol>
  <li>Esej jest formą przedstawienia własnego zdania, własnej opinii na jeden z tematów. Powinna być to więc praca samodzielna, zawierająca minimum 3 argumenty, na których zostanie oparty punkt widzenia autora. </li>
  <li>Jeśli w pracy znajdują się cytaty lub odniesienia do artykułów, stron czy innych źródeł, autor powinien dodać odnośnik, z czego zostały zaczerpnięte informacje lub z jakiego artykułu jest cytowana dana wypowiedź. W przypadku braku takich odnośników i jednoznacznym powielaniu nieswoich słów, praca może zostać zdyskwalifikowana lub może otrzymać punkty karne. </li>
  <li>Zamieszczenie bibliografii, odnośników do cytowanych wypowiedzi czy odnośnika do fragmentu akapitu, który był oparty wyłącznie o dane źródło (a nie zawiera swoich przemyśleń), daje pewność, że praca nie będzie zdyskwalifikowana. </li>
  <li>Praca może zawierać dodatkowe zdjęcia, tabelki czy diagramy. Wpłynie to na pewno na samą wartość pracy, nie wpłynie jednak na wymaganą wielkość eseju.</li>
</ol>

        <h1>OCENA:</h1>

<ol>
  <li>Praca będzie oceniana według kryteriów podanych poniżej. </li>
  <li>Szczegółowa ocena pracy nie będzie zamieszczana do publicznego wglądu, podane zostaną ostateczne punkty zdobyte przez uczestnika.</li>
  <li>Oprócz podstawowych kryteriów oceny pracy, uczestnik zdobywa również punkty od sędziów.</li>
  <li>Przedstawione argumenty czy wiadomości, na których opiera swoje zdanie autor, będą sprawdzane pod względem zgodności z kanonem serii HP, w jej skład wchodzą książki o Harrym Potterze, książki o Świecie Czarodziejów napisane przez Rowling, ale i wiadomości podane na Pottermore czy gry (np. Księga Czarów), na które autorka serii również miała wpływ. Inne wiadomości, które zostały wymyślone przez 'czarodziejów' z ŚM lub pochodzą z mugolskich źródeł, nie będą brane pod uwagę.</li>
</ol>

