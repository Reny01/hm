<h1>Amortencja</h1>
  <img class="img-fluid lewy2" src="../img/pod_amortencja.gif">
<p>Amortencja to nazwa najsilniejszego eliksiru miłosnego na świecie. Nie wzbudza ona jednak prawdziwej miłości, powoduje jedynie silne zauroczenie albo obsesję. Łatwo ją poznać po szczególnym perłowym połysku i oparach tworzących charakterystyczne spirale. Siła tego eliksiru zależy od długości jego przetrzymywania. Sam zapach amortencji każdy odczuwa inaczej, w zależności od tego, co go najbardziej pociąga i co lubi.</p>
<p>Eliksir został wykorzystany w serii przez Meropę Gaunt, zakochaną w mugolu Tomie Riddle`u Sr., który nie zwracał na nią uwagi. Kobieta postanowiła spoić go magicznym wywarem, by być blisko ukochanego. Gdy Meropa zaszła w ciążę, postanowiła przerwać podawanie amortencji mając nadzieję, że jej ukochany pokocha ją prawdziwą miłością. Tak się nie stało, Meropa została sama w ciąży, a syn znany później jako Lord Voldemort nie potrafił kochać, co wynikało z jego poczęcia po użyciu ww. eliksiru.</p>
<p>Amortencję oraz wiele innych eliksirów miłosnych stworzyła Laverne de Montmorency w XIX wieku. </p>
</div>

