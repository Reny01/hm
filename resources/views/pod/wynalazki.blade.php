<h1>Łajnobomba</h1>

<p>Łajnobomba to nic innego jak magiczna, śmierdząca zabawka służąca do robienia dowcipów. Kiedy podniesie się łajnobombę, ręce osoby pozostaną brudnę przez jakiś czas. Została stworzona w XIX wieku przez Alberyka Grunniona, który za ich wynalezienie zosta umieszczony na kartach z Czekoladowymi Żabami. Choć łajnobomby były bardzo popularne wśród młodych czarodziejów, sam ich twórca - Alberyk, nigdy nie uzyska takiego statusu i takiej popularności jak one.</p>
<p>Same łajnobomby były bardzo popularne wśród uczniów Hogwartu, były jednak znienawidzone przez woźnego - Argusa Filcha. Bliźniacy Weasleyowie podłożyli łajnobombę ciotce Muriel, przez co przestała ona odwiedzać Norę co roku. W 1994 roku Harry Potter podarował Ronowi Weasleyowi na święta pudło łajnobomb.</p>
