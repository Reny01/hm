  <h2>Horkruks  </h2>
  <p>Horkruks jest to czarnomagiczny przedmiot, w którym ktoś ukrył cząstke własnej duszy. Rozszczepienie własnej duszy jest uznawanem za akt gwałtu i jest sprzeczne z naturą. Podzielenie własnej duszy dokonuje się poprzez morderstwo. Horkruks został wynaleziony przez Herpona Podłego w starożytności.  </p>
</div>
<div class="col-4 page-hm2">
  <h2>Fasolki wszystkich smaków  </h2>
  <p>Fasolki wszystkich smaków to ulubione cukierki młodych czarodziejów w kształcie fasolek, które mogą przybierać każdy możliwy smak - dosłownie. Zostały wynalezione przez Bertiego Botta w XX wieku.  </p>
</div>
<div class="col-4 page-hm2">
  <h2>Samomieszalny kociołek  </h2>
  <p>Samomieszalny kociołek jest rodzajem kociołka wynalezionym przez Gasparda Singletona w XX wieku, ma asystować czarodziejowi w mieszaniu jego eliksiru.  </p>
</div>
</div>
<div class="row">
<div class="col-4 page-hm2">
  <h2>Bombonierki Lesera</h2>
  <p>Bombonierki Lesera są zestawem cukierków wynalezionych przez Freda i George'a Weasleyów w 1995 roku. Ich zjedzenie powoduje różne objawy chorobowe umożliwiające "urwanie się" z lekcji. Każdy cukierek ma dwie połówki - jedna wywołuje doległiwości, zaś druga je hamuje. Nie jest to jedyny wynalazek braci Weasleyów. </p>
</div>
<div class="col-4 page-hm2">
  <h2>LEK NA SMOCZĄ OSPĘ</h2>
  <p>Lek wynaleziony przez Gunhildę z Gorsemoor na przełomie XVI oraz XVII wieku, lek zwalczał chorobę zakaźną, która mogła być nawet śmiertelna. Występuje ona tylko u społeczności czarodziejów. Objawy są podobne do mugolskiej ospy, z tym, że oprócz charakterystycznej wysypki u chorego, występuje też zabarwienie skóry na zielono, a twarz pokrywa się bolesnymi dziobami.</p>
</div>
<div class="col-4 page-hm2">
  <h2>Wywar Tojadowy</h2>
  <p>Wywar tojadowy przydaje się osobom zarażonym likantropią, czyli wilkołactwem. Przez tydzień poprzedzający pełnię, taka osoba zażywa eliksir. W noc pełni, kiedy to księżyc ukazuje się w całej swej okazałości, zarażony człowiek przemienia się w wilkołaka, lecz zachowuje świadomość i umysł ludzki. Został wynaleziony przez Damoclesa (prawdopodobnie nazywał się Damocles Belby) najprawdopodobniej w roku 1970. </p>
</div>

<div class="row">
<div class="col-4 page-hm2">
  <h2>Eliksir Pieprzowy</h2>
  <p>Eliksir Pieprzowy został wynaleziony przez Glovera Hipwortha w XVIII wieku, jest to eliksir rozgrzewający, który pozwalał złagodzić objawy przeziębienia i katar, chociaż w pełni nie gwarantował uzdrowienia. Mógł wystąpić skutek uboczny, a mianowicie wydobywanie się pary z uszu nawet do kilku godzin po zażyciu eliksiru. Eliksir Glovera opiera się jednak na eliksirze pieprzowym wynalezionym przez Linfreda ze Stinchcombe, jest to jednak unowocześniona forma.</p>
</div>
<div class="col-4 page-hm2">
  <h2>Szkiele-Wzro</h2>
  <p>Eliksir powodujący odtworzenie kości, który przy zażyciu palił w usta i gardło, dusił i wywoływał kaszel. Proces odrastania kości przy użyciu Szkiele-Wzro trwał jedną noc, więc, jak na możliwości magiczne, bardzo powoli. Eliksir zosta wynaleziony najprawdopodobniej przez Linfreda ze Stinchcombe w XII wieku. </p>
</div>
<div class="col-4 page-hm2">
  <h2>Złoty Znicz</h2>
  <p>Złoty znicz jest to złota kula wielkości orzecha włoskiego obdarzona maleńkimi, srebrnymi skrzydełkami poruszającymi się ruchem obrotowym. Wykorzystywana w Quidditchu, za jej złapanie zawodnik zyskuje 150 punktów dla swojej drużyny. Złoty znicz został wynaleziony przez Bowmana Wrighta w XVI wieku.  </p>
</div>


