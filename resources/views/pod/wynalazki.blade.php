  <h2>Lunaskop  </h2>
  <p>Lunaskop jest drewnianym lub metalowym przyrządem służacym do badania faz Księżyca oraz ułatwia sporządzanie wykresów związanych z fazami, wynaleziony został przez Perpetue Fancourt w I połowie XX wieku. </p>
</div>
<div class="col-4 page-hm2">
  <h2>Ulizanna </h2>
  <p>Ulizanna to nazwa eliksiru dla czarownic oraz czarodziejów, który pomaga w układaniu wszystkich rodzajów włosów. Został wynaleziony przez Fleamonta Pottera, dziadka Harry'ego w II połowie XX wieku. Fleamont zbił na nim majątek. </p>
</div>
<div class="col-4 page-hm2">
  <h2>Quod </h2>
  <p>Quod jest magiczną, wybuchającą piłką, którą trzeba było przerzucić przez garnek aby zdobyć punkt w grze o nazwie Quodpot. Piłka jak i sama gra została wymyślona przez Abrahama Peasegooda w XVIII wieku.  </p>
</div>
</div>
<div class="row">
<div class="col-4 page-hm2">
  <h2>Proszek Fiuu</h2>
  <p>Proszek Fiuu jest magiczną substancją, która pozwala na porozumiewanie się oraz przemieszczenia w obrębie sieci Fiuu. Proszek jak i cała sieć została wymyślona przez Ignatię Wildsmith, na przełomie XIII i XIV wieku. </p>
</div>
<div class="col-4 page-hm2">
  <h2>Krwawe pióro</h2>
  <p>Krwawe pióro jest przedmiotem wynalezionym przez Dolores Umbridge w XX wieku, nie wiadomo dokładnie kiedy powstało, jednak już w 1995 roku istniało. Jest to czarnomagiczny przedmiot tortur, pióro nie wymaga atramentu ponieważ piszę się nim za pomocą krwi osoby, któa go używa. </p>
</div>
<div class="col-4 page-hm2">
  <h2>Fałszoskop</h2>
  <p>Fałszoskop jest pewnym rodzajem wykrywacza czarnej magii, przedmiot przypomina szklany bączek dla dzieci. Sam ustawia się pionowo na spiczastym końcu, a kiedy w pobliżu pojawi się ktoś nieuczciwy lub niegodny zaufania zaczyna kręcić się, świecić oraz gwizdać. Wynaleziony przez Edgara Stroulgera w XVIII wieku. </p>
