<h2>TABELA KAMIENIA I <br>jego INTERPRETACJA</h2>
<p>Po skończeniu praktyki należy stworzyć odpowiednią tabelę, która będzie opisywała nasz kamień. Taka tabela powinna zawierać najważniejsze informacje dotyczące kamienia, które pozwolą w łatwiejszy sposób na jego właściwą ocenę. Tabela powinna zawierać również interpretację, czyli to jaką energię, jakie właściwości i jak ostatecznie nasz kamień będzie się zachowywał.</p>
<p>Tabela jest o tyle ważna, że stanowi wizytówkę naszego kamienia, w łatwy i szybki sposób pozwala zapoznać się z właściwościami kamienia, z jego charakterem czy zastosowaniem.</p>
<p>Po prawej znajdziecie przykładową tabelę z informacjami jakie <span class="important">muszą</span> się w niej znaleźć. Dodatkowo można dodać inne pola jak choćby własna nazwa kamienia.</p>
