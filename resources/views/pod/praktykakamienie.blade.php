  <h2> Losowanie Kamienia </h2>
  <img class="img-fluid lewy2" src="../../img/bm/stone.gif">
  <p>Przy praktykach odbywających się na lekcjach wasze kamienie jakie użyjecie zostaną wylosowane na zajęciach. Dopiero po zdobyciu większego doświadczenia oraz obycia w tworzeniu własnych kamieni, można samemu dokonywać wyboru i manipulować odpowiednimi kamieniami oraz mineraami. Na zajęciach jednak uczniowie pracują z tym co otrzymają, nawet jeśli za takim kamieniem czy minerałem nie przepadają, muszą umieć go wykorzystać i znaleźć jego zastosowanie. </p>

<p>Po wylosowaniu kamieni należy zanotować odpowiednie informacje na ich temat, tj. ich <span class="important">twardość według skali MOHSA</span> oraz przypisane im <span class="important">kolory</span>, które później zostaną wykorzystane. </p>

