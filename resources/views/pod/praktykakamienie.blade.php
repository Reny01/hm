<h2>Początek Praktyki</h2>
<p>Początek praktyki związany jest z przygotowaniem Twojego stanowiska pracy, na który składają się takie elementy jak:</p>
<ul>   <li>Przygotowanie wszystkich niezbędnych elementów/składników.</li>
  <li>Ustawienie kociołka na trójnogu. </li>
  <li>Dodanie <span class="important">litra wody</span>! </li>
  <li>Rozpalenie ognia pod kociołkiem i odczekanie aż woda będzie zagotowana.</li>
</ul>
</div>
<div class="col-6 page-hm">
<h2>Koniec Praktyki</h2>
<p> Po dodaniu barwnika, nie ingerujemy już w żaden sposób w zawartość kociołka. Jednak to nie jest koniec naszej pracy! </p>
<p>Należy przez około godzinę podtrzymywać ogień pod kociołkiem. Jeśli z kociołka unosi się dym, znaczy że nasza praktyka się udała. Po odparowaniu wszystkich cieczy, na samym dnie kociołka znajdziemy nasz kamień. </p>
<p> Na sam koniec musimy posprzątać nasze stanowisko.</p>
</p>

