<h1>Proces Tworzenia Kamieni</h1>

<img class="img-fluid lewy2" src="../../img/bm/stone2.gif">
<p>Praktyka tworzenia własnego kamienia o właściwościach leczniczych i wspomagających <span class="important">została w pełni stworzona przeze mnie, tj. przez Reny'ego Boursiera </span>(jeśli ktoś jeszcze nie wie do kogo należy ten podręcznik). Wszystkie zasady i procesy, kolejność dodawania poszczególnych elementów czy same zasady dodawania tych elementów zostały wymyślone przeze mnie. Prosiłbym o uszanowanie mojej pracy i nie kopiowanie tej praktyki w żaden sposób, jest to moje autorskie dzieło i chciałbym aby było stosowane wyłącznie przeze mnie na moich zajęciach.</p>
<p>W tym rozdziale dowiecie się więcej o samym procesie tworzenia kamienia, to tutaj zawarte są informacje o najważniejszych zasadach oraz instrukcjach, o kolejności dodwania elementów w praktyce. Opisane są również niektóre elementy praktyki, pozostałe (które również mogą zawierać zasady) zostały podzielone na inne rozdziały. Całość stanowi praktykę w tworzeniu kamienia. </p>
