<li> Pierwszą najważniejszą zasadą przy tworzeniu kamieni jest wybór odpowiedniego kociołka, w którym mieszane będą składniki. </li>
<li> Przy wyborze kociołka zwracamy uwagę na średnią twardość dwóch kamieni szlachetnych, z których będziemy tworzyć swój kamień.</li>
<li> Twardość kamieni według skali MOHSA określona została na specjalnie przygotowanych tablicach, z których odczytujemy <span class="important"> najniższą wartość</span> dla danego kamienia, czyli np. jeśli skala MOHSA dla kamienia została określona w przedziale np. 5 - 6, bierzemy pod uwagę 5. </li>
<li> Dwie odczytane wartości twardości kamieni (w praktyce używamy dwóch kamieni) dadzą nam wartość średnią, która posłuży nam do wybrania odpowiedniego kociołka. </li>
