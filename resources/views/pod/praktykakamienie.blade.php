<h2>ZASADY PRAKTYKI</h2>
<ol>
  <li> Początek i koniec praktyki jest równie ważny co pozostałe czynności. </li>
<li> Pamiętaj o używaniu odpowiednich zaklęć, wszystko powinno odbywać się za pomocą odpowiednich czarów, istnieją dwa wyjątki: dodawanie cieczy do kociołka (poza wodą), uzupełnienie tabeli stworzonego kamienia.</li>
<li> Używamy wszystkich zaklęć podstawowych wymienionych w rozdziale zaklęcia i przynajmniej jednego dodatkowego!</li>
<li> Po dodaniu każdego stałego elementu (np. kamień, liść, paznokieć) należy zamieszać w kierunku zgodnym z ruchem wskazówek zegara dwa razy! (Manipulacja różdżką) </li>
<li> Po dodaniu każdego składnika podstawowego i składnika głównego, który jest cieczą, należy zamieszać w kierunku przeciwnym do ruchu wskazówek zegara pięć razy! (Manipulacja różdżką) </li>
<li> Wszystkie czynności zapisujemy w klimacie. </li>
<li> Tabela stworzonego kamienia jest obowiązkowo do zrobienia przy praktyce! </li>
<li> Zasady dodawania konkretnych elementów czy ich wykonywania przy praktyce zostały opisane w innych rozdziałach.</li>
</ol>

