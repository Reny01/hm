<h2>Czarodzieje w oczach mugoli</h2>
<img src="../img/pod_woczach.gif" width="200" class="img-fluid lewy2">
<p>W Średniowieczu samo słowo magia uchodziło za temat tabu. Mugole postrzegali czarodziejów jako wysłanników szatana, złych kusicieli, a niekiedy jako demony w ludzkich postaciach. Co ciekawe to głównie kobiety miały być kusicielkami, jako płeć słabsza, (tak postrzegano w tamtym okresie) miała łatwiej ulegać mocy szatana. </p>

<p>Dla niemagicznych ludzi, czarownica w tamtym okresie musiała mieć przede wszystkim krzywy nos, wielki garb czy pełno pryszczy na twarzy, najlepiej jeśli miała zielony kolor skóry. Oczywiście to była jej naturalna forma, bo swoimi sztuczkami mogła zmieniać dowolną postać. Jej ulubioną miała być postać starszej kobiety, staruchy. Niektórzy nawet twierdzili, że magowie, tudzież czarownice, posiadają cechy przypisywane zwierzętom, takie jak np. ogon. Żeby było jednak jasne, tak postrzegali mugole czarodziejów, a sama prawda była zupełnie inna.</p>

<p>W tym okresie czarodzieje nie cieszyli się zbytnim szacunkiem czy pozytywną popularnością, bo popularni niewątpliwie byli, ale w negatywnym świetle. Palenie na stosie czy zwykłe tortury były właśnie przejawami uczuć jakimi mugole pałali do czarodziejów. </p>
