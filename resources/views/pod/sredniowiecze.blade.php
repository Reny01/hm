<div class="col-6 page-hm">
<h2>Rozwój magii</h2>
<p>Jak, biorąc pod uwagę przedstawione kwestie, rozwinęła się magia w tamtym okresie? Otóż magia, jak to zwykle z nią bywa, radziła sobie całkiem nieźle. Mimo z pozoru bardzo ograniczonego pola popisu, praktyki magiczne były jeszcze bardziej doskonalone, rozszerzając swe pole działania w nowe dziedziny. Sytuacja w średniowieczu wymusiła na czarodziejach pewne działania, zaczęli formować pierwsze organy władzy, które miały nimi rządzić, powstawały pierwsze szkoły magii, które miały znacząco przyczynić się do wzrostu wykształconych magów. To wszystko sprawiło, że magia, z pozoru silnie potępiana przez mugoli, rozwijała się w bardzo dobrym kierunku.</p>
