<h2>Dostępność magii</h2>
<img src="../img/pod_dostepnosc.gif" width="200" class="img-fluid lewy2">
<p>W starożytności przekonaliśmy się, że magią zajmowali się głównie wysocy dostojnicy, kapłani lub inna, konkretna grupa ludzi, o konkretnym statusie czy cechach społecznych. Jeśli chodzi o średniowiecze to można tu mówić o rozwoju tak zwanej magii świeckiej. Zapotrzebowanie na kapłanów w takiej formie, w jakiej kultywowano to w starożytności po prostu malało. Coraz silniejsze wpływy miała religia chrześcijańska, która całkowicie krytykowała jakiekolwiek przejawy magii, porównując ją do konszachtów z diabłem. Właśnie dlatego coraz częściej czarodziejami stawali się zwykli ludzie, normalni, nie należący do konkretnej grupy społecznej. Liczył się więc przede wszystkim talent magiczny, a więc dostępność do niej była większa, otwarta na tych czarodziejów, którzy wcześniej nie mogli sobie pozwolić na naukę. </p>

 </div>
<div class="col-6 page-hm">
<h2>Pozycja czarodziejów</h2>
<p>Średniowieczni magowie stracili znacząco na pozycji w porównaniu do poprzednich okresów historycznych. Przez to, że magia stawała się dostępna dla zwykłych ludzi, czarodziej w tym okresie zajmował się też typowymi dla mugoli pracami. Ich pozycja była więc bardzo zróżnicowana, ale mocno ograniczona. Mimo przyjęcia typowych prac mugoli, większość czarodziejów nie wstydziła się tego, w kolejnych rozdziałach poznacie historię sir Cadogana, który właśnie przejawiał taką postawę.</p>
<img src="../img/pod_pozycja.gif" width="200" class="img-fluid prawy2">
<p>Krotko mówiąc, czarodziej w tym okresie mógł zajmować się wszystkim, nie tak jak w starożytności konkretna pracą, charakterystyczną dla magów, ale wszystkim. Była więc to kwestia indywidualnych predyspozycji i potrzeb.</p>
