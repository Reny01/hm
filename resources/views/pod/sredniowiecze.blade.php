<h1> WPROWADZENIE DO ŚREDNIOWIECZA</h1>
<img class="img-fluid lewy" src="../img/pod_sredniowiecze.gif">
<p>Średniowiecze jest dla czarodziejów okresem pełnym zmian, chcąc odnaleźć się w nim musimy całkowicie zapomnieć o zasadach i faktach jakie poznaliśmy w Starożytności na temat magów i samej magii. W tym okresie czarodzieje zmuszeni byli do całkowitej zmiany swojego trybu życia, spowodowane to było głównie polowaniami na czarownice, które w tym czasie stawały się coraz bardziej popularne. Mugole nie byli już tak naiwni i podatni na sugestie czarodziejów jak w Starożytności. Zmienili całkowicie swoje stanowisko w tej sprawie, a tym samym wymusili na czarodziejach pewne ustępstwa i przystosowanie się. </p>

<p>O samych polowaniach powiem jednak w następnym rozdziale, teraz mniej więcej przytoczę obraz średniowiecznego maga i magii pod kilkoma kątami. </p>

