<h1> MAGIA W CHRZEŚCIJAŃSTWIE</h1>
<img class="img-fluid lewy" src="../img/pod_mch.jpg">
<p>Na wstępie tylko dodam, że magia w chrześcijaństwie trafia do działu Prehistorii nie dlatego, że dotyczy głównie tego okresu, choć powstanie Starego i Nowego Testamentu przypisane jest do Starożytności, trafia do Prehistorii ponieważ opisuje historie od początku powstania świata, historie kuszenia (o której mowa będzie dalej), a chcąc zachować pewną ciągłość wydarzeń i zmian, wrzuciłem ten temat do tego działu. Nie da się jednak wszystkich tematów określić w jednym okresie historycznym, to jest niewykonalne bo wydarzenia tak się nie dzielą, niektóre rozgrywają się przez kilkaset lat, przechodząc od prehistorii do starożytności, od starożytności do średniowiecza i podobnie, jednak chcąc poznawać historię trzeba zachować pewną ciągłość wydarzeń i podział czasowy. </p>

<p>Jeśli chcemy doszukiwać się historii lub opowieści związanych z magią i czarodziejami najlepszym ‘źródłem’ są wierzenia religijne. Magia i postacie czarodziejów bardzo często występowały w opowieściach, tradycjach i wierzeniach przekazywanych w przeróżnych religiach. Chrześcijaństwo jest właśnie jedną z nich, znając przeróżne opowieści pojawiające się w Starym czy Nowym Testamencie, można doszukać się, w bardzo prosty sposób, odpowiedników na czarodziejski świat. Wiele osób nie popiera tego, twierdząc, że jest to naginanie pewnych zasad, do tego mocno przesadzone. Sam jestem na skraju tego wszystkiego, uważam, że pewnych rzeczy nie powinno się robić i zmieniać. Religia jest bardzo delikatnym tematem, każdy kto tak naprawdę w coś wierzy, związany jest z jakąś religią, odnosi się do niej z szacunkiem, z szacunkiem do jej prawd i wierzeń. Przeinaczanie więc tego może wywołać u niektórych negatywne emocje. Nie chcę by to było postrzegane w taki sposób, nie chcę też być źle odebranym, a wiem, że przez niektórych tak może to wyglądać. Dlatego postanowiłem odnieść się tylko do jednej historii, jednej opowieści. Uznałem, że jest ona na tyle ciekawa, że warto o niej wspomnieć. Rzuca światło na początek relacji między czarodziejami, a mugolami. Można się w niej też doszukiwać narodzenia pierwszych konfliktów między nimi, pierwszych powodów dlaczego tak się stało i samych motywów. </p>

<p>Z góry również przepraszam jeśli ten temat kogoś uraził. Nie było to moim celem, doszukuje się jednak takich historii, które mają odbicie w Świecie Czarodziejów i można je wykorzystać w odpowiedni sposób. Przedstawiona wersja historii nie jest w żaden sposób prawdziwa czy potwierdzona, nie tak musiało być, jest to jednak jedna z interpretacji, która w samym Świecie Magii pojawia się od bardzo dawna.</p>

