<p>Zasada rzucania zaklęć przy praktyce jest dość prosta, musimy się przede wszystkim skupić oraz określić to co zamierzamy zrobić lub jak chcemy aby zaklęcie zadziałało, następnie wybieramy (dowolnie) ruch różdżki i wypowiadamy zaklęcia. Nie wymagam od was ani odpowiedniego ruchu różdżki, ani akcentu, ani innych rzeczy, które dotyczą praktyki z samymi zaklęciami, a które możecie poznać na lekcjach z Zaklęć i Uroków. </p>

