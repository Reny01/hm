<h1>ZAKLĘCIA STOSOWANE W PRAKTYCE</h1>

<p>Kolejnym równie ważnym elementem są zaklęcia. Praktyka Tworzenia Kamieni musi odbywać się za pomocą magii, inaczej w taki sposób, w jaki my planujemy stworzyć nasze kamienie, nie będzie to możliwe. Równie ważne jest skupienie przy rzucaniu zaklęć oraz odpowiednia motywacja i przerzucenie naszych pragnień w wypowiadane zaklęcia czy same formuły. Pamiętajcie, że jesteście czarodziejami i pewne czynności przy praktykach z magią wymagają jej użycia, dlatego same zaklęcia mają wam ułatwić pracę, mają przelać magię w wasze nawet najzwyczajniejsze czynności. </p>

