<h1>Prawo w Świecie Czarodziejów</h1>

<img class="img-fluid prawy" src="../img/pod_prawo.png">
<p>Świat Czarodziejów jako zintegrowana społeczność żyjąca pod przewodnictwem różnych instytucji oraz rządów jest zobligowana do przestrzegania różnych praw. Na temat niektórych z nich w moim podręczniku istnieją osobe rozdziały, gdzie znajdziecie więcej informacji na ich temat. W tym rozdziale postaram się zebrać najważniejsze informacje na ich temat oraz opisać te, o których nie zdążyłem nigdzie wspomnieć.</p>
<p>Tak jak powiedziałem na początku, prawo może różnić się w zależności od państwa, w którym dany czarodziej żyje. Istnieje jednak kilka dekretów jak Międzynarodowy Kodeks Tajności Czarodziejów, które obowiązują każdego czarodzieja niezależnie w jakim regionie żyje. Proszę również pamiętać, że przytoczone informacje nie są kompletne bo nie mamy dostępu do oficjalnych dokumentów, w których te postanowienia zostały zapisane. </p>

<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Międzynarodowy Kodeks Tajności</center></div>
  <div class="col-2"></div>
</div>
<p>Międzynarodowy Kodeks Tajności Czarodziejów został podpisany w 1689 roku, ale oficjalnie wszedł w życie w roku 1692. Prawo to zostało spisane przez Międzynarodową Konfederacje Czarodziejów. Główne założenie kodeksu miało zapewnić ochronę czarodziejom przed mugolami i całkowicie ukryć ich obecność przed światem niemagicznym.</p>
<p>W myśl kodeksu, każda osoba rządząca społecznością czarodziejów w danym kraju czyli Minister Magii jest odpowiedzialny za ukrywanie wszelkich przejawów magii w swoim państwie. Musiał uwzględnić takie aspekty życia jak choćby hodowla i kontrola nad magicznymi stworzeniami, przejawy magii wśród niepełnoletnich czarodziejów czy choćby kontrola nad rozgrywkami magicznych gier i sportów, które nie narażają na odkrycie świata czarodziejów przez mugoli.</p>
<p>Złamanie ustaleń kodeksu czy też jawne i świadome używanie magii przed osobami niemagicznymi jest równoznaczne z pogwałceniem prawa, i na takie czarodzieja nałożone zostaną odpowiednie kary. Oczywiście można wyróżnić kilka wyjątków, jak choćby zagrożenie życia.</p>
<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Kodeks Użycia Różdżki</center></div>
  <div class="col-2"></div>
</div>
<p>Akt prawa magicznego uchwalony w 1631 r. przez Radę Czarodziejów w formie dekretu, regulujący kwestie związane z posiadaniem i używaniem różdżek. Na mocy klauzyli z paragrafu 3, która nosiła nazwę "Pozbawienie Prawa do Różdżki" (z angielskiego Wand Ban) i brzmiała "Żadnemu nie-ludzkiemu stworzeniu nie wolno nosić lub używać różdżki.", czarodzieje wykluczyli możliwość legalnego używania różdżek przez inne rasy władające magią, np. gobliny czy skrzaty domowe.</p>
<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Zakaz Eksperymentalnej Hodowli </center></div>
  <div class="col-2"></div>
</div>
<p>Zakaz mający na celu skuteczne zapobieganie hodowania na terenie Wielkiej Brytanii nieznanych i nie dających się oswoić magicznych stworzeń, m.in. bazyliszków. Za złamanie obowiązującego przepisu groziła kara pozbawienia wolności: wtrącenie do Azkabanu. Zakaz Eksperymentalnej Hodowli wprowadził w 1965 roku sławny w świecie czarodziejów magizoolog Newton Skamander, który był bardzo dumny z faktu, iż to właśnie jemu udało się wprowadzić ten zakaz.</p>

<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Regulacje dot. Wymiany Materiałów </center></div>
  <div class="col-2"></div>
</div>
<p>Istnieje regulacja sporządzona przez Ministerstwo Magii, w której zawarta jest lista materiałów nie podlegających wymianie, to znaczy że czarodzieje mają zakaz handlu tym przedmiotem w Świecie Czarodziejów. Istnieje kilka klas materiałów niepodlegających wymianie, każdy obiekt przypisany jest do klasy w zależności od swojej wartości oraz natury. Wydaje mi się również prawdopodobne, że czarodzieje mogą uzyskać pozwolenie na handel danym materiałem, jednak im wyższa klasa tym pozwolenie jest trudniej dostać lub wymaga więcej zaświadczeń czy gwarancji. Do znanych materiałów klasy A zaliczamy: jaja akromantuli, jaja chimery, smocze jaja, do znanych materiałów klasy C zaliczymy nasiona jadowitej tentakuli. </p>

<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Międzynarodowy Zakaz Pojedynków</center></div>
  <div class="col-2"></div>
</div>

<p>Międzynarodowy zakaz pojedynków jestem zakaz uznawany za międzynarodowy, prawdopodobnie został przyjęty przez Międzynarodową Konfederację Czarodziejów po 1994 roku. Zimą 1994 roku brytyjskie Ministerstwo Magii zaakceptowało go i usiłowało przekonać inne czarodziejskie rządy aby zrobiły to samo, próbowali nawet przekonać inne kraje do poparcie i wprowadzenia tego prawa. Percy Weasley, który pracował dla Ministerstwa, próbował przekonać Siedmiogrodzkiego Szefa Współpracy Magicznej, by to podpisał.</P>

  <div class="row">
    <div class="col-2"></div>
    <div class="col-8 page-hm3"><center>Prawo Rappaport</center></div>
    <div class="col-2"></div>
  </div>
<p>Prawo przeforsowane przez Emily Rappaport i uchwalone w 1790 roku przez Magiczny Kongres Stanów Zjednoczonych Ameryki obowiązujące w Stanach Zjednoczonych. Prawo obowiązywało do 1965 roku. Zabraniało kontaktów czarodziejów z niemagami, narzucając ścisłą segregację między nimi. Czarodziejom nie wolno było nawiązywać przyjaźni ani poślubiać niemagów, a kary za złamanie tego prawa były niezwykle surowe. Komunikację z niemagami dopuszczano wyłącznie w zakresie niezbędnym do wykonywania codziennych czynności. Zgodnie z tym prawem czarodziej dopiero po osiągnięciu pełnoletniości mógł legalnie posiadać różdżkę poza szkołą: różdżki były przyznawane młodym czarodziejom, gdy uczniowie po raz pierwszy przybyli do Ilvermorny, i musiały pozostać w szkole na czas wakacji. </p>
<div class="row">
  <div class="col-2"></div>
  <div class="col-8 page-hm3"><center>Pozwolenie na różdżkę</center></div>
  <div class="col-2"></div>
</div>
<p>Ustawodawstwo MACUSA wprowadzone pod koniec XIX wieku. Każdy czarodziej i czarownica w Stanach Zjednoczonych musieli je posiadać obowiązkowo. Przepis ten miał umożliwić kontrolowanie wszelkiej magicznej aktywności i identyfikację czarodzieja, który wykorzystał swoją różdżkę, aby złamać Międzynarodowy Kodeks Tajności czy też prawo Rappaport.</p>

<p>Każdy – mieszkańcy jak i przybysze – musieli posiadać pozwolenie na różdżkę. Niespełnienie tego warunku powodowało pozbawienie wolności, ściganie prawne i konfiskatę różdżki. Aby je uzyskać należało złożyć podanie.</p>
