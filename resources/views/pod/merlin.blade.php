<h1>MORGANA LE FAY</h1>

<img class="img-fluid prawy" src="../img/pod_morgana.jpg">
<p>Morgana le Fay jest czarownicą żyjąca w czasach średniowiecznych, jej status krwi jest nieznany i w przeciwieństwie do samego Merlina nie mamy żadnych przypuszczeń ani podpowiedzi, że mogła uczęszczać do Hogwartu. Jest uważana za żeński odpowiednik samego Merlina, choć jej postać jest bardziej kojarzona z czarną magią. </p>
<p>Morgana miała swoja własną kartę w czekoladowych żabach, w jej opisie znajduje się informacja, że była uzdolnioną uzdrowicielką, a także że miała ona wielką moc czarnomagiczną, którą można porównac do mocy Gellerta Grindelwalda czy samego Lorda Voldemorta. </p>
<p>Była przyrodnią siostrą króla Artura, którego najprawdopodobniej później zabiła. Sprawowała swoje rządy na wyspie Avalon, gdzie nazywano ją królową. Była również animagiem, potrafiła się zamieniać w ptaka. </p>
<p>O samej Morganie wiadomo bardzo niewiele, jednak jej postać pojawia się w wielu legendach arturiańskich. W każdej z nich, a więc pewnie i w świecie czarodziejów, jest największym przeciwnikiem Merlina.</p>
