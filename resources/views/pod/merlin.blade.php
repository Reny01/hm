<h1>MERLIN</h1>

<img class="img-fluid lewy" src="../img/pod_merlin.jpg">
<p>Merlin jest średniowiecznym czarodziejem czystej krwi, który był w posiadaniu wielkiej magicznej mocy. Tak naprawdę niewiele wiadomo o życiu i poczynaniach Merlina, oprócz skrawków informacji które poznajemy z różnych źródeł. Merlin został uwieczniony na jednej z kart z Czekoladowymi Żabami, na których znajdują się informacje o tym, że był potężnym czarodziejem, jednym z największych w swoim czasie. Na jego część powstała również prestiżowa nagroda - Order Merlina, która trafia do najbardziej zasłużonych czarodziejów w społeczności magów. Według legendy, zielona wstążką, na której wisi się Oreder Merlina Pierwszej Klasy ma odzwierciedlać dom w Hogwarcie, do którego należał Merlin - czyli Slytherin. </p>
<p>Pochodził najprawdopodobniej z Wielkiej Byrtanii lub z Irlandii, mówi się, że różdżka Merlina była wykonana z angielskiego dębu - nie można jednak tej informacji w jakikolwiek sposób potwierdzić, ponieważ grobu Merlina do tej pory nie znaleziono.
Merlin był członkiem sądu króla Artura, walczył prawdopodobnie z największą czarnoksiężniczką tamtych czasów - Morganą le Fay, która była również przyrodnią króla Artura.</p>
<p>Wiemy, że dysponował potężną mocą, podobno był tolerancyjny i dobry dla mugoli, ubiegał się o lepsze prawa dla niemagicznych osób. Jest to informacja jaka pojawia się na kartach, niemniej jest ona nieco sprzeczna z tym, że mógł należeć do Slytherinu. Należy jednak pamiętać, że nie wszyscy Ślizgoni podzielali poglądy swojego założyciela, tak mogło być również z Merlinem, który miał swoje zdanie na ten temat, a inne cechy sprawiły że trafił do tego domu.</p>

<p>Merlin otrzymał przydomek Księcia Zaklinaczy, prawdopodobnie dlatego, że bardzo dobrze radził sobie z zaklęciami. W jednej z książek Mirandy Goshawk pada informacja, że niektórzy historycy przypisują Merlinowi wynalezienie zaklęcia Expelliarmus, nie zostało to jednak w żaden sposób potwierdzone, choć sam fakt posiadania tytułu Księcia Zaklinaczy może na to wskazywać. </p>

<p>Merlin choć żył w epoce średniowiecza nadal jest obecny w życiu i tradycjach czarodziejów, bardzo często bowiem używa się zawołania wyrażającego zdziwienie lub radośc jak choćby "Na brodę Merlina!". </p>
