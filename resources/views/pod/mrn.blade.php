<h1> MAGIA NATURALNA</h1>
<img class="img-fluid lewy" src="../img/pod_mnat.jpg">
<p>Magia naturalna opierała się na przeświadczeniu, że wszystko co jest częścią natury – ludzie, rośliny, zwierzęta, skały i minerały – przepełnione jest potężnymi, choć ukrytymi mocami nazwanymi „właściwościami okultystycznymi”. Na przykład kamienie szlachetne miały posiadać moc uzdrawiania chorych, wpływania na nastrój, a nawet sprowadzać szczęście. </p>


<p>Do właściwości ziół należało uzdrawianie – czasem wystarczało jedynie zawiesić je nad łóżkiem chorego. Nawet liczby i kolory miały swoje ukryte własności. Magia naturalna uczyła, że rośliny i zwierzęta o podobnym wyglądzie maja takie same właściwości. Na przykład zioło zwane przez wzgląd na swe podobieństwo do skorpiona scorpius miało być skutecznym lekarstwem na ukąszenie pająków. Ten rodzaj magii ma silne powiązania z białą magią, nawiązywanie harmonii z natura i czerpanie tylko z naturalnych źródeł swojej mocy, ma duży wpływ na rozwój mentalny czarodzieja. Jednak szczególne znaczenie w magii naturalnej miała także astrologia, ponieważ sądzono, iż wiele powiązań i sekretnych właściwości bierze się wprost od planet i gwiazd. To z nic czarodziej mógł czerpać swoją wiedzę i mądrość. Co więcej adept magii naturalnej musiał doskonale znać anatomię i zielarstwo.</p>

<p>Magia naturalna miała wpływ na rozwój wielu dziedzin ówcześnie stosowanej i praktykowanej magii, m.in Astronomii, Wróżbiarstwa, Numerologii, Starożytnych Run jak i również Zielarstwa i Medykomagii.</p>



