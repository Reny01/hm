<h1> MAGIA RYTUALNA</h1>
<img class="img-fluid prawy" src="../img/pod_mryt.jpg">
<p>Często nazywana również magią ceremonialną polega ona na przyzywaniu duchów, demonów z zaświatów, kontaktowaniu się z nimi i rozmowie. Nie jest to typowy rodzaj magii, nie używa się tu żadnych specyficznych zaklęć, czy uroków. Swoją moc czerpie z działania rytuałów i formuł magicznych. Jest bardzo związana z magią wysoką oraz czarną magią. Początek jej działań można odnaleźć w istnieniu plemiennych szamanów, którzy poprzez odpowiednie praktyki magiczne nawiązywali kontakt z duchami. Sam jej rozwój postępował wraz z rozwojem i śmiałością, niektórych magów. </p>

<p>Do specjalnych rytuałów przyzywania używano tzw. czarnych ksiąg – grimoires. Przypuszcza się, że ich autorami mogli być Noe, Aleksander Wielki czy nawet Mojżesz. Pisano je w czasach starożytnych do XVII wieku włącznie. Były pomocne przy wszelakich rytuałach, zaczynając od przyzywania poprzez zdobycie miłości czy przyjaźni, a kończąc nawet na zabiciu. </p>

<p>Zdjęcie przedstawia jedną z najpopularniejszych ksiąg czarnej magii, siedemnastowieczne francuskie wydanie Klucza Salomona.</p>

