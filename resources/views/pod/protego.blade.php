<h1> PROTEGO</h1>
  <img class="img-fluid lewy" src="../img/pod_protego.gif">
<p>Zaklęcie Protego inaczej nazywane zaklęciem tarczy jest podstawowym czarem bojowym, niezbędnym w przypadku pojedynków. Ochrania ono rzucającego przed innymi zaklęciami, trzeba mieć jednak dobry reﬂeks aby w odpowiednim momencie użyć tego zaklęcia. Istnieje wiele różnych zaklęć obronnych, ale czar Protego należy do najbardziej popularnych, nadających się do codziennego użytku. Najbardziej widowiskowe użycie tego zaklęcia nastąpiło w roku 1484, a jest ono związane z historią hrabiego Paunchleya. Hrabia był tyranem, który karał mieszkańców wioski za najmniejsze drobiazgi, dzieci były bite za to, że przyłapano je na jedzeniu jabłek z jego sadu, młodego kobiety musiały pracować w zamku jeśli nie dygały odpowiednio nisko, a mężczyźni znikali w tajemniczych okolicznościach jeśli tylko narzekali lub przeciwstawiali się władzy hrabiego. Pewnego dnia, hrabia postanowił zorganizować turniej rycerski, na który to zaprosił arystokrację z całej okolicy. Mieszkańcy pobliskiej wioski również musieli przybyć na turniej i pod groźbą surowych kar, musieli oni kibicować hrabiemu w starciach z rywalami. </p>
  <img class="img-fluid prawy2" src="../img/pod_protego2.gif">
<p>Młody chłopiec – Edmund Gaddlegate, odpowiedzialny za transparenty i proporce hrabiego nie mógł zjawić się na turniej ponieważ dzień wcześniej złamał nogę i był przykuty do łóżka. Hrabia wysłał więc służących, którzy przyprowadzili chłopaka i jego matkę na turniej, a w ramach kary Edmund ma zostać przywiązany do konia i zmuszony do walki przeciwko hrabiemu i innym rycerzom, którzy przybyli na turniej. Inni zawodnicy na widok młodego i rannego chłopaka zrezygnowali z turnieju. Wściekły hrabia Paunchley oznajmił więc, że sam stanie przeciwko Edmundowi. W tej właśnie chwili, znajdująca się w tłumie czarownica – Hannah Cockleford, zdecydowała że pora dać hrabiemu nauczkę. W momencie gdy ten galopował w stronę Edmunda, rzuciła między nimi tarczę. Hrabia wraz z koniem odbił się od magicznej tarczy w taki sposób, że został przygnieciony przez swojego wierzchowca. Mieszkańcy mieli nadzieję, że hrabia nie żyje, ale gdy ten się zaczął poruszać wydali z siebie jęki zawodu. Hrabia ostatecznie wrócił do zdrowia, prawie. Od tego dnia aż do końca swojego życia uważał, że jest osłem o imieniu Włochaty Cyryl. </p>





