<h1> STAROŻYTNY RZYM</h1>
<img class="img-fluid lewy" src="../img/pod_rzym.png">
<p>Rzymscy magowie w okresie Starożytności specjalizowali się głównie w magii praktycznej, wykorzystywanej do powszechnych, rutynowych czynności związanych z życiem codziennym. Niektórzy historycy mówią o dwóch ważnych postaciach związanych z ważeniem eliksirów i rozwojem tej sztuki, a mianowicie o Pliniuszu i Galenie. O ile ten pierwszy miał opisać wiele leczniczych eliksirów, historycy mówią, że nie uważał tego za przejawy magii, a tym samym siebie za czarodzieja. Galen, który był lekarzem również skomponował wiele nowych, jeszcze wtedy nieopisanych eliksirów. To co warto wiedzieć na temat eliksirów, to że sposób ważenia, a więc samo tworzenie eliksirów, dobieranie składników w Starożytnym Rzymie było niemal identyczne jak w czasach dzisiejszych.  </p>


<p>W V w. p.n.e. wprowadzono pierwszą ustawę w prawie rzymskim, zwaną Prawem Dwunastu Tablic. Według niej wszelkie przejawy magii niszczycielskiej, a więc jednego z odłamu ciemniej strony magii, były karane śmiercią. W 139 r. p.n.e. pretor Cornelius Scipio Hispalus skazał na wygnanie wszystkich astrologów i ludzi oskarżonych o czarnoksięstwo. Mimo takich zabiegów, magia stawała się coraz bardziej popularna. Ludzie, zwykli mugole chcieli wiedzieć co ich czeka w przyszłości, czego mogą się spodziewać w najbliższym czasie i jak mogą zdołać zapobiec narastającym nieszczęściom. Astrolodzy, wróżbici, madzy stawali się coraz popularniejszy.  </p>

<p>To właśnie w Rzymie pojawiły się pierwsze czarownice zwane sybillami, które jak sądzono przepowiadały przyszłość ludzi oraz samego świata. Oprócz nich, w Rzymie istniały również inne czarownice, takie jak np. szpetna Erichto, którą kojarzono ze śmiercią i zniszczeniem. Warto również wiedzieć, że to właśnie w Rzymie odkryto wilkołaki oraz strzygi (czyli wiedźmy w postaci ptaków), które dawniej rzekomo miały porywać trupy i je zjadać. </p>
