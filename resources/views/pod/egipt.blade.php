<h1>STAROŻYTNY EGIPT</h1>
<img class="img-fluid lewy" src="../img/pod_egipt.jpg">
<p>Magowie z tego okresu specjalizowali się głównie w astronomii, rozpoznali i opisali wiele magicznych stworzeń (choć nie wszystko przetrwało do dzisiaj). To co warto o nich wiedzieć to fakt, że bardzo rozwinęli magomedycynę w praktycznym jej zastosowaniu. Zaklęcia podpalające i spalające również należały do ich specjalności, dodatkowo jako ciekawostkę, o której zresztą już wspominałem, jest fakt, że pierwsze zapiski o jakichkolwiek magach właśnie pochodzą ze Starożytnego Egiptu. </p>


<p>I dla mnie, starożytni magowie z Egiptu są idealnym przykładem postawy jaką reprezentowali sobą magowie w tym okresie. Egipscy czarodzieje to przede wszystkim kapłani i to oni głównie zarządzali państwem. Faron teoretycznie był władcą Egiptu, praktycznie jednak był marionetką w rękach magów. Czarodzieje w tamtym okresie bardzo wyspecjalizowali się w legilimencji i oklumencji, potrafili manipulować nie tylko dostojnikami (takimi jak faraon), ale tez zwykłym, szarym mieszkańcem Egiptu. Jeśli coś im się nie podobało lub nie układało się tak jak to zamierzali, potrafili wykorzystać swoje umiejętności by zmanipulować drugą osobą, wykorzystać sytuację i wyjść na całym zdarzeniu najlepiej. Tak jak mówiłem, bardzo dobrze znali się na astronomii i potrafili tę wiedzę wykorzystać. Zaćmienia słońca, którymi straszyli mugoli są chyba najbardziej oczywistym przykładem. </p>

<p>To co właśnie mówię, może zostać przez niektórych bardzo negatywnie odebrane. Powtórzę jednak to co powtarzałem kilka razy i nadal będę o tym wspominał. Nie wszystko to co mówię jest prawdą, niektóre rzeczy są po prostu interpretowane przeze mnie i dostosowane do realiów z książek, przynajmniej ja właśnie tak to postrzegam. Na koniec jedynie dodam, że egipscy czarodzieje ze starożytności dają początek świadomości magom ich mocy, ich pozycji jaką mogą i są w stanie sobie zapewnić. Powoli zaczynają dostrzegać w mugolach osoby gorsze, którymi łatwo można manipulować, w końcu zauważają jak wielkie znaczenie w ich życiu sprawuje magia, i jak wiele jej zawdzięczają.</p>


