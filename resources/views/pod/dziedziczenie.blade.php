<h1>Czystość Krwi</h1>
<p>Termin "czysta krew" odnosi się do rodziny lub osoby fizycznej bez mugolskiej (nie-magicznej) krwi. Koncepcja ta jest ogólnie kojarzona z Salazarem Slytherinem, jednym z czterech założycieli Szkoły Magii i Czarodziejstwa w Hogwarcie, której awersja do nauczania kogokolwiek mugolskiego pochodzenia doprowadziła w końcu do zerwania z trzema innymi założycielami i jego rezygnacji ze szkoły.</p>
<img class="img-fluid lewy" src="../img/pod_krew.png">
<p>Dyskryminacja Slytherina na podstawie pochodzenia była uważana przez większość czarodziejów za niezwykły i błędny pogląd w tym czasie. Współczesna literatura sugeruje, że mugolaki byli nie tylko akceptowani, ale często uważani za szczególnie uzdolnionych. Przeszli pod kochającą nazwę "Magbobs" (dużo debatowano na temat pochodzenia tego terminu, ale wydaje się, że w takim przypadku magia "podskoczyła" znikąd).</p>

<p>Opinia magiczna uległa pewnej zmianie po tym, jak Międzynarodowy Kodeks Tajności wszedł w życie w 1692 roku, kiedy magiczna społeczność poszła na dobrowolne ukrywanie się po prześladowaniu przez mugoli. To był traumatyczny czas dla czarownic i czarodziejów, a małżeństwa z mugolami spadły do ​​najniższego poziomu kiedykolwiek znanego, głównie z powodu obaw, że małżeństwa mieszane doprowadzą nieuchronnie do odkrycia, a w konsekwencji do poważnego naruszenia prawa czarodziejskiego. *</p>

<p>W takich warunkach niepewności, strachu i urazy doktryna czystej krwi zaczęła zdobywać zwolenników. Co do zasady, ci, którzy ją przyjęli, byli również tymi, którzy najintensywniej sprzeciwili się Międzynarodowemu Kodeksowi Tajności, opowiadając się wprost za wojną z mugolami. Coraz więcej czarodziejów głosiło, że małżeństwo z mugolem nie tyle co narazi na niebezpieczeństwo ewentualnego naruszenia nowego Kodeksu, ale że było to haniebne, nienaturalne i doprowadziło do "zanieczyszczenia" magicznej krwi. **</p>

<p>Ponieważ mugolskie / czarodziejskie małżeństwo było powszechne od stuleci, ci, którzy teraz opisywali czystą krew, raczej nie mieli większego odsetka czarodziejskich przodków niż ci, którzy tego nie robili. Nazywanie siebie czystą krwią było dokładniej deklaracją intencji politycznej lub społecznej ("Nie ożenię się z mugolem i uważam, że mugolskie / czarodziejskie małżeństwo jest naganne") niż oświadczenie o fakcie biologicznym.</p>
<img class="img-fluid prawy2" src="../img/pod_krew.jpg">
<p>Kilka dzieł wątpliwych uczonych, opublikowanych na początku XVIII wieku i częściowo opartych na pismach samego Salazara Slytherina, nawiązuje do rzekomych wskaźników statusu czystej krwi, oprócz drzewa genealogicznego. Najczęściej wymienianymi znakami były: początek zdolności magicznych przed ukończeniem trzeciego roku życia, wczesna (przed siódmym rokiem życia) sprawność na miotle, niechęć lub strach przed świniami i tymi, którzy je pielęgnują (świnia jest często uważana za szczególnie niemagiczne zwierzę i jest niezwykle trudna do zaczarowania), odporność na powszechne choroby wieku dziecięcego, wybitną atrakcyjność fizyczną i niechęć do mugoli, którą można zaobserwować nawet w dziecku czystej krwi, które rzekomo wykazuje oznaki strachu i obrzydzenia w ich obecności.</p>

<p>Kolejne badania opracowane przez Departament Tajemnic dowiodły, że te przypuszczalne cechy czystej krwi nie mają żadnej podstawy. Niemniej wielu z nich nadal cytuje je jako dowód swojego wyższego statusu w społeczności czarodziejów.</p>

<p>Na początku lat trzydziestych XX w. Anonimowo opublikowano "Skorowidz Czystości Krwi" w Wielkiej Brytanii, który wymieniał dwadzieścia osiem prawdziwie czystych rodzin, według nieznanego autorytetu, który napisał książkę ***, z "celem pomaganie takim rodzinom w utrzymaniu czystości ich linii krwi ". Tak zwane "Nienaruszalna Dwudziestka Ósemka" obejmowało rodziny:</p>
