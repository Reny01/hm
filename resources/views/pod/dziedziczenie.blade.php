<h1>Dziedziczenie Magii</h1>

<img class="img-fluid lewy" src="../img/pod_dziedziczenie.gif">
<p>W Świecie Czarodziejów magia jest swego rodzaju cechą dziedziczną, to znaczy że zwykle jest przekazywana z pokolenia na pokolenie. Jeśli nasi rodzice posiadadli talent magiczny jest duża szansa, że my również ten talent odziedziczymy. Niestety często jest tak, że magia potrafi płatać figle i ciężko jest jednoznacznie stwierdzić czy nasze dziecko, nasze potomstwo urodzi się z tym magicznym potencjałem, bo nawet wśród rodów czystej krwi rodzą się charłacy, nawet w rodzinach mugoli może pojawić się potomek, który będzie posiadał talent magiczny. To wszystko sprawia, że ciężko jest jednoznacznie określić dziedziczność tej cechy.</p>
<p>Zastanawiałem się swego czasu czy dziedziczenie magii można potraktować jak mugolską genetykę, Rowling na ten temat piszę niewiele, nie przedstawia jak to wygląda naprawdę, więc jedyne co nam pozostaje to domysły. Z mugolskiej genetyki zrezygnowałem z kilku powodów, jednak najważniejszy z nich to fakt, że mówimy tutaj o magii. O czymś co wykracza poza standardowe myślenie mugola, o czymś co przeczy wszelkim prawom fizyki i jest zjawiskiem, które bardzo często powoduje że coś, co wcześniej myśleliśmy, że jest niemożliwe bo tak mówiła logika, nagle okazuje się realne i prawdziwe. Dlatego według mnie nie da się dopasować typowych zasad dziedziczenia jakie może znamy pod magię, bo magia w gruncie rzeczy nie ma ścisłych reguł. W książkach o Harrym Potterze dowiadujemy się, że istnieją pewne ograniczenia, że są rzeczy których za pomocą magii nie zrobimy, ale w kolejnych tomach pojawiają się osoby lub sytuacje, w których te niemożliwe rzeczy stają się realne. To wszystko sprowadza się do tego, że nie jesteśmy w stanie w ścisły sposób określić jak dziedziczona jest magia, nie jesteśmy też w stanie przewidzieć czy nasze potomstwo urodzi się z magicznym potencjałem, to prawda że niektóre krzyżówki mają więcej szans na to, a niektóre mniej - ale jasnej zasady nie ma, bo genetyka nie uwzględnia czegoś takiego jak magia.</p>
