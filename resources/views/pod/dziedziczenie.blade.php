<h2>Mugol</h2>
<p>Mugol - to człowiek, który nie posiada żadnych zdolności magicznych, ani nie jest w stanie ich w jakikolwiek sposób rozwinąć. Określenie to jest stosowane głównie w Wielkiej Brytanii, w Stanach Zjednoczonych przyjęto stosowanie nazwy niemag, które jest skrótem od osoby niemagicznej. W niektórych przypadkach stosuje się również określenie ‘No-Maj’.</p>
</div>
<div class="col-4 page-hm">
  <h2>Mugolak</h2>
<p>Mugolak - jest to czarodziej, który urodził się w rodzinie mugoli. Najprawdopodobniej zdolności magiczne pochodzą od dalekiego przodka, a dopiero teraz się ujawniły. Zdarza się również tak, że charłak poślubił mugola i świadomość magicznego pochodzenia zanikła.</p>
</div>
<div class="col-4 page-hm">
  <h2>Charłak </h2>
<p>Charłak - jest osobą, która urodziła się w rodzinie czarodziejów, ale nie posiada żadnych zdolności magicznych. Najprawdopodobniej to od nich pochodzą mugolaki, których moc ujawnia się po kilku pokoleniach.</p>
</div>
</div>
<div class="row">
