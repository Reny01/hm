      <h2>Czarodziej czystej krwi </h2>
<p>Czarodziej czystej krwi - potocznie nazywa się tak czarodziejów, którzy przez kilkanaście pokoleń w swojej rodzinie nie mieli żadnych mugoli, mugolaków czy charłaków. Po jakimś czasie za czarodziejów czystej krwi uznano rody, które występują w Skorowidzu Czystości Krwi.</p>
</div>
<div class="col-6 page-hm">
    <h2>Czarodziej półkrwi</h2>
<p>Czarodziej półkrwi – czarodziej, który urodził się od jednego rodzica mugola, a drugiego czarodzieja bądź jeden rodzic jest mugolakiem lub charłakiem, a drugi przynajmniej półkrwi. Większość czarodziejów jest tego statusu krwi ponieważ bardzo ciężko jest aby przez wszystkie pokolenia w rodzinie pojawiali się wyłącznie czarodzieje czystej krwi.</p>
