<p>Mniejszość część tych rodzin publicznie ubolewała nad ich umieszczeniem na liście, oświadczając, że ich przodkowie z pewnością byli mugolami, z czego oni się nie wstydzili. Najbardziej oburzającą była liczna rodzina Weasleyów, która pomimo swoich powiązań z niemal każdą starą rodziną czarodziejów w Wielkiej Brytanii, była dumna ze swoich przodków w postaci wielu interesujących mugoli. Ich protesty przyniosły tym rodzinom opór zwolenników doktryny czystej krwi i epitet "zdrajca krwi". Tymczasem większa liczba rodzin protestowała, że ​​nie znalazły się na liście czystej krwi.</p>

<p>* W kolejnych dziesięcioleciach i stuleciach liczba mieszanych małżeństw zaczęła ponownie wzrastać aż do dzisiejszego poziomu, co nie doprowadziło do powszechnego odkrycia ukrytej magicznej społeczności. Profesor Mordicus Egg, autor książki The Philosophy of the Mundane: Dlaczego mugole wolą tego nie wiedzieć, wskazuje, że mugole zakochani na ogół nie zdradzają swoich mężów i żon, a mugole, którzy wypadają z miłości, są wyszydzani przez własną społeczność, gdy twierdzą, że ich partner jest czarownicą lub czarodziejem.</p>

