<h1> LUMOS</h1>
  <img class="img-fluid lewy2" src="../img/pod_lumos.gif">
<p>Zaklęcie Lumos należy do jednych z najbardziej powszechnych i podstawowych zaklęć, za jego pomocą na czubku twojej różdżki pojawia się światełko, które rozświetla ciemność. Zaklęcie to zostało wynalezione w 1772 roku przez Levinę Monkstanley, która pracowała w Departamencie Tajemnic. Pewnego dnia w swojej pracy, upuściła ona pióro w zaciemnionym kącie. W tamtych czasach, kiedy nie znano jeszcze zaklęcia rozświetlającego, korzystano z różnych rodzaju magicznych latarni, unoszących się w powietrzu świec oraz bardziej lub mniej skutecznych zaklęciach tworzących światło. Levina tamtego dnia zaskoczyła swoich współpracowników, bowiem w trakcie poszukiwań upuszczonego pióra, rozświetliła ona czubek swojej różdżki. Przez wiele lat kilku czarodziejów deklarowało się, że to oni są wynalazcami zaklęcia Lumos. Jeden z nich, niemiecki czarnoksiężnik – Garvin Luegner, mówił że zaklęcie to jest efektem ubocznym prac nad Proszkiem Natychmiastowej Ciemności. Później jednak okazało się, że kłamał w tej sprawię, a sam jego proszek służył jedynie do lekkiego przygaszania światła. </p>





