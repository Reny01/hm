<h1>Międzynarodowa Konfederacja Czarodziejów</h1>

<img class="img-fluid lewy" src="../img/pod_mkc.jpg">
<p>Międzynarodowa Konfederacja Czarodziejów jest organizacja czarodziejów, która ma na celu współpracę między społecznością czarodziejów na całym świecie, która jest reprezentowana w postaci rządu danych państw. Ich celem jest wzajemna pomoc i przestrzegania praw, które mają chronić społeczeństwo magów. W Wielkiej Brytanii członków Międzynarodowej Konfederacji Czarodziejów wybiera Ministerstwo Magii, najprawdopodobniej za pośrednictwem i zgodą Wizengamotu. Przywódcą Międzynarodowej Konfederacji Czarodziejów nosi tytuł Najwyższej Szychy.</p>

<p>Pierwszym przewodniczącym, a więc Najwyższą Szychą Międzynarodowej Konfederacji Czarodziejów był Pierre Bonaccord, a pierwsze spotkanie odbyło się we Francji. Nie pozwolono wtedy goblinom wziąć udziału. Wybór Bonaccorda nie spodobał się czarodziejom z Liechtensteinu, dlatego że Bonaccord dążył do zatrzymania polowań na trolle, czego Liechtenstein nie chciał. W konsekwencji Liechtenstein odłączył się od organizacjii.</p>

<p>W 1692 roku Międzynarodowa Konfederacja Czarodziejów uchwaliła dokument zwany Międzynarodowym Kodeksem Tajności. Zabraniał on czarodziejom używać magii w obecności mugoli, czemu gorączkowo sprzeciwiała się Carlotta Pinkstone. Po wielu tygodniach rozmów i dyskusjii czarodzieje odnaleźli sposób, by wszystkie magiczne stworzenia ukryć tak samo jak wszelkie magiczne budynki, miejsca, szkoły. Złamanie przepisów skutkowało na początku ostrzeżeniem, ale później nawet osadzeniem w więzieniu.</p>

<p>W XVIII wieku Dorka Twelvetrees ujawniła lokalizację MACUSA i Ilvermorny Bartołomiejowi Barebone'owi, potomkowi czyścicieli, oraz prawie ujawniła czarodziejski świat. MACUSA stanęło wtedy w obliczu upokarzającej krytyki ze strony Międzynarodowej Konfederacji Czarodziejów. Emily Rappaport była zobowiązana do złożenia zeznań przed MKC podczas publicznego śledztwa i przyznała, że nie może być pewna, że wyczyszczono pamięć wszystkim niemagom, narażonym na wyciek informacji o świecie czarodziejów.</p>

<p>Albus Dumbledore, dyrektor Szkoły Magii i Czarodziejstwa w Hogwarcie był wieloletnią Najwyższą Szychą. Został odwołany ze stanowiska po wypowiedzeniu powrotu Voldemorta, a znowu powołany na to stanowisko, kiedy okazało się że Voldemort faktycznie powstał. </p>

<p>W obrębie Międzynarodowej Konfederacji Czarodziejów funkcjonuje Biuro Edukacyjne, które odpowiedzialne jest za minitorowanie jedenastu czarodziejskich szkół na całym świecie. Szkoły te są zarejestrowane w Międzynarodowej Konfederacji Czarodziejów, natomiast rodzice którzy chcą dowiedzieć się czy w ich sąsiedztwie znajduje się jakaś magiczna placówka, mogą wysłać sowę z zapytaniem do Biura. </p>
