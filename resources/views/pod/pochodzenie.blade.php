<p>Wspomnieliśmy o rozwoju magii poprzez przekazywanie praktyk magicznych z pokolenia na pokolenie. Warto dodać, że sama forma magii i jej rozwój spowodowany jest wymieszaniem technik i zasad stosowanych przez różne kultury. Do takich właśnie kontaktów i wymianie myśli pomiędzy przedstawicielami różnych kultur dochodziło coraz częściej po III w p. n. e., kiedy to Aleksander Wielki podbił Syrię, Egipt, Babilon i Persję. Kultura między tymi cywilizacjami zaczęła się przenikać i integrować, podobnie też było z magią. Praktyki magiczne były wzajemnie testowane i porównywane, tym samym sama świadomość magii również wzrosła. Czarodzieje różnych kultur mogli porównywać swoje umiejętności, swoją wiedzę. I choć z pozoru wydaje się to być niezwykłą okazją, trzeba pamiętać, że doprowadziło to też do konfliktów i sporów. Ale o tym w innym dziale tego podręcznika. </p>

<p>Powiedzieliśmy, że słowo magia pochodzi od nazwy starożytnych, perskich kapłanów. Jednak samo powstanie magii jest bardzo trudne do określenia.  </p>

<p>Przyjmujemy, że magia miała narodzić się w Starożytnym Egipcie, który niemal od III tysiąclecie p.n.e. uważany był za ‘tarlisko magii’. Żadna starożytna cywilizacja nie była tak przepełniona zainteresowaniem zmarłymi i życiem pozagrobowym, czyli głównie skomplikowanymi rytuałami, które miały ściśle określony przebieg.  </p>

<p>Zjawiska paranormalne jak chociażby wystąpienie tak zwanych plag egipskich, strach przed zaćmieniem słońca. Zagadkowe, a niekiedy mistyczne znaczenie hieroglifów. Jak i również istnienie piramid, których powstanie nawet mugole nie mogą wytłumaczyć, traktując je jako twór kosmitów.  </p>

<p>Najbardziej wiarygodne źródła podają, że początków magii właśnie należy poszukiwać w Egipcie – pierwsze zaklęcia, pierwsi magowie. Wszystko to wywodzi się ze starożytnego Egiptu. </p>

