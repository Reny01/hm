<h1> POCHODZENIE MAGII </h1>
<p>Ciężko mówić o jednoznacznym miejscu i czasie pochodzenia magii. Jak wiadomo, żadna pojedyncza osoba, czy nawet kultura nie mogła stworzyć magii. Magiczne praktyki były przekazywane z pokolenia na pokolenie w wielu cywilizacjach, do tego sama magia mogła powstawać i rozwijać się w kilku miejscach jednocześnie, dlatego bardzo często powstanie samej magii przyrównuje się do powstania kultury. </p>

<p>Patrząc na to, trudno jest jednoznacznie określić pochodzenie magii, wielu historyków czy teoretyków magii może przyjmować inną jej genezę, dlatego czytając o jej początkach proszę zwrócić uwagę na niejednoznaczne źródła i wiarygodność przekazywanych informacji. Wspominam o jej pochodzeniu żeby zobrazować sposób jej rozwoju i samą naturę magii, natomiast na samo miejsce i dokładną datę proszę patrzeć z przymrużeniem oka.</p>

<p>Uważa się, że słowo ‘magia’ pochodzi z VI w. p. n. e. ze Starożytnej Persji, od nazwy tamtejszych kapłanów, którzy powszechnie nazywani byli magami znanymi głównie z gruntownej wiedzy i zdolności magicznych. Zajmowali się interpretacją snów, a także astrologią. Służyli radą władcom w istotnych dla państwa kwestiach, umieli korzystać ze swoich umiejętności na tyle, by móc spokojnie żyć i czerpać możliwości ze swojej pozycji.</p>

<div class="blok-cytat"><center>Samo słowo magia ma pochodzić od perskiego słowa oznaczającego siłę, władzę albo wielkość <br>co w wolnym tłumaczeniu może oznaczać aktywność zmierzającą do osiągnięcia celów.</center><Br>
</div>
