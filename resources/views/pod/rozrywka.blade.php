<p>Fatalne Jędzę są zespołem rockowym cieszącym się dużą popularnością w społeczności czarodziejów w latach 90. XX wieku. Jedną z fanek zespołu była Nimfadora Tonks, która miała nawet koszulkę z zespołem. W 1994 roku grupa wystąpiła na Balu Bożonarodzeniowym podczas trwania Turnieju Trójmagicznego w Szkole Magii i Czarodziejstwa w Hogwarcie. </p>
