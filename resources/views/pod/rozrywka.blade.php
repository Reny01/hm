<h1>CELESTYNA WARBECK</h1>
<img class="img-fluid lewy" src="../img/pod_wada.png">
<p>Znana na arenie międzynarodowej za śpiewający ewenement, Celestyna Warbeck (znana również jako „Śpiewająca Czarownica”) pochodzi z Walii, urodziła się 18 sierpnia 1917 roku. Jej ojciec, zwykły funkcjonariusz w Urzędzie Łączności z Mugolami, poznał jej mugolską matkę (upadłą aktorkę), kiedy ta została zaatakowana przez śmierciotulę, która udawała sceniczną kurtynę. </p>

<p>Nadzwyczajny głos Celestyny objawił się już w młodym wieku. Matka Celestyny była bardzo rozczarowana, że nie istnieje coś takiego jak czarodziejska szkoła sceniczna, niechętnie więc przystała na to aby córka udała się do Szkoły Magii i Czarodziejstwa w Hogwarcie. Pani Warbeck nie pozostawała jednak na tym i zalała Armando Dippeta, ówczesnego dyrektora Hogwartu, masą listów nakłaniajac go aby utworzył w szkole chórek, kółko teatralne i kurs tańca, tak aby można było uwydatnić talenty jej córki.  </p>

<p>Koncerty Celestyny, na których ta często występuje w akompaniamencie z szyszymorami, cieszą się należną sławą. Troje oddanych fanów na miotłach wzięło udział w powietrznej kolizji nad Liverpoolem, gdy starali się dostać na ostatni wieczór jej trasy koncertowej – „Kapryśna Afrodyta”; bilety na jej koncerty często pojawiają się na czarnym rynku w znacznie zawyżonych cenach (to jeden z powodów, przez które Molly Weasley jeszcze nigdy nie zobaczyła na żywo swojej ulubionej piosenkarki). </p>

<p>Celestyna użycza czasami swojego nazwiska i talentów dla dobrych celów, takich jak zbieranie środków na rzecz Kliniki Magicznych Chorób i Urazów Szpitala Świętego Munga poprzez nagranie hymnu Zjednoczonych z Puddlemere, „Odbijajcie tłuczki, chłopcy, migiem podajcie kafla”. Bardziej kontrowersyjnie Celestyna wykorzystała swój wokal w geście niezgody, gdy Ministerstwo Magii starało się nałożyć ograniczenia na sposób, w jaki magiczna społeczność może świętować Halloween. </p>

<p>Do najbardziej znanych piosenek Celestyny należą: „Wyczarowałeś ze mnie serce” i „Kociołek pełen gorącej miłości”. Jej fanami są zazwyczaj starsi ludzie, którzy kochają jej efekciarski styl i potężny głos. Album „Ukradłeś mój kociołek, ale nie możesz mieć mojego serca”, który ukazał się u schyłku XX wieku, był olbrzymim światowym hitem. </p>

<p>Prywatne życie Celestyny dostarczyło wiele materiału dla plotkarskich kolumn „Proroka Codziennego”. Wczesne małżeństwo z członkiem scenicznego baletu trwało zaledwie rok, później Celestyna poślubiła swojego menadżera, z którym ma syna, by porzucić go dziesięć lat później dla kompozytora, Irvinga Warble’a. </p>

