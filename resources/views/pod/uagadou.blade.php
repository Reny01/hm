<h1> Szkoła Magii Uagadou</h1>
<img class="img-fluid lewy" src="../img/pod_uag2.png">
<p>Szkoła Magii Uagadou mieści się w Afryce, nie jest znane jej dokładne położenie, jedynym adresem jaki jest podawany to ‘Góry Księżycowe’, podobno właśnie we wnętrzu tych gór, mieści się przepiękny budynek, otoczony mglą do tego stopnia, że wydaje się jakby unosił się w powietrzu. Mimo że w Afryce mieści się sporo pomniejszych magicznych placówek, to tylko Uagadou przetrwała próbę czasu i cieszy się znakomitą reputacją na świecie. </p>
<p>Ma co najmniej tysiąc lat i jest największą ze wszystkich istniejących szkół, przyjmuje bowiem czarodziejów z całego kontynentu. To właśnie w Afryce najbardziej rozwinęła się magia w czasach Starożytności, także teraz wielu uważa, że cała magia pochodzi właśnie z tego kontynentu. Absolwenci Uagadou są szczególnie uzdolnieni w takich dziedzinach jak Astronomia, Alchemia czy Transmutacja.</p>
<img class="img-fluid prawy2" src="../img/pod_uag.png">
<p> W afrykańskiej szkole magii dopiero niedawno wprowadzono różdżkę jako narzędzie dzięki którym można czarować, do tej pory zaklęcia rzucane przez magów były za pomocą wskazania na coś palcem lub gestem ręki. Jest to spore ułatwienie w przypadku oskarżenia o łamanie Międzynarodowej Ustawy o Tajności, zawsze mogą powiedzieć, że tylko machnęli ręką, nie chcieli nic zrobić. Nie tylko sposobem czarowania szokują afrykańscy czarodzieje, na niedawnym Sympozjum Animagów, uczniowie ze szkoły Uagadou zaprezentowali niebywały talent w dziedzinie transmutacji zsynchronizowanej. Na jednym z pokazów, uczniowie mający zaledwie czternaście lat z łatwością zamieniali się w takie zwierzęta jak słonie czy gepardy. Wielu z doświadczonych czarodziejów było oburzonych, a tym bardziej zagrożonych takimi zdolnościami. Wywołało to ogólne zamieszki, a nawet oﬁcjalne skargi do Międzynarodowej Konfederacji Czarodziejów. Taką skargę złożył Adrian Tutley, którego forma animaga to myszoskoczek.</p>
<img class="img-fluid lewy2" src="../img/pod_uag3.png">
<p> Jednym z bardziej znanych absolwentów Uagadou jest Babajide Akingbade, która zastąpiła Albusa Dumbledore’a na stanowisku Najwyższej Szychy Międzynarodowej Konfederacji Czarodziejów. O przyjęciu do Uagadou młodzi czarodzieje dowiadują się poprzez specjalne tokeny, które odnajdują zaraz po przebudzeniu w swoich dłoniach. Żetony dostarczają Posłańcy Snów, którzy wysyłani są przez dyrekcję. Pełni to podobną funkcję jak list z Hogwartu, bowiem na kamieniach zapisane sa najważniejsze informacje. Jest to jedyna szkoła, która wykorzystuje taką metodę do selekcji uczniów. </p>


