<h1> MAGIA NISKA</h1>
<img class="img-fluid lewy" src="../img/pod_mn.gif">
<p>Inaczej jest z magią niską, większość czarodziejów ma bardziej doraźne i praktyczne cele. Chcą osiągnąć bogactwo, sławę, sukces polityczny, zdrowie i piękno, wzbudzać miłość, wygrywać w zawodach sportowych czy po prostu rozwiązywać codzienne problemy. Nie wymaga to dużego przygotowania czy lat praktyki. Stawianie problemów i ich rozwiązywanie odbywa się na porządku dziennym, tak więc typowe przyziemne sprawy, zwyczajna nauka młodych adeptów, to wszystko jest połączone z magią niską. </p>



