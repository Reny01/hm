<h1> PODZIAŁY MAGII</h1>
<p>Może się wydawać, że same podziały magii to bardzo prosty temat, dość oczywisty, w końcu dzielimy magię na dane rodzaje i każdy z nich opisujemy. W rzeczywistości jest jednak inaczej, każdy kto choć na chwilę skupił się na tym zagadnieniu wysunął własne wnioski. Niektórzy dzielą magię (jeśli chodzi o podstawowy podział) na magię białą i czarną, niektórzy wyróżniają w tym podziale magię szarą, która ma być zwyczajnym jej użytkowaniem. Bardzo często jest też tak, że czarodzieje uznają tylko i wyłącznie magię czarną, a o magii białej całkowicie zapominają lub jest przez nich pominięta. Jeśli chodzi o inne podziały często stosowane, mówi się o magii wysokiej i magii niskiej, czyli podziale uwzględniającym predyspozycje czarodzieja, jego moc i sam charakter używanej magii. Oprócz tego, magię można scharakteryzować przez szereg innych definicji, innych czynników, które uwzględnimy chcąc wyróżnić jakieś kategorie. Ja skupie się tylko na niektórych z nich.</p>
