<h1> MAGIA WYSOKA</h1>
<img class="img-fluid prawy" src="../img/pod_mw.gif">
<p>Wymaga ona o wiele więcej pracy i wysiłku, jej główne założenia opierają się o osiągnięcia wiedzy niedostępnej poprzez zwyczajne doświadczenia, czy proste nauki. W Starożytności, kiedy to uprawiający ją magowie prosili coś bogów czy same duchy, czynili to dla najwyższych celów, mieli nadzieję otrzymać dar jasnowidzenia, czyli prorocze wizje, stać się uzdrowicielami zdolnymi uleczyć każdą chorobę. Chcieli uzyskać wgląd w istotę rzeczy oraz samopoznanie siebie, swoich umiejętności, jak i idealną, harmonijną koegzystencje z otaczającym światem. Niektórzy nawet pragnęli stać się podobnym do bogów. </p>

<p>Czarodzieje, którzy używali magii wysokiej, byli całkowicie pochłonięci swoimi celami, musieli się poświęcić by osiągnąć zamierzone cele i oczekiwania. Skupiali się całkowicie na nauce i doświadczeniu, na poznaniu natury magii. Niekiedy całe ich życie toczyło się wyłącznie wokół jednej dziedziny, którą wybrali by to właśnie w niej osiągnąć swego rodzaju doskonałość.</p>

<p>Magia wysoka nie jest dla każdego, tylko utalentowani czarodzieje mogą poszczycić się jej używaniem. W końcu nie wszyscy są zdolni do takich poświęceń. </p>

