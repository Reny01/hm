<div class="col-12 page-hm"><h2 class="purple">Herpon Podły, inaczej Głupiec</h2>
<img class="img-fluid lewy2" src="../img/pod_herpon.jpg">
<p>Starogrecki czarodziej czystej krwi. Jest uznawany za jednego z pierwszych czarnoksiężników.Jego osiągnięcia zostały wykorzystane przez następnych magów uprawiających czarną magię. To waśnie Herpon Podły stworzył pierwszego Bazyliszka - podłożył kurze jajo ropusze. Wymyślił też wiele przekleństw, klątw i uroków, uważa się również że jako pierwszy stworzył horkruksa. Był jednym z pierwszych wężoustych. </p>
<p>Wiele elementów wskazuje na to, że mógł być on przodkiem Salazara Slytherina, ze Slytherinem kojarzy się umiejętność rozmowy z wężami, Bazyliszek był potworem, który zamieszkiwał Komnatę Tajemnic - pomieszczenie stworzone w Hogwarcie przez Slytherina. Dodatkowo w herbie tego domu znajduję się wąż. </p>
<p>Herpon mógł być swego rodzaju wzorem dla Salazara, tak jak Salazar dla Lorda Voldemorta.<br><br></div>
<div class="col-12 page-hm"><h2 class="purple">Kirke</h2>
  <img class="img-fluid prawy" src="../img/pod_kirke.jpg">
<p>To jedna z największych greckich czarodziejek. Za pomocą czarodziejskiej różdżki, eliksirów, ziół i zaklęć przemieniała ludzi w zwierzęta, przemieszczała lasy i z dnia czyniła noc. Kirke, córka boga słońca Heliosa i nimfy morskiej Perseidy, mieszkała na wyspie Aja (Aeaea) u wybrzeża Italii, gdzie spędzała dni, tkając olśniewające tkaniny i śpiewając czarownym głosem. Pewnego dnia na jej wyspę przybył bożek morski Glaukos, prosząc ją o napój miłosny, który miał mu pomóc mu zdobyć miłość jego serca, nimfę Skyllę. Kirke zakochała się w Glaukosie i poprosiła go aby z nią został. Kiedy odmówił, do wody, w której kapała się jej rywalka Kirke wrzuciła trujące ziele, przemieniając Scyllę w potworne monstrum. </p>

<p>Najbardziej znanym gościem Kirke jest Odyseusz i jego załoga, wracający z wojny trojańskiej. Gdy żeglarze dotarli do domu Kirke, ta podała im potrawy nasączone silnym eliksirem, który odebrał gościom pamięć i chęć powrotu do domu. Następnie Kirke za dotknięciem zarodziejskiej różdżki zamieniła ich wszystkich w świnie i zaprowadziła do chlewa. Odyseusz wyruszył aby odszukać swoich ludzi. Po drodze do domu Kirke spotkał Hermesa, który dał mu ziele zwane „moly”, neutralizujące zgubne działania uroków i eliksirów. Nie mogąc uciec się do żadnych sztuczek, Kirke zaprzyjaźniła się z Odyseuszem i przywróciła ludzką postać jego załodze. Od tamtej pory była jego doradcą, ostrzegała go przed niebezpieczeństwami, jakie może napotkać, i uczyła porozumiewać się z duchami, które staną na jego drodze w zaświaty.
Kirke miała portret w Hogwarcie, który strzegł tajnego przejścia.</p>
