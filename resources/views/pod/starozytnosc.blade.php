<h1> WPROWADZENIE DO STAROŻYTNOŚCI</h1>
<img class="img-fluid lewy" src="../img/pod_staro.jpg">
<p>W poprzednich rozdziałach mówiłem o pochodzeniu magii i miejscu jej narodzin. Powiedziane zostało, że nie można jednoznacznie stwierdzić kiedy i gdzie dokładnie powstała magia, można jednak przypuszczać, że jej największy rozwój i swój początek bierze z okresu Starożytności. Bowiem właśnie w tym okresie nastąpił bardzo szybki rozwój kultury, różnego rodzaju wierzeń i zabobonów, i tym samym można przypuszczać, że magia właśnie wtedy najsilniej się rozwinęła. </p>

<p>Niektórzy twierdzą, o czym już mówiłem, że magia mogła narodzić się w Starożytnym Egipcie, prawda jednak jest taka, że tego nie wiemy i nie możemy w żaden sposób udowodnić. Tak samo jak kultura, tak i magia rozwijała się w kilku miejscach na raz jednocześnie. I to jest główną trudnością w stwierdzeniu jej początku. Czarodzieje odkrywali ją najprawdopodobniej w różnych zakątkach globu, ale w tym samym czasie. </p>

<p>W miarę rozwoju kulturowego tamtejszego okresu, rozwijała się również magia, ale to co jest jeszcze ważne, to że wtedy również powstawała relacja między czarodziejami i mugolami. Ich pierwszy zalążek współpracy, oszukiwania siebie nawzajem i wykorzystywania swoich możliwości i pozycji. Starożytność to epoka, w której magowie nie ukrywali się, świadomie ujawniali swoje możliwości i moce zyskując na tym łaskę, władzę i ogólny szacunek. To początek historii kapłanów, szamanów, druidów i uzdrowicieli. To różne wcielenia magów, jakie się tu rozwinęły oraz ich historia, która znacznie wpływa na historię czarodziejstwa. To również bardzo ważny czas, w którym sama magia jest szanowana, jest podziwiana i dostępna pod każdym względem poza poznaniem jej sekretów. Mugole mogli jednak widzieć skutki magii, doświadczać jej, a niekiedy byli pod jej wpływem, będąc świadomymi jej istnienia oraz mocy. </p>

<p>Tak jak już zdążyłem wspomnieć – nie można jednoznacznie stwierdzić początków rozwoju magii, mogła się ona rozwijać w jednym czasie ale w kilku miejscach. Dlatego też lekcje związane z tą epoką poświęcone będą przede wszystkim różnym kulturom i ludom żyjącym w antyku. Czasami duża odmienność pod względem zajmowanego terytorium, kultury i ustroju powodowała zupełnie inny rozwój magii. Czasem też, mimo tak dużej ilości różnić, magia przejawiała się w bardzo podobny sposób i była traktowana tak samo. </p>
