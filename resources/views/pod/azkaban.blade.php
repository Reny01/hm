<h1> AZKABAN</h1>

<div class="blok-cytat">Przyczyna, dla której ojciec chrzestny Harry’ego uprzednio zniknął z jego życia, była prosta — Syriusz przez wiele lat przebywał w Azkabanie, przerażającym więzieniu dla czarodziejów, strzeżonym przez istoty zwane dementorami, ślepe demony, wysysające z ludzi dusze.
<p> Fragment książki Harry Potter i Czara Ognia.</p></div><br>
<img class="img-fluid lewy" src="../img/pod_azkaban2.jpg">
<p>Pierwotnie Azkaban był fortecą należąca do pewnego maga, a jej początek sięga wieku XV. Jak wiadomo, forteca ta mieściła się na wyspie niezaznaczonej na żadnej z mugolskich ani czarodziejskich map. Niektórzy sądzą, że sama wyspa mogła być powiększona przez zaklęcia. Początkowo w swej historii był to dom dla Ekrizdisa, czarodzieja, który Azkaban traktował jako swego rodzaju laboratorium, gdzie mógł praktykować Czarną Magię pod każdym kątem. Często zwabiał on żeglarzy i na nich stosował zakazane techniki, czary. Dopiero po jego śmierci, zniknęły wszelkie zaklęcia, dzięki którym forteca ta była niedostępna i niewidoczna dla innych. </p>

<p>Warto wiedzieć, że Azkaban był siedliskiem dementorów jak i innych, przerażających ‘rzeczy’, dlatego przez większość czasu twierdza i wyspa były omijane szerokim łukiem. Niektórzy chcieli ją jak najszybciej zniszczyć, inni natomiast nie zgadzali się z tym i próbowali tego uniknąć. Dlaczego? Otóż dementorzy byli i są bardzo niebezpiecznymi stworzeniami, które żerują na szczęściu innych. Bano się, że jeśli zniszczono by fortecę, naturalny dom dementorów, wtedy one ruszą na ląd i zaczną atakować ludzi lub będą żerować na czarodziejach. </p>

<p>Sytuacja uległa jednak zmianie po roku 1692, kiedy to wprowadzono Kodeks Tajności Czarodziejów. Czarodzieje obawiali się, że tak duża liczba więzień rozsianych po całym kraju może uniemożliwić im wprowadzenie w życie kodeksu, tj. skuteczne ukryć się przed oczami mugoli. Dlatego pracowano nad wybudowaniem lub stworzeniem jednego więzienia, które byłoby oddalone od mugoli na tyle, by uniknąć zauważenia czy odkrycia. I tak, gdy ministrem magii został Damocles Rowle wybrał właśnie Azkaban na miejsce, do którego zaczęto przewozić więźniów z całego kraju. Sytuacja wydawała się opanowana, żaden z więźniów nie uciekł, o Azkabanie nie wiedzieli też mugole, a dementorzy okazali się tanią siłą roboczą. Jednak gdy ministrem magii został Eldritch Diggory, postanowił on odwiedzić Azkaban i sprawdzić jak forteca radzi sobie, jako więzienie. To co tam zobaczył przerosło jego ‘oczekiwania’. Więźniowie nie uciekali bo prawie w większości zamieniali się w szaleńców niezdolnych do racjonalnego myślenia, a obok fortecy stworzono nawet specjalny cmentarz dla nich, ponieważ liczba zgonów samych skazańców przerosła wszelkie wyliczenia. </p>
<img class="img-fluid prawy" src="../img/pod_azkaban.jpg">
<p>Dementorzy oczywiście byli zadowoleni z obrotu sytuacji i posłusznie zgodzili się pełnić role strażników tylko dlatego, że ministerstwo samo przysyłało im pokarm, w formie więźniów. Oczywiście Diggory nie był tym zachwycony, stworzył on nawet specjalny komitet, który miał za zadanie znaleźć alternatywne rozwiązanie dla Azkabanu. Zanim jednak zdołał cokolwiek zdziałać, zmarł na smoczy syfilis. </p>

<p>Przez ponad 3 wieki Azkaban pozostawał więzieniem, żaden z ministrów przez ten czas nawet nie próbował tego zmienić, utrzymując fortecę w takiej formie, w jakiej została ukształtowana przez ministra Rowle. Dopiero po roku 1998, kiedy to nowym ministrem magii został Kingsley Shacklebolt, dementorzy zostali usunięci z Azkabanu.</p>

