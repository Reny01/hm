<p>BAAL SHEM (Pan Imienia) - w tradycji żydowskiej ten, kto poznał właściwe imię Boga, posiadał wielka tajemnice: mógł wypowiadać je jako zaklęcie i niczym sam Stwórca, ożywić martwa materie.</p></div>
