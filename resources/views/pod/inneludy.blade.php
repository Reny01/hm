<h1> INNE LUDY STAROŻYTNE</h1>

<p>W pierwszych rozdziałach tego podręcznika oraz na zajęciach z mojego przedmiotu dowiadujecie się, że magia mogła rozwijać się w kilku miejscach w tym samym czasie, choć znajdujemy wskazówki, które mowią że mogła bardziej rozwinąć się wśród konkretnego społeczeństwa. Zawsze jednak bezpieczną odpowiedzią dotyczącą jej powstania jest stwierdzenie, że nie można powiedzieć w którym miejscu oraz kiedy sama magia powstała. I w gruncie rzeczy taka jest prawda - nie wiemy tego, nie wiemy kiedy magia powstała i gdzie. </p>
<p> Zakładamy jednak że magia rozwijała się w różnych zakątkach świata, wśród różnych grup społecznych, a każda z nich wprowadziła coś innego od siebie, rozwinęła inne dziedziny, niektórzy byli bardziej wyspecjalizowani w astronomii i wróżbiarstwie, a inni w eliksirach. To również jest poprawne stwierdzenie. Jednak jeśli mamy być szczegółowi... tu już wchodzimy na dość niepewny grunt, nie wiemy jak magia się rozwijała w danym społeczeństwie, nie wiemy co się wtedy działo, ogólnie mało wiemy na ten temat. A wszystko to co jest przekazywane w Świecie Magii na temat różnych ludów starożytnych to nic innego jak wiadomości niekanoniczne, a więc takie które mogły zostać pozyskane z podręczników z historii w mugolskiej szkoly lub co gorsza... z innych realiów czy światów. </p>

<p> Nie da się jednak ukryć, że takie wiadomości się pojawiają dlatego aby uznać to za pewną ciekawostkę, pewne rozwinięcie tematów, poniżej znajdziecie wiadomości niekanoniczne na temat ludów starożytnych. Proszę mieć jednak na uwadze, że nie są to informacje potwierdzone przez Rowling. </p>
