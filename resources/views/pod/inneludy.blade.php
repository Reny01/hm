  <h3 class="purple">MEzOPOTAMIA</h3>

<p>W Mezopotamii rozegrała się jedna z wielu bitew dobra ze złem – Ea z Apsu. Ea potężnym zaklęciem unicestwił swojego nieprzyjaciela. Właśnie tam potężny Murduk zniszczył i stworzył swoją szatę na nowo zaklęciami, ponieważ chciał udowodnić swoją wartość. W Mezopotamii często występowało opętanie, dlatego bardzo rozwinęła się gałąź magii egzorcystycznej. Opętanie objawiało się najczęściej cielesną chorobą, którą mógł wyleczyć jedynie szanowany mag – egzorcysta. </p>

<p>Wróżbiarstwo było głównie związane z Astrologią. Innym sposobem przepowiadania przyszłości było wróżenie z organów wewnętrznych. Hepatoskopia to wróżenie z wyglądu wątroby. Jednak dlaczego z wątroby, a nie z inne organu? Ponieważ wątroba jest organem wytrzymalszym i bardziej intrygującym niż inne. Wątroba była zwierzęca, zwykle owcy. Specjalistyczne teksty zostały zapisane pismem klinowym.
</p>
