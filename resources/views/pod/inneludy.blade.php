  <h3 class="purple">PERSOWIE</h3>
<img class="img-fluid lewy" src="../img/pod_zara.jpg">
<p>Twierdzi się, że wysoka magia narodziła się w Persji, a magami byli kapłani Zaratustry, choć nie ma pewnych danych. Bywało także, że tych kapłanów wiązano z przedstawionymi przez św. Mateusza magami, którzy kierując się jakąś tajemniczą gwiazdą, mieli przybyć ze Wschodu do małego Jezusa. Do dzisiaj ciągnie się debata, kim mogli być ci magowie. Według współczesnej wiedzy o Ewangelii jest to midrasz, czyli pewna opowieść, obrazująca to, co św. Łukasz wyraził w sposób bardziej abstrakcyjny słowami Symeona. Magowie ci reprezentują wiec rozmaitych mędrców, którzy przybyli złożyć pokłon Chrystusowi. Natomiast mag w starożytnej Persji zajmował się równocześnie magia i astrologia. Kapłani ci zajmowali się objaśnianiem słów i prorokowaniem, zaś w czasie ceremonii ofiarniczych śpiewali pieśni o narodzinach bogów.</p>

<p>Wiedza oraz umiejętności kleru perskiego określane były mianem 'magii' (mageia) Z Persja kojarzy się na pewno Zoroaster, inaczej Zaratustra, który jako jeden z pierwszy odkrył historie Równowagi. Tej historii jednak nie będziemy tu przytaczać, gdyż nie może ona zostać spisana w całości. Jest znana tylko bardzo nielicznym magom.</p>
