<div class="col-6 page-hm"><h3 class="purple">ABORYGENI</h3>
<p>Ich magia opierała się na wierze w istnienie jednorodnej, potężnej siły wypełniającej cały wszechświat. Energię tą zwali 'mana'. Tworzyli różne magiczne przedmioty, które gromadziły tą energię. W tej kulturze istniało tabu, którego przestrzeganie zabezpieczało przed utratą many. Pojęcie tabu zostało przejęte przez mugoli i często dziś jest błędnie stosowane. Do najważniejszych tabu u aborygenów należały zakazy: kazirodztwa, zabójstwa, zjadania nieczystych potraw, dotykania nieczystych ludzi i przedmiotów.</p>
</div>
<div class="col-6 page-hm"><h3 class="purple">ANGAKOWIE</h3>
<p>W mitologii eskimoskiej czarownicy posiadający wiedzę tajemna, dzięki której potrafią przebłagać groźne niezbadane moce, a nawet im rozkazywać. Odbywali rytualne podróże na dno morskie i planety w celu zaskarbienia swojej społeczności łask i dobrodziejstw ducha przyrody.</p></div>
</div>

<div class="row">
<div class="col-4 page-hm"><h3 class="purple">AMERYKA POŁUDNIOWA</h3>
<p>Curaca - czarownik puszczańskich plemion w Ameryce Południowej. Gromadzi tradycyjną wiedzę Indian, zajmuje się wieszczeniem, kontroluje tajemne siły przyrody, unicestwia wrogich swojemu ludowi czarowników.</p></div>
<div class="col-4 page-hm"><h3 class="purple">AZTEKOWIE</h3>
<p>Ten lud z Meksyku wróżył z wnętrzności pelikana [hepatoskopia].</p></div>
<div class="col-4 page-hm"><h3 class="purple">CYGANIE</h3>
<p>Czowala - w wierzeniach Cyganów węgierskich szamanka - opiekunka kultu, wróżbitka i znachorka. Wielkim przełomem w dziejach czarodziejstwa było powstanie pierwszej szkoły, w której nauczano magii.</p></div>
</div>

<div class="row">
<div class="col-12 page-hm"><h3 class="purple">CELTOWIE</h3>
<p>Magia Celtycka to prastara praktyka, zakorzeniona w panteonie bóstw celtyckich, naturze i żywiołach. Tradycja celtycka przetrwała do dziś we współczesnych formach czarostwa i szamanizmu, a niektóre jej elementy takie jak drzewny horoskop, runy anglosaskie czy niektóre symbole weszły na dobre do kanonu magii europejskiej. Prastara magia druidów uczy, jak wcielać magiczne symbole i narzędzia rodem z celtyckiej mitologii do codziennego życia i korzystać z pradawnej mądrości, bez względu na czasy, w jakich przyszło nam żyć.</p>
<img class="img-fluid lewy" src="../img/pod_druid.jpg">
<p>Horoskop drzewny pochodzi od druidów celtyckich i ma już ponad dwa tysiące lat. Druidowie wierzyli, że opiekunem każdego człowieka jest drzewo, wpływa ono na jego cechy charakteru i kształtuje losy. Wcześni Celtowie nie budowali świątyń ku czci swoich bóstw; uważali natomiast pewne gaje (nemeton) za święte i godne tego, aby być miejscami kultu. Niektóre drzewa same były uważane za święte. Celtowie zaczęli budować świątynie dopiero w okresie wpływów rzymskich, a zwyczaj ten przeszedł później na plemiona germańskie, przez które zostali później wyparci.</p>

<p>Druid - starożytny kapłan celtycki, który przewodził obrzędom i ceremoniom religijnym. Specjalizowali się w uprawianiu magii, ofiar i wróżbiarstwa. Dużą moc dawało druidom prawo geasa – nakazy i zakazy indywidualne, nakładane na poszczególne osoby, wiążące szczególną mocą magiczną. Złamanie takiego zakazu groziło zemstą sił nadprzyrodzonych. Kapłanów uważano za potężnych czarowników, ich moc wykorzystywano nawet w wojnach. Wierzono, iż mogli rozkazywać żywiołom, zmieniać dzień w noc, lato w zimę, ich moc miała oddziaływać również na wolę bogów, a oni mogli ingerować w boskie sprawy. Druidzi mieli rozległą wiedzę na temat leczniczych właściwości roślin, które musiały być zbierane w specjalny sposób lub o odpowiedniej porze. Podczas zbierania jemioły odcinano jej pędy złotym sierpem, a ponieważ nie mogła przy tym dotknąć ziemi, łapano ją w płótno rozpostarte przez kapłanów pod drzewem. Zbierano ją tylko w 6 dniu księżycowego miesiąca. Druidzi wierzyli, że jemioła dodana do napoju przywraca zwierzętom płodność i jest remedium na wszelkie zatrucia. Druidzi wierzyli także w moc skamieniałych jeży morskich, o których sądzili, że są zrobione z wężowej śliny, i które nazywali jajami węża (wspominał o tym Pliniusz). Można je było zbierać tylko w określonej fazie księżyca, a miały przynosić szczęście w procesach sądowych.</p>

<p>Morhołt — olbrzym z Irlandii. Był bratem króla Irlandii. Zranił on celtyckiego bohatera Tristana za to, że ten chciał go zabić posługując się zatrutym nożem. Miał 50 metrów wysokości.</p></div>
</div>

<div class="row">
<div class="col-7 page-hm"><h3 class="purple">ETRUSKOWIE</h3>
<p>Do żadnego przedsięwzięcia publicznego czy prywatnego nie przystępowano bez zasięgnięcia porady u wróżbiarza. Orzekał on o przychylności bogów. Wróżono z wnętrzności zwierząt, piorunów i lotu ptaków. Wiedza z poszczególnych dziedzin wróżbiarstwa była zawarto kolejno w księgach: libri haruspicini, libri fulgurales i libri rituales. Haruspikowie, czyli wróżbiarze cieszyli się poważaniem w całym Rzymie (Etruskowie to przodkowie Rzymian). Etruskowie przyswoili hepatoskopię od Babilończyków, lecz dopiero tutaj powstawały pierwsze szkoły świątynne uczące tego sposobu wróżenia. We włoskim muzeum mugolskim (Piacenza)możemy oglądać naturalnej wielkości model wątroby wykonany z brązu znaleziony w etruskiej szkole.</p>
</div>
<div class="col-5 page-hm"><h3 class="purple">HETYCI</h3>
<p>Celem obrzędów magicznych u Hetytów było poznawanie rzeczywistości i pozyskanie możliwości jej modyfikowania. Magię stosowano np. w przypadku ciężkiej choroby. Ważną rolę w tego rodzaju magii odgrywała tzw. starucha, czyli doświadczona kobieta. Ważną rolę odgrywały słowa. Hetyci znali zarówno magię białą, jak i czarną, szkodliwą. Znaczną część tekstów magicznych stanowią notatki wróżbiarzy.</p></div>
</div>


