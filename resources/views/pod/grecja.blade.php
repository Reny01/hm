<h1> STAROŻYTNA GRECJA</h1>
<img class="img-fluid lewy" src="../img/pod_grecja.jpg">
<p>Greccy magowie specjalizowali się w tworzeniu magicznych przedmiotów, wynaleźli również wiele zaklęć i klątw, dodatkowo to właśnie tam, hepatoskopia była bardzo popularną metodą wróżenia, a polegała na odczytywaniu przyszłości z wątroby zwierząt. Platon ‘nadał osobowość’ żywiołom, tj. odkrył ich potencjał oraz wzajemne relacje, zajmował się również astrologią oraz jej filozoficznym aspektem czyli jej odzwierciedleniem w życiu ziemskim. Właśnie w Grecji odkryto pierwsze właściwości planet i tworzono pierwsze horoskopy. </p>


<p>Tetrablos, czyli cztery księgi Klaudiusza Ptolemeusza z Aleksandrii były uważane za biblię astrologów. Ptolemeusz żył ok. 85 – 115 n. e. Wszelkie dowody wskazują na to, że to właśnie w Grecji powstała różdżka, słynęli oni również z licznych wyroczni, a jedna z najpopularniejszych znajdowała się w Delfach. Patronką praktyk magicznych była tajemnicza Hekate – bogini zmarłych i upiorów, mistrzyni czarów oraz opiekunka czarownic. Wierzono, że noca odbywa ona powietrzne jazdy z orszakiem demonów, poprzedzona gromada czarnych psów. Mówi się, że ma wężowate włosy, jest wysoka oraz, że trzyma w lewej ręce pochodnię, a w prawej miecz. To właśnie w Grecji, w II w. p.n.e. został opisany pierwszy zabieg magii miłosnej. </p>

<p>Tak jak w każdej kulturze, tak i w Grecji można wyróżnić różne rodzaje czarodziejów, np. goeci, których nazywano tak od wycia i dzikich krzyków towarzyszącym im przy wypowiadaniu swoich zaklęć. Wierzono, że to właśnie oni rzucają wszystkie złe uroki, przez co nie byli zbyt popularni. Również oni, za pieniądze dostarczali trucizny i talizmany pozostałym mieszkańcom. Greccy czarodzieje znali się również na astrologii, która mieli wynaleźć Chaldejczycy. Ich nazwa oznaczała w Grecji astrologa, czyli czarodzieja który potrafił odczytywać z gwiazd przyszłe losy świata, ludzi i wszystkich narodów. Najbardziej znanym Chaldejczykiem był Berosus, który założył szkołę astrologii i wróżbiarstwa na wyspie Kos.</p>


