<h1>Międzynarodowe Siły Ochrony</h1>

<img class="img-fluid lewy" src="../img/pod_mio.jpg">
<p>Międzynarodowe Siły Ochrony to specjalny zespół reagowania, powoływany przez Międzynarodową Konfederację Czarodziejów w sytuacjach, w których ten międzynarodowy czarodziejski organ zarządzający ma trudności z egzekwowaniem Międzynarodowego Kodeksu Tajności Czarodziejów wobec któregoś z krajów członkowskich, czyli inaczej kiedy dochodzi do sytuacji, w którym w danym kraju zagrożone jest społeczeństwo czarodziejów, kiedy możliwe jest ujawnienie czarodziejów i magii przed mugolami, a jest to taka sytuacja, w której urzędnicy danego rządu nie mogą sobie poradzić, w takich wypadkach Konfederacja gromadzi grupę zadaniową, która wysyłana jest na miejsce wystąpienia problemu, gdzie stacjonuje tam aż do momentu kiedy zagrożenie przestanie istnieć. </p>
<p>Najważniejszymi zadaniami Międzynarodowych Sił Ochrony jest egzekwowanie postanowień Międzynarodowego Kodeksu Tajności Czarodziejów.</p>

<p>Znanym przypadkiem kiedy grupa zadaniowa z Sił Ochrony musiała interweniować, jest przypadek w górach Tybetu kiedy to liczni mugoli zauważali istnienie Yeti, wtedy grupa została wysłana w góry i stacjonowała tam do momentu aż sytuacja się nie unormowała. </p>
