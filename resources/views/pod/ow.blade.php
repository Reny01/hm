<p>Organizacja została rozwiązana, by ponownie się zebrać w 1995 roku, po ogłoszeniu przez Harry’ego Pottera powrotu Lorda Voldemorta. Ministerstwo nie chciało przyznać, że Czarny Pan powrócił, więc Zakon sam próbował chronić Harry’ego i przepowiednię dotyczącą jego i Voldemorta w Departamencie Tajemnic. Po bitwie między Zakonem, a Śmierciożercami, Ministerstwo musiało potwierdzić informację o powrocie Czarnego Pana.</p>

<p>W następnym roku Zakon stracił lidera - Albusa Dumbledore'a, którego obowiązki przejął Alastor Moody, a Ministerstwo zostało podbite przez Voldemorta. Zakon kontynuował swoją działalność podziemną, starając się chronić mugoli i informować o prawdziwej sytuacji w czasie Drugiej Wojny Czarodziejów. Odpowiedzieli na wezwanie do broni przez Gwardię Dumbledore'a w dniu 2 maja 1998 roku, by walczyć z wojskami Voldemorta i bronić Hogwartu. Bitwa o Hogwart rozpoczęła się o północy w dniu 2 maja i pochłonęła wiele istnień, również wśród członków organizacji, gdzie śmierć ponieśli: Remus i Nimfadora Lupin, Severus Snape i Fred Weasley. Bitwa zakończyła się definitywnym zwycięstwem dobra nad złem i ostatecznym pokonaniem Voldemorta przez Harry’ego Pottera. </p>
