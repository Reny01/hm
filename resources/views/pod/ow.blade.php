<h1>Gwardia Dumbledore'a</h1>
<div class="col-12">
  <div class="row">
  <div class="col-1"></div>
  <div class="col-10">
<img class="img-fluid ramka" src="../img/pod_gwardia.jpg">
</div>
<div class="col-1"></div>
</div>
</div>
<br>
<p>Gwardia Dumbledore'a to tajna organizacja, której pomysłodawczynią oraz założycielką była uczennica V roku – Hermiona Granger. Natomiast nauczanie uczniów objął uczeń tej samej klasy – Harry Potter. Zrzeszała czarodziejów i czarownice, którzy uwierzyli w powrót Lorda Voldemorta i pragnęli poznać zaklęcia i uroki, aby się przed nim obronić, ponieważ na lekcji obrony przed czarną magią, którą prowadziła niedoświadczona Dolores Umbridge uczyli się tylko teorii zawartej w okrojonym podręczniku. </p>

<p>Wbrew zakazowi tymczasowej dyrektorki Hogwartu, która zakazała tworzenia jakichkolwiek organizacji bez jej wiedzy, Harry nie zamierzał rozwiązać organizacji. Pomysł na miejsce spotkań podsunął mu Zgredek – Pokój Życzeń, który znajdował się na siódmym piętrze Szkoły Magii i Czarodziejstwa Hogwart, naprzeciw gobelinu przedstawiającego Barnabasza Bzikika i trolle. Aby się tam dostać, należało przejść wzdłuż ściany trzy razy, intensywnie myśląc nad tym, czym pokój się ma stać lub do czego ma posłużyć. Dla GD zamieniał się w ogromną salę z poduszkami, książkami pomocnymi przy nauce oraz fałszoskopami.</p>

<p>Spotkania odbywały się nieregularnie, ponieważ w tygodniu trzeba było znaleźć miejsce na treningi quidditcha i inne zajęcia dodatkowe. Pierwsze spotkanie gwardii odbyło się 9 października. Fałszywe galeony, na które Hermiona rzuciła zaklęcie Proteusza, zamiast numeru seryjnego goblina, który wykonał odlew, wskazywały datę i godzinę następnego spotkania. Kiedy data ulegała zmianie, rozmiar galeona zwiększał się tak znacznie, że członkowie Gwardii czuli to nosząc w swych kieszeniach.</p>

<p>W trakcie drugiej bitwy o Hogwart, która rozgrywała się podczas drugiej Wojny Czarodziejów, większość członków Gwardii Dumbledore`a stanęła do walki o szkołę i Harry'ego. To Gwardia Dumbledore'a zawiadomiła Zakon Feniksa o nadchodzącym starciu.</p>
