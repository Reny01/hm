<h1>Aurorzy</h1>

<img class="img-fluid prawy" src="../img/pod_auror.png">
<p>Auror jest członkiem jednostki wysoko wyszkolonych i utalentowanych czarodziejów pracujących dla Ministerstwa Magii. Do ich głównych obowiązku należy badanie przestępstw związanych z czarną magią, mogą również aresztować i uwięzić czarownice lub czarodzieja, którzy posługują się zakazaną magią. Można traktować ich jak odpowiednik mugolskiej policji czy wojska. W Brytyjskim Ministerstwie Magii pracują w Departamencie Przestrzegania Prawa Czarodziejów, podobnie zresztą i w Magicznym Kongresie Stanów Zjednoczonych Amerki - tam również pracują w departamencie o tej samej nazwie. </p>
<p>Program rekrutacyjny dla aurorów w Wielkiej Brytanii powstał w XVII wieku dzięki ministrowi magii - Eldritchowi Diggory.  </p>
<p>W 2007 roku Szefem Brytyjskiego Biura Aurorów został Harry Potter, który pełnił tę funkcję do 2019 roku, gdzie został Szefem Departamentu Przestrzegania Prawa Czarodziejów. Harry Potter jest jedynym przypadkiem czarodzieja, który został aurorem nie przechodząc procesu rekrutacyjnego czy szkolenia, został polecony na to stanowisko przez ówczesnego Ministra Magii - Kingsleya Shacklebolta. Po II wojnie czarodziejów brakowało w Ministerstwie Magii aurorów, dlatego wydaje mi się, że byli skłoni zatrudnić Pottera ze względu na jego dokonania. </p>
<p>Zadaniem aurora jest tropienie i łapanie czarnoksiężników. Kiedy złoczyńca zostaje złapany, zazwyczaj jest przekazywany władzy do osądzenia. Nieraz zdarza się, iż przestępcy nie chcą dobrowolnie oddać swojej wolności. Część z nich woli walczyć na śmierć, niż oddać się w ręce aurorów, przykładem jest śmierciożerca Evan Rosier.</p>


<div class="blok-cytat">
Do tego są potrzebne najwyższe oceny (...). Wymagają minimum pięciu owutemów, z których musisz mieć przynajmniej „powyżej oczekiwań”. Potem czeka cię cała seria surowych testów charakteru i umiejętności w urzędzie aurorów. To trudna ścieżka kariery, Potter, przyjmują tylko najlepszych.
  <p> Minerva McGonagall, Harry Potter i Zakon Feniksa</p></div><br>

<p>Szkolenie na aurora jest niezwykle trudne i wymaga wiele pracy, podzielone jest więc na kilka etapów. Na podstawie tego co mówiła profesor McGonagall nikt nie został przyjęty w szeregi aurorów w przeciągu trzech lat przed rokiem 1995. Zgodnie z brytyjskimi standardami, aby ubiegać się o przyjęcie na szkolenie należy zdobyć minimum pięć owutemów, zalecanymi przedmiotami są: Eliksiry, Obrona przed Czarną Magią, Transmutacja, Zielarstwo i Zaklęcia. Przedmioty te muszą być zaliczone na Wybitny lub Powyżej Oczekiwań. </p>

<div class="blok-cytat">
  Podczas kursu aurorów miałam najwyższe oceny z maskowania się, chodź wcale się tego nie uczyłam. (...) O mały włos nie oblałam kradzieży i śledzenia.
    <p> Nimfadora Tonks, Harry Potter i Zakon Feniksa</p></div><br>
<img class="img-fluid lewy2" src="../img/pod_auror.gif">
<p> Potencjalni kandydaci do zawodu aurora po przejściu procesu rekrutacyjnego, muszą zaliczyć rygorystyczne testy umiejętności i charakteru, w celu udowodnienia jak reagują pod presją. Kandydaci po testach wstępnych przystępują do intensywnego szkolenia z zaawansowanej walki magicznej oraz innych elementów praktycznej obrony. Prawdopodobnie również uczą się metod dochodzenia karnego. </p>

<p> Kwatera główna brytyjskich aurorów znajduje się na drugim poziomie Brytyjskiego Ministerstwa Magii. Biuro ma szereg otwartych kabin, w których każdy auror ma zapewnione miejsce do pracy. Listy gończe złych czarodziejów, mapy, wycinki z Proroka Codziennego i inne przydane rzeczy są zawieszane na tablicach.</p>
