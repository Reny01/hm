<h1>Zakon Feniksa</h1>
<div class="col-12">
  <div class="row">
  <div class="col-1"></div>
  <div class="col-10">
<img class="img-fluid ramka" src="../img/pod_zakon1.jpg">
</div>
<div class="col-1"></div>
</div>
