<br>
<p>Zakon Feniksa to tajna organizacja założona przez Albusa Dumbledore'a podczas pierwszego dojścia do władzy Lorda Voldemorta w 1970 roku w celu zwalczania rosnącego z jego strony zagrożenia. Do organizacji należało wiele utalentowanych czarownic i czarodziejów, a w tym również i niektórzy aurorzy z Ministerstwa Magii. Organizacja została założona po powrocie Czarnego Pana do Wielkiej Brytanii z zagranicy i rozpoczęciu przez niego działań mających na celu przejęcie Brytyjskiego Ministerstwa Magii. Dumbledore stworzył metodę komunikacji wśród członków Zakonu, którzy porozumiewali się ze sobą poprzez mówiące Patronusy. </p>
<p>Mimo że Śmierciożercy atakowali głównie mugoli i mugolaki, wkrótce zwrócili uwagę na Zakon i zaczęli walczyć ze „zdrajcami krwi”. Fabian i Gideon Prewett zostali zabici przez grupę sześciu Śmierciożerców prowadzonych przez Antonina Dołohowa. Caradoc Dearborn zniknął, rodzina Bonesów została niemal całkowicie zniszczona, Benio Fenwick został brutalnie zamordowany, a Marlena McKinnon i jej rodzina zostali zabici przez Śmierciożerców, wśród których był Travers. Pomimo ogromnych strat w ludziach Zakon nadal działał i walczył, czwórka z jego członków – James i Lily Potter oraz Frank i Alicja Longbottom – przeciwstawili się Voldemortowi trzy razy</p>
<p>Zakon Feniksa odegrał on kluczową rolę w czasie trwania Pierwszej Wojny Czarodziejów, która zakończyła się 31 października 1981 roku - zwycięstwem Zakonu, po tym jak Lord Voldemort został pokonany przez Harry'ego Pottera.  </p>
