<h1>WIZENGAMOT</h1>

<div class="blok-cytat">Poniższy tekst na temat Wizengamotu został opracowany przez Russela Harta, a ja za jego zgodą, zdecydowałem się go opublikować. Sam lepiej nie rozwinąłbym tematu sądu w Świecie Czarodziejów, a biorąc pod uwagę tak niewiele znanych faktów i konkretnych informacji przekazanych przez autorkę, jest to zadanie bardzo trudne. Informacje dopisane przeze mnie stanowią jedynie niewielki fragment, zamieszczony jako osobny akapit, na samym końcu prezentowanego rozdziału.<Br><Br></div><br>
<center><img class="img-fluid ramka" src="../img/pod_wizengamot.jpg" width="1000"></center><br>
<p>Wizengamot wyłania spośród swoich członków kandydatów na ministra magii – odbywają się wybory, w których bierze wtedy udział całość uprawnionych, pełnoletnich obywateli magicznych (należą do nich czarodzieje oraz wszystkie istoty magiczne). Wizengamot nadzoruje ministra magii, który ma w pewnym sensie uprawnienia obejmujące zarówno sferę ustawodawczą, wykonawczą, jak i sądowniczą (może być najwyższym sędzią Wizengamotu, kierować jego rozprawami, o ile wszyscy członkowie wyrażą zgodę). Minister magii obejmuje swoje stanowisko z zasady bezterminowo, jedynie wniosek społeczności czaro-dziejów i Wizengamotu może spowodować jego szybsze odsunięcie od władzy. W dodatku istnieje specjalna procedura dotycząca sytuacji kryzysowych, np. gdy zagraża społeczności wielkie niebezpieczeństwo i potrzebna jest silna, natychmiastowa władza, tak jak wyglądało to w „Księciu Półkrwi”, gdy Scrimgeour bardzo szybko po abdykacji Knota został wyłoniony wskutek obrad Wizengamotu w trakcie nadzwyczajnego posiedzenia plenum. Zwłaszcza że był szefem biura aurorów. </p>

<p>Naczelny Mag Wizengamotu zazwyczaj pełni funkcję osoby nadzorującej obrady (swego rodzaju marszałka) czy proces sądowy. Dumbledore utracił ten tytuł i został zawieszony przez piorunująco zgodne wotum nieufności. </p>

<img class="img-fluid lewy" src="../img/pod_wizen.png">
<p>Wizengamot sam w sobie łączy funkcję legislacyjną, czyli ustawodawczą, tworzenia i wydawania prawa, wraz z sądowniczą, czyli egzekwowania jego przestrzegania. Jest zarazem parlamentem, jak i najwyższą instancją sądowniczą. Członkowie Wizengamotu są wybierani dożywotnio, lecz mogą zostać zawieszeni bądź usunięci przez zgodne orzeczenie wszystkich członków. Tak jak Dumbledore w V tomie, zarzucano mu bowiem szerzenie niebezpiecznych tez i podważanie autorytetu samego ministra magii. Składa się z 50 członków. </p>

<p>Wizengamot był dotychczas niejako kołem wzajemnej adoracji. Nie istniały jako takie wolne, powszechne wybory na jego członków; członkowstwo zapewniały wpływy, pieniądze, niby też dokonania w ŚM. To Wizengamot powoływał nowych członków Wizengamotu, tak że dochodziło do ciągłego wypełniania luk w osobach, które umierały - i tak od bardzo dawna.</p>

<p>Niższą instancją sądowniczą jest Rada Prawa Czarodziejów.</p>
<p>Konsorcjum Finansowe Goblinów i Czarodziejskich Inwestorów, instytucja rejestrująca i nadzorująca działanie banku Gringotta (coś jak Rada Polityki Pieniężnej w NBP), który pełni rolę banku centralnego magicznej Wielkiej Brytanii oraz jest bankowym monopolistą w tej społeczności, na zasadzie ugody między czarodziejami a goblinami. Bank Gringotta pełni podobną funkcję do banku rezerw federalnych w USA. Międzynarodowy Czarodziejski Fundusz Walutowy.</p>
