<h1> BRYTYJSKIE MINISTERSTWO MAGII</h1>
  <img class="img-fluid lewy" src="../img/pod_mm1.gif">
<p>Brytyjskie Ministerstwo Magii formalnie powstało w roku 1707 wraz z powołaniem pierwszego czarodzieja na stanowisko Ministra Magii, którym był Ulick Gamp. Przyczyną zmian w organach władzy, w świecie czarodziejów (wcześniej istniała Rada Magów/Czarodziejów) było wprowadzenie i oficjalne uchwalenie Międzynarodowego Kodeksu Tajności Czarów/Czarodziejów, które nastąpiło w roku 1692. Świat Czarodziejów potrzebował organizacji rządzącej, która byłaby o wiele lepiej zorganizowana, działałaby na o wielu więcej płaszczyznach życia; pomagając, regulując i komunikując się z czarodziejami. Potrzebowali takiej struktury, która byłaby o wiele bardziej złożona i uporządkowana. </p>

<p>Minister Magii wybierany jest najczęściej na drodze demokratycznej elekcji, są jednak wyjątki od tego. W czasach kryzysu zdarzało się, że stanowisko było oferowane komuś bez publicznych wyborów. Nie ma ustanowionego limitu w urzędowaniu tego stanowisko, jednak każdy minister jest zobowiązany do przeprowadzania regularnych wyborów w odstępie minimum 7 lat. </p>
<img class="img-fluid prawy2" src="../img/pod_mm2.gif">
<p>Siedziba Ministerstwa Magii znajduję się pod ziemią, żeby się do niego dostać trzeba w wybranej budce telefonicznej wykręcić numer 62442 (odpowiadający na klawiaturze telefonu literom MAGIA) i oczywiście podać nazwisko oraz cel wizyty. Po otrzymaniu przepustki, budka zjeżdża na dół do głównego holu. Jest to jednak jeden ze sposobów dotarcia do MM, najczęściej stosowany przez gości odwiedzających ministerstwo. Pracownicy mogą do ministerstwo udawać się innymi metodami, jedną z nich jest podróż przez sieć Fiuu. </p>

<p>Jak każde ministerstwo również i MM posiada wyspecjalizowane działy, a w tym przypadku departamenty, zajmujące się konkretnymi sprawami w świecie czarodziejów, oto one:  </p>
