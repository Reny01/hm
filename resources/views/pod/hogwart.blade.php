<h1> Szkoła Magii i Czarodziejstwa w Hogwarcie</h1>
<p>W Świecie Magii niektórzy nauczyciele HM uczą dokładnych dat powstania Hogwartu. Spotkałem się już z rokiem 992, 1015 i innymi. Swego czasu przeszukałem wszelkie możliwe źródła, przeczytałem większość wypowiedzi Rowling na temat ‘Tajemnic HP’ i w żadnym z nich nie padła dokładna data powstania szkoły, jest prawie wszędzie wzmianka ‘że został założony tysiąc lat temu’ i to wszelkie informacje. </p>

<div class="blok-cytat">- No cóż… - powiedział z namysłem – Zaraz, niech no pomyślę… Komnata Tajemnic… No więc, jak na pewno wszyscy wiecie, Hogwart został założony po-nad tysiąc lat temu… dokładna data nie jest znana… przez czworo największych czarodziejów i czarownic tamtych czasów: Godryka Gryffindora, Helgę Hufflepuff, Rowenę Ravenclaw i Salazara Slytherina. Do dziś ich nazwiska noszą wasze cztery domy. Razem zbudowali ten zamek z dala od wścibskich spojrzeń mugoli, była to bowiem epoka, w której ludzie bali się magii, a czarownice i czarodzieje byli bardzo prześladowani.
<p> Profesor Cuthbert Binns wypowiada się o Hogwarcie – HP i Komnata Tajemnic.</p></div><br>
<img class="img-fluid lewy" src="../img/pod_hogwart1.gif">
<p>Biorąc pod uwagę słowa profesora Binns, rzeczą niemal pewną jest, że dokładna data założenia Hogwartu nie jest znana, skoro sam profesor Historii Magii tego nie wie. Można jednak mieć przypuszczenia co do samego wieku powstania tej szkoły. Wydarzenia w książce dzieją się w latach 1992 – 1993 i wiemy, ze Hogwart powstał ‘ponad tysiąc lat temu’ to znaczy, że został założony przed rokiem 1000. W takim wypadku rok 1015 odpada. Jeśli założenie przypadało by na rok 992 to Binns użył by sformułowania ‘dokładnie’ lub po prostu tysiąc lat temu, jeśli jednak coś jest ponad, to znaczy że przekracza podaną wartość, czyli samo założenie przypada przed rokiem 992. Wspomina o epoce, w której czarownice i czarodzieje byli bardzo prześladowani, i tu musimy zajrzeć do wiadomości z historii mugoli. Wiadomo, że pierwsze wzmianki o prześladowaniach lub próby ich potępienia można przypisać na rok 906 kiedy to wydano Canon Episcopi. Zainteresowani mogą poszukać dokładnych informacji czym to było, ja nie będę się jednak nad tym rozpisywał bo nie jest to naszym głównym tematem, chodzi jednak oto, że jeśli Rowling opiera swoje wiadomości w jakimś stopniu o historie mugoli to można przypuszczać, że sam Hogwart mógł być założony między rokiem 906, a 992, czyli w wieku X. </p>

<p>Wysunąłem tę hipotezę ale prawda jest też taka, że Rowling zdarza się popełniać błędy w samych datach i liczbach. Pojawiło się nawet o tym kilka dyskusji na przeróżnych forach, więc moje domysły mogą być błędne. Patrząc jednak na przedstawione wiadomości z książek, porównując to z danymi z mugolskiej historii można wysunąć właśnie takie przypuszczenia.</p>
