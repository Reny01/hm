  <h2>SPOSÓB PRZYJĘCIA</h2>
<img class="img-fluid prawy" src="../img/pod_hogwart3.gif">
<p>Warto wiedzieć, że w Hogwarcie, w jednej z wieży niedostępnej dla uczniów znajduje się magiczne pióro (Quill of Acceptance) oraz stara księga (The Book of Admittance), które nadal działają pod wpływem zaklęć założycieli. Ich zadaniem jest wyłapywanie nowo narodzonych czarodziejów i wpisywaniem na listę przyszłych studentów Hogwartu. </p>

<p>O ile magiczne pióro ma łagodne usposobienie i nawet najmniejszy przejaw magii budzi je do życia, o tyle księga jest bardziej surowsza i potrzebuje większych dowodów na to, by pozwolić na zapisanie nazwiska przyszłego studenta Szkoły Magii i Czarodziejstwa. Ich współpraca jest o tyle dobra i niesamowita przez te minione wieki, że wyłapują tylko osoby z prawdziwym talentem do czarowania, a nie, np. charłaków, którzy z pozoru mogą emitować niewielką moc magiczną (szczególnie w dzieciństwie, kiedy mogą emitować słabą magię od swoich rodziców).</p>

