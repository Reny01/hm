  <h2>CHARAKTERYSTYKA</h2>

<p>Szkoła Magii i Czarodziejstwa w Hogwarcie założyło czterech wielkich czarodziejów tamtych czasów: Godryk Gryffindor, Helga Hufflepuff, Rowena Ravenclaw i Salazar Slytherin. </p>
<img class="img-fluid lewy" src="../img/pod_hogwart2.gif">
<p>Wybudowano ją na wzniesieniu skalnym, z jednej strony otoczonym jeziorem, a z drugiej lasem. Do Szkoły trafić może każdy kto przejawia zdolności magiczne i niekoniecznie pochodzi z rodziny czarodziejskiej. Młody czarodziej dostaje zaproszenie do Hogwartu w momencie skończenia 11 lat, a to znaczy, że nie jest brany pod uwagę rok urodzenia, ale konkretna data. Hermiona mimo, że jest w jednej klasie z Harrym, jest od niego starsza, a to dlatego, że urodziny obchodzi we wrześniu i swój list z Hogwartu dostała w momencie gdy trwał już rok szkolny, więc automatycznie była zapisana na kolejny. </p>


<p>W Szkole Magii i Czarodziejstwa pierwszoroczni mają obowiązek uczestniczenia na zajęciach z siedmiu przedmiotów: Astronomii, Eliksirów, Historii Magii, Obrony Przed Czarną magią, Zaklęć, Transmutacji i Zielarstwa. Lekcje latania na miotłach są dla nich również obowiązkowe. Pod koniec drugiego roku nauki, uczniowie muszą wybrać minimum dwa nowe przedmioty, których zaczną się uczyć, czyli: Numerologia, Mugoloznastwo, Wróżbiarstwo, Starożytne Runy i Opieka Nad Magicznymi Stworzeniami. </p>

<p>Istnieje możliwość wystąpienia w dwóch ostatnich latach nauki bardziej specjalistycznych przedmiotów, pod warunkiem, że uzyskają wystarczające zainteresowanie ze strony uczniów, takim przedmiotem jest choćby Alchemia.</p>

