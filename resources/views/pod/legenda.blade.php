  <p>Nie istnieje jedna najbardziej wiarygodna legenda, każda z historii z którą się spotkamy może zawierać w sobie prawdę, ale też może być całkiem zmyślona i nie obrazować w żaden sposób prawdziwej historii, z tym jednak wiąże się samo słowo legenda. Właśnie dlatego czytając powyższe opowieści, jak i inne historie z którymi można się spotkać, trzeba mieć na uwadze dokładne znaczenie słowa legendy oraz na całą historię patrzeć z przymrużeniem oka. Dlaczego więc legendy tak często się pojawiają? Cóż, na to pytanie jest mnóstwo odpowiedzi, wiąże się to z mnóstwem czynników, zaczynając od kulturowych po historyczne tradycje, próby wytłumaczenia niektórych zjawisk czy po prostu pełną historię, która zaczyna się od danego momentu, rozwija przez pewien czas i kończy w ściśle określonym miejscu. Jest to temat rzeka, a ja przedstawiłem tylko niewielki zalążek tego jak może to być odbierane.</p>
