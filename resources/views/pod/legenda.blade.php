<h1> LEGENDA O POWSTANIU PIERWSZEJ RÓŻDŻKI</h1>
<img class="img-fluid lewy" src="../img/pod_legenda2.jpg">
<p>Jaskiniowiec po straszliwej burzy szukał schronienia. Idąc znalazł odłamaną gałąź, która została lekko przypalona od uderzenia pioruna w drzewo. Gałąź całkiem przypadkiem upaprał błotem gdy zaczął ją oglądać z każdej strony. W pewnym momencie, całkiem przypadkiem, dźgnął się w oko. Zdenerwowany zaczął wymachiwać "patykiem" i "jakimś dziwnym sposobem" na końcu różdżki zaiskrzył ogień. I tak została wynaleziona nie tylko różdżka, ale także ogień i pierwsze zaklęcie, ale oczywiście jaskiniowiec nie zrozumiał, co właśnie zrobił. Po wielu latach to zaklęcie nabrało normalniejszego kształtu.</p>

<p>Inna legenda, którą zaczyna opowiadać coraz więcej historyków magicznych, mówi o tym, że gdy pierwsi ludzie przebywali w schronieniu, jeden z nich zaczął ocierać gałąź o gałąź. Było mu najprawdopodobniej zimno, a czynność którą właśnie wykonywał była zupełnie przypadkowa, podobnie jak wypowiedziane słowa, które wymówił podczas jej wykonywania. Mogły to być również rytualne słowa i języki. Wtedy właśnie z końca patyków zaczęło iskrzyć i pojawił się ogień. </p>

