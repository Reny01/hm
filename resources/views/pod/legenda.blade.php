  <p>Niedawno archeolodzy znaleźli na terenie dawnej Mezopotamii, stare zapiski z roku 700 p.n.e, był to najprawdopodobniej dziennik prowadzony przez nieznanego człowieka, przypuszczalnie pierwszego świadomego maga, jak wynika z odczytanej na pergaminie wiadomości. Oto o czym ona mówiła: </p>

  <p><span class="important">3 dzień po zaćmieniu słońca.<span><br>
  Stało się dzisiaj coś bardzo dziwnego. Sam nie wiem jak mam to tłumaczyć. Nie planowałem tego. To nie był mój cel a co dopiero mówić o zamiarze. Nazywano mnie już cudownym kapłanem. Padały też takie słowa jak diabeł. Ale dzisiaj pierwszy raz sam o sobie mogę mówić mag. Muszę dokładnie sprawdzić to co przed chwilę się stało. Jednak nie mam do tego teraz siły. Jestem cały roztrzęsiony. Czy to w ogóle możliwe. Muszę to sprawdzić.</p>

  <p><span class="important">4 dzień po zaćmieniu słońca.<span><br>
  Poszedłem do rzemieślników. Wyjaśnili mi działanie niektórych z podstawowych czynności. Wydaje mi się że można to jakoś udoskonalić. Ale jak nazwać kawałek patyka który strzela ogniem. Ogniopatyk. Nie zastanawiałem się nad tym jeszcze, ale może umie on inne rzeczy. To zbyt dużo, zbyt dużo. Potrzebuje więcej czas. Ale nie mogę tego robić w świątyni. Przeniosę się gdzieś poza miasto. </p>
<img class="img-fluid lewy" src="../img/pod_legenda1.gif">
  <p><span class="important">6 dzień po zaćmieniu słońca.<span><br>
  Przeniosłem się do starej chaty nad Tygrysem, między drzewami nikt jej nie zauważy. W końcu mogę w spokoju prowadzić badania. Z tego co już zdążyłem zauważyć, patyk należy do czarnego drzewa, służył mi wcześniej jako składnik do przeprowadzania niektórych wróżb i mikstur. Przypadkiem, podczas tworzenia jednego z takich wywarów zaczepił o siebie włos białego konia. Bardzo rzadkiego, z rogiem na środku pyska. Nie pamiętam co stało się potem. Wiem, ze spaliłem przez ten patyk pół swojej pracowni. Mogę mieć tylko domysły, żadnych udowodnionych prawd. Potrzebuje więcej badań. </p>

  <p><span class="important">7 dzień po zaćmieniu słońca.<span><br>
