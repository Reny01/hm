<h1> Wingardium Leviosa</h1>
  <img class="img-fluid lewy" src="../img/pod_wg.gif">
<p>Zaklęcie Wingardium Leviosa jest jednym z najbardziej podstawowych zaklęć jakie są nauczane w Szkole Magii i Czarodziejstwa w Hogwarcie, dzięki niemu czarodziej może unieść dowolną rzecz w powietrze poprzez jedno machnięcie różdżki. Wszystko zaczęło się w roku 1544, kiedy to mag Jarleth Hobart był święcie przekonany, że udało mu się posiąść umiejętność latania. Postanowił on zorganizować pokaz tego co udało mu się wynaleźć, dlatego zaprosił on wielu czarodziejów, a w tym również i Naczelny Maga Wizengamotu na 16 lipca 1544. Tego dnia przed tłumem zgromadzonych magów wspiął się on na dach kościoła i wyskoczył w powietrze. Początkowo cały jego misterny plan wydawał się skutkować, unosił się on bowiem w powietrzu, a ludzie byli w niego wpatrzeni z zachwytu. Jednak po trzech minutach zaczęli się niecierpliwić, chcieli bowiem zobaczyć jak Jarleth lata, a nie tylko się unosi. Zaczęli go wygwizdywać i szydzić pod nosem. Czarodziej próbował wykonywać szybkie ruchy, jak przy pływaniu, ale niewiele mu to pomogło. Pomyślał, że to przez ubrania, które ma na sobie nie może się przemieścić, dlatego zdjął szybko buty i szatę, efekt był jednak przeciwny do zamierzonego – opadł on na trzy metry. Na ten widok zgromadzeni czarodzieje zaczęli głośniej szydzić i wyśmiewać Hobarta, tak że ten zdenerwowany zdjął wszystkie ubrania jakie miał na sobie, włącznie z bielizną. Szybko jednak przekonał się, że nie była to najlepsza decyzja, zaraz bowiem spadł na ziemie łamiąc sobie parę kości. Naczelny Mag Wizengamotu skwitował cały występ jako szalony pokaz głupoty. Jarleth Hobart nie poddał się jednak, wrócił on do domu i kontynuował pracę nad swoim odkryciem. Zdał sobie sprawę, że zaklęcie które wymyślił tak naprawdę sprawiało, że dany przedmiot zaczynał unosić się w powietrzu, nie był to lot bo nie można było zmienić kierunku przemieszczenia się danego przedmiotu, była to za to lewitacja. Uradowany swoim odkryciem postanowił on ponownie zaprosić czarodziejów aby zademonstrować im prawdziwą naturę tego zaklęcia. </p>
<img class="img-fluid prawy2" src="../img/pod_wg2.gif">
<p>Na jego drugie spotkanie zjechało dużo więcej magów niż poprzednio, wszyscy bowiem liczyli na dobrą zabawę i powody do śmiechu. Samo spotkanie przebiegło dużo lepiej niż poprzednio, Jarleth zaczął unosić małe przedmioty takie jak kamienie, przechodząc do coraz większych jak np. powalone drzewa. Tłum wiwatował. Jarleth dopiął swego, tłum był zachwycony jego wyczynami, a on postanowił że na wielki ﬁnał tego spotkania uniesie kapelusz Naczelnego Maga Wizengamotu. Niestety dopiero potem wraz z całym zgromadzonym tłumem czarodziejów, zdał sobie sprawę, że Naczelny Mag nosi tupecik. Tylko dzięki uniesieniu się szat Naczelnego Maga i szybkiej ucieczce udało mu się uniknąć kary. </p>



