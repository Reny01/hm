<p>Bank Gringotta powszechnie uważany był za jedno z najbezpieczniejszych miejsc w Wielkiej Brytanii, prawie tak bezpieczny jak Hogwart. Tyle że do obu miejsc się włamano. Jeśli chodzi o Gringotta, pierwsze włamanie miało miejsce w roku 1991 i dokonał tego profesor Quirrel, a drugie w 1997 dokonane przez Harrego Pottera, Rona Weasleya i Hermione Granger. Bank Gringotta nie ma znanej konkurencji (o czym świadczą słowa Hagrida) w branży bankowości czarodziejów i pod tym względem jest monopolistą w Wielkiej Brytanii. Warto więc wiedzieć, że gobliny mają sporą władzę nad czarodziejami w kwestiach gospodarki, w końcu to one wybijają galeony, sykle czy knuty. Można więc przypuszczać, że istnieją pewne uregulowania prawne między Bankiem Gringotta, a Ministerstwem Magii w razie pojawienia się pewnych sprzeczności czy konfliktów, które miałyby powstrzymać gobliny przed zwiększeniem swojej władzy lub wpływów nad czarodziejami.</p>


