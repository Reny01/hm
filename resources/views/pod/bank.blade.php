<h1> BANK GRINGOTTA</h1>
  <img class="img-fluid lewy" src="../img/pod_bank1.gif">
<p>Jedyny bank czarodziejów istniejący w ich świecie, pieczę nad nim sprawują gobliny. Jego początek sięga roku 1474, w którym to goblin Gringott założył nową instytucję, która przyniosła mu sławę i bogactwo, zostając automatycznie jej szefem. Bank Gringotta jest jedną z bardzo niewielu rzeczy, które czarodzieje nie odebrali goblinom. Sam Gringott kiedyś sprawował rządy nad bankiem, nie zatrudnił w nim ani jednego czarodzieja, wyłącznie gobliny.  </p>

<p>Główne biura tej instytucji znajdują się na ulicy Pokątnej w Londynie. Sam bank oprócz przechowywania pieniędzy, kosztowności, pamiątek rodzinnych czy cennych dokumentów czarodziejów i czarownic, oferuję również wymianę mugolskich pieniędzy na pieniądze czarodziejów. Mottem Banku Gringotta jest ‘Fortius Quo Fidelius’ będący łacińskim zwrotem oznaczającym ‘Siłę poprzez lojalność’.  </p>

  <img class="img-fluid prawy2" src="../img/pod_bank2.gif">
<p>Skrytki w Banku Gringotta znajdują się pod głównymi biurami, w specjalnych kryptach. Im dana skrytka znajduje się głębiej, tym zabezpieczenia ochraniające ją są większe, a samo otwarcie skrytki wymaga większych nakładów. Dla rodzin najbiedniejszych, gdzie skrytki znajdują się niemal na samym początku, wystarczy sam klucz do ich otwarcia i w takim też wypadku, nie potrzeba aby goblin towarzyszył rodzinie w otwarciu skarbca. Im jednak niżej tym pojawia się więcej zabezpieczeń, niektóre krypty otworzyć mogą wyłącznie gobliny. Trzeba jednak pamiętać, że jeśli taką kryptę spróbuje otworzyć zwykła osoba, zostanie ona wciągnięta do środka skarbca. Gobliny sprawdzają skrytki co dziesięć lat i wtedy mogą usunąć ewentualne ciała. Na samym dnie, w kryptach położonych najbardziej pod powierzchnią znajdują się najstarsze i najlepiej chronione skarbce, należące zazwyczaj do najbogatszych rodzin czarodziejów. Strzeże ich smok, ślepy Spiżobrzuch Ukraiński. Niektóre ze skrytek chronione są również tak zwanym Wodospadem Złodzieja, który wykrywa i usuwa wszelkie zaklęcia i przebieranki, od razu wszczynając alarm, i dając znać goblinom o potencjalnym oszuście.   </p>

