<h1> MAGICZNE STWORZENIA</h1>
<img class="img-fluid lewy" src="../img/pod_stwo.jpg">
<p>Próby definicji zwierząt i istot od wieków sprawiały czarodziejom trudność, czasem do tego stopnia, że wywoływały ogólne kłótnie i otwarte spory. Mimo że samo pojęcie nie wiąże się ściśle z Historią Magii, to niemniej organizowane spotkania oraz konsekwencje jakie wywoływał ten problem odbiły się na kartach historii czarodziejów. Sama Bathilda Bagshot opisywała w swojej książce jedno ze spotkań, na które zaproszono przedstawicieli każdej z nacji. Właśnie to spotkanie, było pierwszą z prób zdefiniowania nowych praw czarodziejów. Burdock Muldoon, przewodniczący Rady Czarodziejów, postanowił zaprosić na to spotkanie wszystkich przedstawicieli ‘istot’, w celu dokładniejszego omówienia kwestii prawnych związanych z definicją jaką przedstawił. Niestety nie wszystko poszło zgodnie z jego przypuszczeniami.  </p>


<div class="blok-cytat">Niewiele dało się usłyszeć w straszliwym zgiełku, w którym skrzeczenie dirikraków i lamenty lelków wróżebników mieszały się z nieustającą, przeszywającą pieśnią świergotników. Kiedy czarownice i czarodzieje próbowali omówić leżące przed nimi dokumenty, najróżniejsze chochliki i elfy zaczęły krążyć nad ich głowami, chichocząc i trajkocząc. Około tuzina trolli jęło rozbijać komnatę maczugami, podczas gdy baby-jagi krążyły po sali w poszukiwaniu dzieci do zjedzenia. Przewodniczący rady wstał, poślizgnął się na odchodach kudłonia, i wybiegł z sali, miotając przekleństwa.
<p>Bathilda Bagshot, Dzieje Magii.</p></div><br>

<img class="img-fluid prawy" src="../img/pod_stwo2.jpg">
<p>Samo spotkanie okazało się wielką klapą, a Burdock ostatecznie zrezygnował z prób integracji między czarodziejami, a magicznymi stworzeniami. Kolejna próba wywołała nowe powody do sporów, a definicja Elfridy Clagg nie była dostatecznie dobra by doszło do jakiejkolwiek współpracy między uczestnikami tego wydarzenia. Trolle demolowały salę, wozaki urządzały wyścigi, a delegacje duchów i centaurów były urażone sposobem jakim je potraktowano, rezygnując, a nawet protestując przed przyjściem na to spotkanie.  </p>

<p>Dopiero w roku 1811, Grogan Kikut, nowo mianowany minister magii ustalił podział, który był w stanie zadowolić większość mieszkańców magicznego świata. Trzeba jednak pamiętać, że wymagało to od niego nieco więcej starań niż jego poprzedników. Nie wszyscy bowiem byli zadowoleni wynikłymi podziałami, i tym sposobem w Urzędzie Kontroli nad Magicznymi Stworzeniami, Kikut stworzył trzy wydziały: Wydział Zwierząt, Wydział Istot oraz Wydział Duchów. </p>

<p>Warto jednak dodać, że sama klasyfikacja nie jest doskonała. Wiele stworzeń jest bardzo trudnych do przyporządkowania, do konkretnej grupy, czy to ze względu na ich niezdolność do współpracy, brutalność czy sposób wypowiadania się. Nawet Ministerstwo Magii ma z tym problem i nie do końca jest zdecydowanych gdzie umieścić dane stworzenie, przykładem jest choćby przerzucanie wilkołaków pomiędzy wydziałami. Służby Pomocy Wilkołakom działają w Wydziale Istot, natomiast ich rejestr i Brygada Ścigania Wilkołaków funkcjonuje w Wydziale Zwierząt. </p>

