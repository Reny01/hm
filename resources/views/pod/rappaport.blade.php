<h1>Prawo Rappaport</h1>
<div class="row">
<div class="col-1"></div>
<div class="col-10">
<img class="img-fluid ramka" src="../img/pod_mac.jpg">
</div>
<div class="col-1"></div>
</div>
<br>
<p>W roku 1790 pełniąca funkcję piętnastego przewodniczącego MACUSA Emily Rappaport przeforsowała prawo mające na celu całkowite oddzielenie społeczności czarodziejów i niemagów. Było ono następstwem jednego z najpoważniejszych naruszeń Międzynarodowej Ustawy o Tajności i upokarzającego przejęcia MACUSA przez Międzynarodową Federację Czarodziejów. Problem był tym poważniejszy, że naruszenie miało swoje źródło w samym MACUSA.</p>

<p>Mówiąc w skrócie, w katastrofę zamieszana była córka zaufanego współpracownika przewodniczącej Rappaport, strażnika skarbu i dragotów (dragot to amerykańska waluta świata czarodziejów, a strażnik dragotów, jak sama nazwa wskazuje, odpowiada mniej więcej sekretarzowi skarbu Stanów Zjednoczonych). Arystoteles Twelvetrees był kompetentnym człowiekiem, ale jego córka Dorka nie grzeszyła bystrością umysłu. Była raczej słabą uczennicą Ilvermorny, a w czasie, gdy jej ojciec otrzymał swą prestiżową posadę, mieszkała w domu i lekceważyła magię, skupiając się na ubraniach, układaniu włosów i przyjęciach.</p>

<p>Pewnego dnia na regionalnym pikniku z Dorką Twelvetrees zaczął flirtować przystojny niemag imieniem Bartłomiej Barebone. Dziewczyna nie zdawała sobie sprawy, że jest on potomkiem Czyścicieli. Nikt w jego rodzinie nie posiadał magicznych mocy, ale jego wiara w magię była niezachwiana, podobnie jak przekonanie, że czarownice i czarodzieje są złem wcielonym.</p>

<p>Zupełnie nieświadoma niebezpieczeństwa, Dorka wzięła zainteresowanie Bartłomieja jej „sztuczkami” za przejaw sympatii. Zwiedziona niewinnymi pytaniami młodzieńca, wyjawiła mu tajne adresy MACUSA i szkoły Ilvermorny, opowiedziała o Międzynarodowej Federacji Czarodziejów i zdradziła, w jaki sposób organizacje te utrzymują świat magii w tajemnicy.</p>

<p>Wyciągnąwszy od Dorki wszystkie informacje, Bartłomiej ukradł różdżkę, którą dziewczyna mu uprzejmie wręczyła, pokazał ją wszystkim dziennikarzom, do których zdołał dotrzeć, po czym zebrał grupę uzbrojonych przyjaciół, by znaleźć i – o ile się uda – zabić wszystkich czarodziejów w okolicy. Ponadto Bartłomiej wydrukował ulotki z adresami, pod którymi czarownice i czarodzieje się spotykali, i wysłał listy do najważniejszych osobistości niemagicznego świata. Niektórzy z adresatów uznali za konieczne sprawdzić, czy rzeczywiście w wyznaczonych miejscach odbywają się „zebrania straszliwego kultu”.</p>

<p>Podniecony swą misją dekonspiracji magicznego świata Ameryki, Bartłomiej Barebone posunął się o krok za daleko i otworzył ogień w kierunku grupy ludzi, których wziął za czarodziejów MACUSA, a którzy okazali się pechowymi niemagami, wychodzącymi z obserwowanego przez niego budynku. Na szczęście nikt nie zginął, Bartłomiej zaś został aresztowany i trafił do więzienia bez konieczności jakiejkolwiek interwencji ze strony MACUSA. MACUSA przyjął te wieści z wielką ulgą, ponieważ walka z konsekwencjami niedyskrecji Dorki nie należała do łatwych.</p>

<p>Bartłomiej rozprowadził swoje ulotki po całym kraju, a niektóre gazety wzięły jego rewelacje poważnie i wydrukowały nawet zdjęcia różdżki Dorki z notatką, że „kopie jak koń”, gdy się nią pomacha. Budynek MACUSA przyciągał tyle uwagi, że kongres zmuszony był zmienić siedzibę. Jako przewodnicząca, Rappaport musiała oznajmić na publicznym przesłuchaniu Międzynarodowej Federacji Czarodziejów, że nie jest w stanie zagwarantować, że wszystkim osobom, które uwierzyły w informacje Dorki, wymazano je z pamięci. Przeciek był na tyle poważny, że jego następstwa miały być odczuwane przez długie lata.</p>

<p>Mimo że wielu członków czarodziejskiej społeczności wnioskowało o skazanie Dorki na dożywocie lub nawet śmierć, dziewczyna spędziła w więzieniu zaledwie rok. Zhańbiona i wstrząśnięta, powróciła do zupełnie innego środowiska. Swych dni dokonała w samotności – jej jedynymi towarzyszami było lustro i wierna papuga.</p>

<p>Niedyskrecja Dorki doprowadziła do ustanowienia prawa Rappaport, które narzuciło ścisłą segregację na czarodziejów i niemagów. Czarodziejom nie wolno już było nawiązywać przyjaźni ani poślubiać niemagów, a kary za spoufalanie się były niezwykle surowe. Komunikację z niemagami dopuszczano wyłącznie w zakresie niezbędnym do wykonywania codziennych czynności.</p>

<p>Prawo Rappaport uwydatniło jeszcze różnice kulturowe pomiędzy amerykańskim i europejskim środowiskiem czarodziejów. W Starym Świecie rządy magiczne i niemagiczne zawsze współpracowały ze sobą w tajemnicy przed obywatelami. W Ameryce MACUSA działał samodzielnie, nie kontaktując się z rządem niemagów. W Europie czarownice i czarodzieje poślubiali i przyjaźnili się z niemagami, natomiast w Ameryce zaczęto traktować ich jak wrogów. Ostatecznie prawo Rappaport zaprowadziło amerykańską społeczność czarodziejów, która już zmagała się z wyjątkowo podejrzliwym usposobieniem niemagów, jeszcze głębiej pod ziemię. </p>
