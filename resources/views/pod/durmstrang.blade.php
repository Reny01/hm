<p>Mówi się o złej sławie Durmstrangu ale trzeba jednak powiedzieć, że w ostatnich latach szkoła przeżywa swego rodzaju odrodzenie, a nawet pewne odkupienie. W końcu w poczet jej absolwentów dołączył międzynarodowej sławy zawodnik quidditcha, Wiktor Krum.  </p>

<p>Uczniowie w Durmstrangu są wychowani na wytrwałych i silnych czarodziejów, a samą szkołę można porównać do swego rodzaju szkoły wojskowej przez rygorystyczne zasady i usposobienie uczniów. W zimie studenci Instytutu nosili futra i krwistoczerwone szaty, natomiast na herbie Durmstrangu znajduje się dwugłowy orzeł oraz elementy Cyrylicy.
