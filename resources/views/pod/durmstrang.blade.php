<h1> INSTYTUT DURMSTRANG</h1>
<img class="img-fluid lewy" src="../img/pod_dur1.gif">
<p>Szkoła Magii przyjmująca w swoje szeregi zarówno czarodziejów jak i czarownice, jest jedną z trzech największych europejskich szkół magii, znajdująca się na północnych obszarach Szwecji lub Norwegii. Uznawany jest za najbardziej sekretną ze szkół, nawet osoby, którym udało się trafić do niej, nie są w stanie w pełni opisać zamku i jego otoczenia (przez zaklęcia wymazujące pamięć). Nieliczni jednak opowiadają o oszałamiających widokach oraz niesamowitym, upiornym statku, z którego uczniowie skaczą do wody, oczywiście w lecie. Szkoła przyjmuje uczniów, którzy pochodzą z rodzin czystej krwi lub są półkrwi, mugolacy nie mają prawa się tam uczyć.  </p>

<p>Przez dłuższy czas Durmstrang był uważany za najmroczniejsza ze wszystkich istniejących szkół magii (było ich około 11). Przyczynić do tego mogli się przede wszystkim czarownicy złej sławy, którzy rządzili szkołą, jak i fakt, że w grono uczniów Instytutu wchodził niebezpieczny uczeń, powszechnie uważany za czarnoksiężnika. Właśnie dlatego sądzono, że sam Instytut ma ‘konszachty’ ze złem. Trzeba jednak wspomnieć, że w swej historii Hogwart również liczył dyrektorów owianych złą sławą, negatywnie nastawionych do niektórych ze swoich uczniów, no i co najważniejsze, do absolwentów Hogwartu również można zaliczyć czarnoksiężnika, przez niektórych uznawanego za najbardziej podłego i groźnego.  </p>

<img class="img-fluid prawy2" src="../img/pod_durm.png">
<p> Założycielką Instytutu Durmstrang była Nerida Vulchanova, pochodzenia bułgarskiego, która zmarła w niewyjaśnionych okolicznościach. Jej następcą został Harfang Munter, który składał szczególny nacisk na pojedynki oraz wszelkie formy morderczej magii, jego program nauczania miał szczególny wpływ na reputację szkoły. Kolejnym dyrektorem, który przyczynił się znacząco na utrwalenie mrocznej sławy Durmstrangu był Igor Karkarow. Śmierciożerca, który na wieść o powrocie Lorda Voldemorta porzucił swoje dotychczasowe stanowisko i uciekł. Za czasów jego dyrektorowania wiele rodziców zabierało swoje dzieci z Durmstrangu ponieważ Karkarow miał w zwyczaju zastraszać swoich uczniów, a do tego był pozbawionym zasad egoistą. Mówiąc o postaciach powiązanych z Insytutem Durmstrang, których osoba znacząco wpłynęła na utrwalenie mrocznej sławy szkoły warto wspomnieć o jednym z jej uczniów, Gellercie Grindelwaldzie. Uznawany jest, obok Voldemorta, za najniebezpieczniejszego czarnoksiężnika XX wieku. Ludzie powiązują go z Durmstrangiem jako miejscem, w którym rzekomo rozbudził w sobie zainteresowanie czarną magią i złem. Czarodzieje jednak zapominają, że to właśnie za niebezpieczne eksperymenty i kontakty z ciemnymi mocami został on wyrzucony i nie ukończył szkoły.  </p>

