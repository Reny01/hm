<h1> Szkoła Magii Mahoutokoro</h1>

<p>Mahoutokoro jest japońską placówką, której pałac zbudowany jest z nefrytu i mieści się na szczycie wulkanicznej wyspy Minami Iwo Jima, dla mugoli jest to niezamieszkałe i opuszczone miejsce. Wśród wszystkich jedenastu Szkół Magii, przyjmuje ona w swoje grono najmniej uczniów, ale jako jedyna rejestruje adeptów już w wieku 7 lat. Od siódmego roku życia młodzi czarodzieje mogą przygotowywać się do nauki w Mahoutokoro, ale traﬁają do niej dopiero po skończeniu 11 lat. W trakcie trwania roku szkolnego, uczniowie przylatują i odlatują każdego dnia do swoich domów na grzbietach nawałników burzowych. </p>
<img class="img-fluid prawy" src="../img/pod_maho3.jpg">
<p>Na początku nauki uczniowie otrzymują specjalne szaty, które rosną wraz z nimi oraz zmieniają barwy, które świadczą o stopniu umiejętności danego czarodzieja. Początkowy kolor to słaby róż, a końcowy to złoty, który świadczy o uzyskaniu przez ucznia najlepszych ocen ze wszystkich przedmiotów. W momencie gdy kolory szat ucznia przyjmują kolor biały, oznacza to że dany czarodziej dopuścił się złamania Japońskiego Kodeksu Czarodzieja oraz stosował nielegalne praktyki, które prościej mówiąc oznaczają ‘Czarną Magię’. Kolor biały może też oznaczać złamanie Międzynarodowego Kodeksu Tajności Czarów. Stanie się ‘białym’ w społeczeństwie czarodziejów z Japonii jest wielką hańbą, a taki uczeń automatycznie zostaje wydalony z Mahoutokoro oraz poddany procesowi sądowemu w Japońskim Ministerstwie Magii. Mahoutokoro znany jest nie tylko z niesamowitej sprawności akademickiej, ale również z niezwykłej renomy Quidditcha, który według legend, został sprowadzony do Japonii wieku temu przez bandę lekkomyślnych uczniów Hogwartu, którzy zboczyli z kursu podczas próby okrążenia globu na całkowicie nieodpowiednich miotłach. Zostali jednak uratowani przez członka personelu czarodziejów z Mahoutokoro, który obserwował ruchy planet. Zostali zaproszenie do placówki jako goście i pozostali tam wystarczająco długo, aby nauczyć ich podstaw gry Quidditcha, czego później bardzo żałowali.</p>
<img class="img-fluid lewy3" src="../img/pod_maho.png"><p> Członkowie japońskiej drużyny Quidditcha oraz aktualni zwycięzcy Mistrzostw Ligi (Toyohashi Tengu) swoje męstwo przypisują wyczerpującemu treningowi, który otrzymali w Mahoutokoro, gdzie czasem ćwiczyli nad nie spokojnym morzem w warunkach sztormowych. Musieli uważać nie tylko na tłuczki, ale również na samoloty, bowiem mugolskie lotnisko znajdowało się na sąsiedniej wyspie. </p>


