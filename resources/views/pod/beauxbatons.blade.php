<h1> AKADEMIA MAGII BEAUXBATONS</h1>
<img class="img-fluid lewy" src="../img/pod_amb1.gif">
<p>Uznawana za jedną z najbardziej znanych i największych szkół magii, w której nauczani są zarówno czarodzieje jak i czarownice. Znajduje się najprawdopodobniej w południowej części Francji, nad Morzem Śródziemnym, bardzo prawdopodobne, że gdzieś w Pirenejach. Szkoła została założono ponad siedemset lat temu, a to znaczy, że jest młodsza od Hogwartu. Zamek, pełniący formę placówki dydaktycznej, otoczony jest malowniczymi ogrodami i magicznymi fontannami. Powszechnie uważa się, że akademia została częściowo dofinansowana przez państwo Flamel – Nicolasa i Perenellę, którzy jak wiadomo, ukończyli Beauxbatons, i tam właśnie się poznali. Nazwano nawet na ich cześć jedną z fontann, znajdujących się w środku parku, która podobno ma właściwości lecznicze i poprawiające urodę. </p>

<div class="blok-cytat">W Beauxbatons mamy na Bozinarozeni lodowi statui w cali Jadalni Komnat. Nie rozpliwai się, naturellement, są jak wielki statui z diament... I mami lasowi nimfi, one nam serenadui, kiedy jemy. Nie mamy taki stari zbroji, a jak poltergeist pojawi się w Beauxbatons, to robimy tak – tu trzasnęła dłonią w stół – i poltergeista nie ma!
<p>Fleur Delacour mówi o Beauxbatons – HP i Czara Ognia</p></div><br>

<img class="img-fluid lewy2" src="../img/pod_bea.jpg"><p>Podstawową różnicą pomiędzy Akademią, a Hogwartem jest zdawanie egzaminów, w Beauxbatons odbywają się one po 6 klasie, natomiast w Hogwarcie po piątej. Dodatkowo akademia przystosowywała uczniów do zdobywania artystycznego wykształcenia, widać to po przedstawionym tańcu potencjalnych reprezentantek na Turnieju Trójmagicznym. Szkolne szaty uczniów są błękitne i jedwabne, a więc przystosowane do klimatu we Francji, w Anglii było im zimno. Jednakowy kolor szat może świadczyć również o tym, że w Beauxbatons nie ma podziału na domy czy inne kasty. </p>
<img class="img-fluid prawy2" src="../img/pod_amb2.gif">
<p> Oprócz wcześniej wspomnianych Flamelów, do znanych absolwentów francuskiej szkoły magii należą również: Vincent Duc de Trefle-Picques, który uniknął dekapitacji poprzez rzucenie uroku ukrywania na swoją szyję i udawania, jakoby jego głowa miała być już odcięta, ma to oczywiście związek z rewolucją francuską; Luc Millefeuille, który był piekarzem i znanym trucicielem mugoli. Herbem szkoły są dwie skrzyżowane ze sobą różdżki, w złotym kolorze, z których to każda wypuszcza trzy gwiazdy, całość przedstawiona na błękitnym tle. Oczywiście nad herbem znajduje się nazwa placówki, a obok niej dwa pegazy. </p>
<img class="img-fluid lewy" src="../img/pod_amb3.gif">
<p>Uczniowie przyjmowani do szkoły, pochodzą z takich państw jak Francja, Hiszpania, Portugalia, Belgia, Luksemburg i Holandia. I powszechnie wiadomo, że do samego Beauxbatons uczęszcza dużo więcej uczniów niż do Hogwartu. Dyrektorką za czasów Turnieju Trójmaiczneo organizowanego w 1994 roku, była Madame Maxime. </p>

<p>Uczniowie tej szkoły, w 1994 roku przyjechali na Turniej Trójmagiczny do Hogwartu powozem zaprzężonym w abraksany, którymi oczywiście później zajął się Hagrid, gajowy Hogwartu. Według dyrektorki Beauxbatons, konie te potrzebowały dużo whisky i ‘mocnej ręki’. Sam powóz służył celom mieszkalnym całej reprezentacji akademii. </p>

