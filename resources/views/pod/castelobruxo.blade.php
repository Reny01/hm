<h1> SZKOŁA MAGII CASTELOBRUXO</h1>
<img class="img-fluid lewy" src="../img/pod_castelo.jpg">
<p>Castelobruxo jest brazylijską szkołą magii, do której uczęszczają adepci z terenów całej Ameryki Południowej. Zamek ukryty jest głęboko w lesie deszczowym i dla mugoli wygląda jak najzwyczajniejsza ruina, pod warunkiem że jakikolwiek mugol byłby w stanie znaleźć owe ruiny. Od lat trwają spory, kto od kogo zgapił pomysł ukrycia zamku, Castelobruxo od Hogwartu, czy Hogwart od Castelobruxo.</p>

<p>Zamek brazylijskiej szkoły magii z wyglądu przypomina świątynie, wykonaną ze złotego kamienia na bazie kwadratu. Gmachu jak i otaczających go błoń strzegą małe i bardzo futrzaste istoty, zwane Caiporami. Są one bardzo złośliwe i sprytne, wyłaniają się pod osłoną nocy, by pilnować uczniów i stworzenia żyjące w najbliższych lasach. Z tymi stworzeniami związana jest pewną historią, kiedy to Hogwart odwiedziła była dyrektorka Castelobruxo – Benedita Dourado, a ówczesny dyrektor, Armando Dippet, żalił się kobiecie, na temat Irytka, poltergeista. Rozbawiona kobieta zasugerowała, że przyślę do Zakazanego Lasu kilka Caipor, aby dyrektor mógł przekonać się czym tak właściwie są prawdziwe kłopoty. Oczywiście propozycja nie została przyjęta, a śmiech Dourado podobno było słychać w całym Hogwarcie.</p>
<img class="img-fluid prawy2" src="../img/pod_castelo.png">
<p> Uczniowie brazylijskiej szkoły magii noszą jasnozielone szaty i są szczególnie zaawansowani w Zielarstwie i Magizoologii. Dodatkowo Castelobruxo oferuje program wymiany dla europejskich uczniów, którzy chcą skupić się na studiowaniu magicznej ﬂory  i fauny Ameryki Południowej. Bill Weasley brał udział w wymianie uczniów między Castelobruxo a Hogwartem, on miał jechać do Brazylii, a jego znajomy, którego poznał przez korespondencje, miał jechać do Hogwartu. Niestety wymiana nie doszła do skutku, ponieważ państwo Weasley mieli problemy ﬁnansowe, które z każdym rokiem narastały (wydatki na dzieci). Bill w takiej sytuacji musiał po prostu odmówić koledze z Brazylii, niestety ten nie przyjął tego zbyt dobrze i najprawdopodobniej się obraził, ponieważ niedługo potem wysłał Billowi zaczarowany kapelusz, a jak tylko ten go włożył, to uschły mu uszy.</p>

<div class="blok-cytat">– Oni chyba są z zagranicy – odpowiedział Ron. – Wiem, że jest kilka zagranicznych szkół, ale jeszcze nigdy nie spotkałem ucznia którejś z nich. Bill korespondował z kimś z Brazylii... dawno, dawno temu... i chciał pojechać tam na wymianę, ale rodzice nie mogli sobie na to pozwolić. Jego korespondencyjny kumpel obraził się i przysłał mu zaczarowany kapelusz, jak Bill go włożył, uschły mu uszy.
