<br>
<p>Zanim czarodziejska społeczność została zmuszona do ukrywania się, nie było niczym niezwykłym, by czarodziej żył w mugolskiej społeczności i utrzymywał to, co teraz uważalibyśmy za mugolską robotę.</p>

<p>Powszechnie uważa się w kręgach czarodziejów, że Sir Cadogan był jednym ze słynnych Rycerzy Okrągłego Stołu, choć mało znanym i osiągnął tę pozycję dzięki przyjaźni z Merlinem. Z pewnością został usunięty ze wszystkich mugolskich tomów opowieści Króla Artura, ale czarodziejskie wersje opowieści opisują Sir Cadogan wraz z Sir Lancelotem, Sir Bediverem i Sir Percivale. Te powieści ukazują go jako porywczego, zaciętego i odważnego aż do szaleństwa, choć dobrego człowieka odsuniętego do kąta.</p>

<p>Najsłynniejsze spotkanie Sir Cadogana odbyło się z wiwerną znad rzeki Wye, smokiem, który terroryzował Zachód. Podczas pierwszego spotkania bestia zjadła przystojnego rumaka sir Cadogana, złamała różdżkę na pół i stopiła jego miecz i przyłbicę. Nie mogąc zobaczyć, jak para unosi się z jego topniejącego hełmu, Sir Cadogan ledwo uszedł z życiem. Jednak zamiast uciekać, zatoczył się na pobliską łąkę, złapał małego, grubego kucyka pasącego się na nim, skoczył na niego i galopował z powrotem w kierunku wiwerny z tylko złamaną różdżką w dłoni, przygotowaną na mężną śmierć. Stwór opuścił swoją przerażającą głowę, by połknąć sir Cadogana i kucyka, ale odłamana i niewypadająca różdżka przebiła jego język, wywołując gazowe opary powstające z jego żołądka i powodując eksplozję wiwerny.</p>

<p>Starsze czarownice i czarodzieje nadal używają słowa "wezmę kucyka Cadogana" co może oznaczać "wybrnę najlepiej jak potrafię, z trudnej sytuacji".</p>

<p>Portret Sir Cadogana, który wisi na siódmym piętrze w zamku w Hogwarcie, pokazuje go wraz z kucykiem, na którym jeździł joż do końca swojego życia (który zbytnio nie przepadł za Sir Cadoganem) i dokładnie przedstawia jego gorący temperament, jego miłość do nierozważnego wyzwania i jego determinację do pokonaia wroga. </p>
