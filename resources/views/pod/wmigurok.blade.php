<h1> Wmigurok</h1>

<div class="blok-cytat">Czujesz, że nie dotrzymujesz kroku rozwojowi współczesnej magii? Zdarza ci się usprawiedliwiać, że nie wyszło ci najprostsze zaklęcie? Wykpiono twoje bezskuteczne wymachiwanie różdżką? Oto rozwiązanie twoich problemów! Wmigurok to zupełnie nowy, szybki, łatwy i absolutnie niezawodny sposób na poznanie współczesnej magii. Setki czarownic i czarodziejów skorzystało z dobrodziejstw tej nowej metody!
<p> Reklama Wmigurokowa</p></div><br>

  <img class="img-fluid lewy2" src="../img/pod_o_wmigurok.jpg">
<p>Wmigurok jest korespodencyjnym kursem dla poczatkujących czarodziejów oraz charłaków. Osobą, która odbywała ten kurs był Argus Filch, charłak pracujący na stanowisku woźnego w Szkole Magii i Czarodziejstwa w Hogwarcie.  </p>
<img class="img-fluid prawy2" src="../img/pod_wmigurok.jpg">
<p>Wszystko wskazuje na to, że Argus Filch probówał wykrzesić w sobie magiczną moc i nauczyć się jakichś prostych zakleć, mogło to być spowodowane tym, że pracując w szkole magii czuł się gorzej bo młodzi, czasem niedopowiedzialny uczniowie, nad którymi musiał czuwać, mieli więcej talentu magicznego niż on sam. Jego zachowanie w stosunku do uczniów mogło więc wynikać z tego, że był na nich w pewien sposób zły, że oni mogą posługiwać się magią, a on sam nie. Próbował więc różnych sposobów aby ten talent w sobie znaleźć, stąd jego zainteresowanie kursem takim jak Wmigurok. Niestety według tego co przekazywała nam autorka, mimo starań charłaków taki kurs nie jest im w stanie pomóc.  </p>

<p>O samym kursie dowiadujemy się w momencie kiedy to Harry Potter podpatrzył kopertę z Wmiguroku w kantorku woźnego, kiedy Filch zaprowadził go tam by wpisać go do swojej kartoteki za ubłocenie korytarza.  </p>

<p>Podobno z kursu zadowolonych było wiele słabszych czarodziejów, takich jak m.in. Madame Z. Nettles, która nie potrafiła zapamiętać zakleć, a z jej eliksirów śmiała się cała rodzina, czy Demetrius J. Prod, z którego czarów szydziła nawet żona. Według relacji wiemy, że kurs pomógł im osiągnąć pewien zadowolający poziom w posługiwaniu się magią, a rodzina madame Nettles czy żona pana Proda już więcej nie żartowała sobie z nich (Elsie Prod, żona Demetriusa została zamieniona przez męża w jaka, więc nie miała szans na szyderstwa). </p>


