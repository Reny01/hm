<h1> diffindo</h1>

<img class="img-fluid lewy2" src="../img/pod_diffindo.gif">
<p>Zaklęcie odcinające, a także przecinające i rozrywajace przedmioty zostało stworzone w XV wieku, przez czarodziejkę - Delfinę Crimp, która była również szwaczką. Potrzebowała ona prostego i wygodnego sposobu na odcinanie tkanin i nitek. Przed samym tym zaklęciem, czarodzieje jeśli chcielić coś przyciąć czy odciąć musieli to ręcznie odrywać lub nawet palić. Trzeba pamiętać, że wiek XV był jeszcze tym czasem kiedy czarodzieje byli prześladowani, kiedy nie tworzyli zwartych grup czy społeczeństw, żyli pomiędzy mugolami i musieli ukrywać swoje talenty.
Delfina Crimp była szwaczką, która wypromowała wiele trendów modowych w społeczności nie tylko czarodziejów, ale i mugoli, właśnie przez to starała się ukryć swoją moc magiczną ponieważ jej talent szwaczki wywoływał wiele zazdrości u innych osób zajmujących się podobną pracą. Suknie i płaszcze panny Crimp były bardzo popularne, mówiło się że nawet mniej urodziwe kobiety wyglądały w nich pięknie. Z każdym dniem sława wyrobów Delfiny rosła, podobnie jak i jej bogactwo.  </p>
<img class="img-fluid prawy2" src="../img/pod_diffindo2.gif">
<p>Największym rywalem kobiety był mugol - Snickerton, który za wszelką cenę chciał poznać jej tajemnicę. Zgłosił się więc on pod przebraniem do pracy w jej zakładzie. Mimo jednak starań nie udało mu się znaleźć dowodów na użycie magii czy innych technik, które mogłyby pomóc w tworzeniu takich strojów. Zauważył on jednak, że w zakładzie Delfiny nie ma żadnej pary nożyczek. W końcu jednak, nie tracąc nadziei, owinął się on w belę aksamitu i zobaczył w nocy jak madame Crimp wycina skomplikowany wzór za pomocą różdżki. Następnego dnia, chcąć ujawnić wiedźmę, zebrał on gromadę ludzi i udał się do jej zakładu. Złapali oni kobietę, która nie potrafiła się wytłumaczyć dlaczego nie posiada nożyczek, a sam Snickerton dodatkowo nalegał aby związać ręce kobiety by ta nie mogła rzucić na nich żadnej klątwy. Mugol udał się w poszukiwaniu różdżki, jednak nie udało mu się jej znaleźć nawet po kilku godzinach poszukiwań. W pewnym momencie madame Crimp poprosiła o rozwiązanie rąk, aby mogła chociaż podrapać się po podbródku, na którym wyrosła spora krosta. Mugole, którzy nie widzieli w tym żadnego ukrytego planu, zgodzili się. W momencie gdy kobieta dotknęła krosty, wyskoczyła z niej ukryta różdżką, dzięki której czarownica błyskawicznie deportowała siebie i worek złota. Nigdy więcej nie widziano Delfiny Crimp w Londynie. Od tego też momentu Snickerton nie był w stanie uszyć żadnego ubrania ponieważ zawsze rano kiedy się budził, w jego pracowni każde ubranie było pocięte na kawałki. Pogłoski mówią również o tym, że oszalał i zmarł w przeświadczeniu, że wszędzie gdzie się ruszył, za nim ganiały go niewidzialna para nożyczek. </p>
