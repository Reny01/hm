<h1> ALOHOMORA</h1>

<img class="img-fluid lewy2" src="../img/pod_alohomora.gif">
<p>W świecie czarodziejów istnieje wiele sposobów na przejście przez zamknięte drzwi, wszystko sprowadza się jednak do pomysłowości czarodzieja jak przez te drzwi chce przejść, czy bardziej dyskretnie czy głośno i 'z trzaskiem'. Zanim odkryto zaklęcie Alohomora, najpopularniejszym czarem służącym do przejścia przez zamknięte drzwi było zaklęcie "Portaberto!", które zwykle wyrywało zamek razem z drzwiami, a w miejscu gdzie powinien znajdować się zamek często zostawiało dymiącą dziurę. Było to jednak i tak lepsze zaklęcie niż "Sezamie, otwórz się!", które również było popularne przed powstaniem Alohomory.
Samo zaklęcie otwierające jest nazywane przyjacielem włamywaczy, ponieważ każdy szanujący się zbir jeśli chce włamać się po cichu do jakiegoś pomieszczenia, musi znać ten czar.</p>
