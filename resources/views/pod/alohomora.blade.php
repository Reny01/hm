<h1> HISTORIA ZAKLĘCIA</h1>
<p>Zaklęcie Alohomora zostało najprawdopodobniej wynalezione przez starego, afrykańskiego czarodzieja, nie wiadomo jednak jak się ów czarodziej nazywał ani kiedy dokładnie to zaklęcie powstało. W Wielkiej Brytanii pojawiło się w XVII wieku dzięki włamywaczowi - Eldonowi Elsrickle'owi, który uciekł do Afrykii usiłując uniknąć kary za włamania, których dokonał. Po pewnym czasie powrócił do Wielkiej Brytanii z większą pewnością siebie, ponieważ miał ze sobą dwa skarby. Pierwszym z nich było niebezpieczne stworzenie - Nundu, które było jeszcze na tyle młode aby można było je kontrolować, natomiast drugim skarbem była znajomość zaklęcia otwierającego, które nazywano również 'przyjacielem złodziei'. </p>
<img class="img-fluid prawy2" src="../img/pod_alohomora2.gif">
<p>Eldon był na tyle pewny siebie, że powrócił na przestępczą ścieżkę, z której poprzednio uciekł. W tym momencie miał w domu Nundu, które broniło jego posiadłości oraz skarbów, które udawało mu się zdobyć po cichu i bez zostawiania żadnego śladu dzięki zaklęciu Alohomora. Złodziej od razu trafił na główne strony gazet - zarówno tych mugolskich jak i czarodziejskich, bowiem okradał każdego komu było można coś zabrać. Ludzie zaczęli go nazywać "Złodziejem - zjawą ze starego Londynu". </p>
<p>Eldon kiedy przebywał w swoim domu, usypiał Nundu tak że mógł bezpiecznie w nim przebywać, kiedy wychodził "do swojej pracy", budził stworzenie, które teraz nie pozwalało nikomu wejść do posiadłości. Czasem zdarzało się, że czarodzieje którzy byli okradani przez włamywacza przyłapywali go na gorącym uczynku, ten jednak wpadł na pomysł aby dzielić się wiedza na temat zaklęcia otwierającego z tymi, którzy go złapali. I większość czarodziejów szła na taki układ. Wkrótce wiedza o zaklęciu rozeszła się po całym Londynie, a po mieście zaczęła grasować szajka włamywaczy określona mianem "Widmowych złodziei". Wszystko ułożyłoby się dla Eldona całkiem dobrze, gdyby nie Blagdon Blay, który został okradziony aż 19 razy w przeciągu dwóch tygodni. Stworzył on zaklęcie antyotwierające i podzielił się nim z resztą czarodziejów, którzy również byli okradani. I tak, pewnej nocy Eldonowi nie udało się dostać do żadnego z domów, zmęczony wrócił do swojego domu, zapominając aby wcześniej uśpić stworzenie broniące posiadłości. Ostatnim słowem wypowiedzianym przez czarodzieja zanim został zjedzony przez Nundu podobno było "Alohomora". </p>


