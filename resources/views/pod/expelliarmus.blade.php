<h1>Expelliarmus</h1>

<img class="img-fluid lewy" src="../img/pod_exp.gif">
<p>Według Mirandy Goshawk prawdopodobnym twórcą zaklęcia rozbrającego była młoda czarownica Elizabeth Smudgling, która pierwszy raz zaprezentowała czar podczas turnieju w Dartmoor w 1379 roku. </p>
<p>Turniej Pojedynków w Dartmoor w hrabstwie Devon w Anglii odbył się w nocy, zapewne dlatego aby nie zwracać uwagi mugoli. Sam turniej cieszył się dużą popularnością, a pojedynkujący czarodzieje używali magii na wysokim poziomie. W turnieju tym do ostatecznej rozgrywki miało dojść pomiędzy czterema uczestnikami, jednym z nich była panna Smudgling. Sędziowie turnieju, do których należeli czarodzieje z różnych narodów, przemówili do półfinalistów turnieju mówiąc im: <i> „Myślcie o rzeczach wielkiego kalibru! Większego, niż można wymarzyć! Dzisiejszej nocy pokażcie najwspanialszą magię. Niechaj przyszłe pokolenia pozazdroszczą, że mogliśmy być jej świadkami!”. </i></p>
<p>Podczas pierwszego starcia jeden z czarodziejów zamienił się w byka, drugi mając nadal trzeźwy umysł i nie bojąc się szarżującego zwierzęcia, zamienił się w żmiję, która ukąsiła byka. Czarodziej odniósł sukces i wygrał ten pojedynek. </p>
<p>W kolejnym starciu, drugi z czarodziejów biorących udział w półfinałach (ten, który wcześniej zamienił się w żmiję) wyczarował chmurę burzową, która zalała przeciwnika wodą i błyskawicami. Trzeci czarodziej natychmiast odpowiedział, wyczarował on potężną trabę powietrzną, która zmiotła burzę, jego przeciwnika ale również większość widzów i sędziów - tak czy siak, doprowadziło to do zwycięstwa trzeciego zawodnika. </p>
<img class="img-fluid prawy2" src="../img/pod_exp2.gif">
<p>W turnieju pozostał ostatni pojedynek pomiędzy Elizabeth Smudgling oraz trzecim zawodnikiem, zanim jednak do niego doszło, wielu sędziów i widzów musiało powrócić po poprzednim starciu. W tym czasie sporo osób próbowało nakłonić Elizabeth aby zrezygnowała i nie walczyła ze swoim przeciwnikiem, lepiej bowiem dać mu już ten puchar niż ryzykować, że więcej osób ucierpi od samego patrzenia na pojedynek. Kobieta jednak odmówiła i postanowiła, że weźmie udział w ostatecznej walce. Pojedynek rozpoczął się, czarodziej zaczął wypowiadać długą i skomplikowaną inkantację, zanim jednak zdążył dokończyć czar, kobieta wypowiedziała cicho zaklęcie rozbrajające - Expelliarmus i pozbawiła przeciwnika różdżki. Gdyby na czas nie zareagowała, z pewnością przegrałaby ponieważ czar trzeciego zawodnika miał przywołać potężną górę, która przygniotłaby kobietę. Elizabeth Smudgling otrzymała tytuł Mistrza Pojedynków, natomiast samo zaklęcie rozbrajające stało się najbardziej powszechnym i najważniejszym zaklęciem pojedynkujących się czarodziejów. </p>
<p>Wielu historyków sprzecza się na temat powstania tego zaklęcia, pierwszą znaną i udokumentowaną historią jest ta przedstawiona wyżej, niektórzy jednak stworzenie zaklęcia Expelliarmus przypisują Merlinowi. </p>
