<h1> CZARODZIEJSKA AKADEMIA TEATRALNA</h1>

<p>Jest placówką działającą w Świecie Czarodziejów, która uczy chętnych magów pracy aktora i rozwoju ich artystycznego potencjału. Można przypuszczać, że została założona po roku 1928 ponieważ właśnie wtedy do Hogwartu dostała się Celestyna Warbeck, której matka pozwoliła tam iść tylko dlatego, że nie ma innej szkoły, która skupiła by się na rozwoju artystycznego młodych czarodziejów. </p>

<p>Wiadomo jednak, że jednym z wykładowców w Czarodziejskiej Akademii Teatralnej był profesor Herbert Beery, który zrezygnował z nauczania zielarstwa w Szkole Magii i Czarodziejstwa w Hogwarcie właśnie na rzecz pracy wykładowcy. Uczył on wtedy gdy szkołą zarządzał Armando Dippet, a nauczycielem transmutacji był Albus Dumbledore. </p>
<img class="img-fluid prawy2" src="../img/pod_wada.jpg">
<p>Wizarding Academy of Dramatic Arts może mieć odniesie do The Royal Academy of Dramatic Arts, która znajdowała się w Londynie. Można więc przypuszczać, że Czarodziejska Akademia Teatralna również znajduje się gdzieś na terenie Wielkiej Brytanii.  </p>

<p>Czworo absolwentów WADA: Beckett, Wirginia, Samuel i Darby, pod koniec 1990 roku wystawiło na Carkitt Market w Londynie lalkarską wersję dwóch opowieści zawartych w Baśniach Barda Beedle'a, tj. Opowieść o trzech braciach oraz Fontannę Szczęśliwego Losu.</p>



