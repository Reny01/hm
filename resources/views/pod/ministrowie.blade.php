<div class="col-5 page-hm4 offset-4">
  <h2>   Maximilian Crowdy</h2>
  <span class="important"> #8 (1770 - 1781)</span>
</div>
</div><Br>
  <p>Został wybrany na Ministra Magii w roku 1770 i był na ten urząd wybrany jeszcze przynajmniej jeszcze raz.  Powszechnie uważany za bardzo charyzmatycznego lidera, ojca dziewięciorga dzieci, który zdołał rozbić kilka ekstremistycznych grup czarodziejów propagujących czystość krwi oraz dominację czarodziejów nad mugolami. Minister Crowdy zapobiegł tym samym kilka ataków na mugoli. Zmarł w roku 1781, a jego tajemnicza śmierć stała się tematem wielu książek i teorii spiskowych.     </p>
</div>
<div class="col-6 page-hm">
<div class="row text-center">
<div class="col-5 page-hm4 offset-4">
  <h2>  Porteus Knatchbull</h2>
  <span class="important"> #9 (1781 - 1789)</span>
</div>
</div><Br>
  <p> Wybrany na Ministra Magii w roku 1781, pełnił ten urząd do roku 1789. W roku 1782 został poproszony przez ówczesnego mugolskiego premiera – Fredericka Northa, aby ten sprawdził czy byłby wstanie pomóc wobec niestabilności umysłowej króla Jerzego III. Wszystko miało pozostać między nimi, jednak informacje o tym, że Lord North wierzy w czarodziejów doprowadziły do jego usunięcia z pozycji mugolskiego premiera.</p>
</div>
</div>



<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2>   Unctuous Osbert </h2>
  <span class="important"> #10 (1789 - 1798)</span>
</div>
</div><Br>
  <p> Został wybrany na Ministra Magii w roku 1789, uczęszczał on do Hogwartu choć jego dom do którego należał nie jest znany. Większość społeczeństwa czarodziejów podejrzewa, że największy wpływ na jego wybory i decyzje miał Septimus Malfoy, członek bardzo potężnej i bogatej rodziny Malfoy. W okresie, w którym trwała jego kadencja, Septimus był inwestorem i miał ogromny wpływ na poczynania Ministerstwa, czarodzieje uważają więc, że sam Osbert był niczym więcej jak tylko marionetką w rękach Malfoya. W roku 1798 zrezygnował on z rządów, odchodząc.</p>
</div>
</div>




<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2> Artemisia Lufkin</h2>
  <span class="important"> #11 (1798 - 1811)</span>
</div>
</div><Br>
    <img class="img-fluid lewy2" src="../img/mm/mm_11.jpg">
  <p> Została wybrana na Ministra Magii w roku 1798, była pierwszą kobietą, która pełniła funkcję ministra, została przynajmniej raz wybrana ponownie na ten urząd. Urodziła się w roku 1754, w rodzinie nieznanego statusu krwi (choć musiała mieć przynajmniej jednego rodzica czarodzieja). Uczęszczała do Szkoły Magii i Czarodziejstwa w Hogwarcie w latach 1765 – 1772, gdzie należała do domu Hufflepuff. </p>
  <p>Wiadomo, że wybór kobiety na Ministra Magii wywołał oburzenie w społeczności czarodziejów, a szczególnie u osób wysoko-postawionych. Niektórzy członkowie Wizengamotu, wystąpili ze swoich pozycji, w ramach sprzeciwu i protestu przeciwko jej wyborowi. Była Ministrem przez 13 lat, ustępując w 1811 roku Groganowi Stumpowi. Założyła Departament Międzynarodowej Współpracy Czarodziejów, w którym ciężko pracowała nad tym aby jedna z edycji Mistrzostw Świata w Quidditchu odbyła się w Anglii podczas jej urzędowania. </p>
  <p>Zmarła w roku 1825, mając 71 lat. W latach 90. XX wieku jej podobizna znalazła się na Kartach z Czekoladowymi Żabami. W 1991 roku prefekt domu Hufflepuff – Gabriel Truman – wygłosił pierwszorocznym krótką mowę zachwalając najwybitniejszych członków domu Helgi, a tym samym wymieniając wśród tych osób Artemizję. </p>
