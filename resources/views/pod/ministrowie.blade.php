<div class="col-4 page-hm4 offset-4">
  <h2>Ulick Gamp</h2>
  <span class="important"> #1 (1707 - 1718)</span>
</div>

</div><Br>
<img class="img-fluid lewy2" src="../img/mm/mm_1.png">
  <p>Miał trudne zadanie przystosowania społeczność Świata Czarodziejów do warunków i ustaleń Międzynarodowego Kodeksu Tajności Czarów. Czarodzieje musieli przyjąć nowe warunki, które zostały im narzucone, a przystosowanie i kontrolowanie tego wymagało od Ministra silnej ręki, ale i pomysłowości, w końcu magowie należeli do społeczności krnąbrnych, śmiałych ludzi o skłonności do bardzo dziwnych zachowań. Potrafili robić niesamowite rzeczy, a czasem i bardzo głupie, jedynie dla swej rozrywki. Gamp miał więc bardzo trudne zadania do wykonania. Nie można powiedzieć, że nie zadanie to nie zostało wykonane, to dzięki niemu powstał Departament Przestrzegania Prawa Czarodziejów, z którego sam minister był bardzo dumny, jako wcześniejsza głowa Wizengamotu. Nałożył wiele zaklęć ochronnych oraz przepisów na Dziurawy Kocioł oraz jego klientele, dopiero po pewnym czasie przekazał on odpowiedzialność za ochronę oraz wpuszczanie czarodziejów na Ulicę Pokątną ówczesnemu właścicielowi pubu, a ten w podzięce stworzył nową markę piwa – Starego Towarzyskiego Gampa, które jest tak obrzydliwe, że do tej pory nie znalazł się nikt, kto byłby wstanie wypić to piwo do końca. Jego portrety wiszą w Ministerstwie Magii oraz u premiera mugoli, jest to bowiem pewien sposób na komunikacje, a portret Gampa sprawuje funkcje posłańca między nimi.</p>
  <p>Ulick najprawdopodobniej był czystej krwi lub nawet półkrwi (choć bardziej czystej, ze względu na zajmowane stanowiska), a to ze względu na fakt, że pierwszym ministrem magii, który był mugolakiem, był Nobby Leach wybrany prawie 250 lat po Gampie. Gampa opisywano jako małego, podobnego do żaby człowieka, który zazwyczaj nosił purpurowe szaty i kręcone, srebrne peruki. </p>
</div>
</div>


<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2>Damocles Rowle</h2>
  <span class="important"> #2 (1718 - 1726)</span>
</div>
</div><Br>
<img class="img-fluid prawy" src="../img/mm/mm_2.jpg">
  <p>Po wprowadzenie Kodeku Tajności Czarodziejów, pracowano nad wybudowaniem lub stworzeniem jednego więzienia, które byłoby oddalone od mugoli na tyle, by uniknąć zauważenia czy odkrycia. Dodatkowo, miało to być ułatwienie zarówno dla nowopowstałej instytucji rządzącej, która w łatwiejszy sposób mogła zarządzać jednym więzieniem, a nie kilkoma rozsianymi po całym kraju, jak i dla społeczności czarodziejów, którzy nie musieli się obawiać mieszkania przy niebezpiecznych osobnikach. Początkowo planowano stworzenie więzienia na Hybrydach, plany te zostały jednak zmienione przez Domoclesa Rowle, który został Ministrem Magii w 1718 roku. Został wybrany na ministra z powodu opinii przeciwnika mugoli i twardej ręki w polityce przeciwko nim. Ostatecznie to on dokonał zmian i wybrano Azkaban, jako miejsce do którego mieli napływać kryminaliści z całego kraju. Część czarodziejów protestowała przeciwko wybraniu tej twierdzy na więzienie, sugerując, że ma ona już swoją historię, w dodatku mroczną, a jej mieszkańcami są dementorzy, stworzenia bardzo niebezpieczne, które ciężko kontrolować. Rowle oczywiście zrobił tak, jak od początku planował, dodatkowo mówił, że de mentorzy mogą stanowić tanią, siłę roboczą, która zaoszczędzi Minsiterstwu dodatkowych kosztów, wysiłku i kłopotów. I tak, Domocles Rowle ustanowił Azkaban więzieniem, do którego początkowo zaczęły trafiać osoby, które nie popełniły strasznych zbrodni, a niewielkie wykroczenia. Nie miało to jednak znaczenia, bowiem dla dementorów, którzy pilnowali więźniów, stanowili tylko dodatkowy pokarm, a ministerstwo miało ich z głowy. Rowle został wybrany na ministra co najmniej dwa razy, jednak jego narastająca polityka antymugolska doprowadziła do tego, że nawet Międzynarodowa Konfederacja Czarodziejów musiała interweniować, a część z jego działań zostały ocenzurowane. W 1726 roku, Rowle został usunięty z posady Ministra Magii. </p>
</div>
</div>

<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2> Perseus Parkinson</h2>
  <span class="important"> #3 (1726 - 1733)</span>
</div>
</div><Br>
  <p> Podobnie jak jego poprzednik, tak i Parkinson prowadził propagandę antymugolską. Domagał się aby wprowadzić oficjalny zakaz małżeństw czarodziejów z mugolami, próbował nawet zatwierdzić ustawę, która tego by zabraniała. Społeczeństwie czarodziejów zależało jednak na utrzymaniu dobrych stosunków z mugolami, a zatwierdzenie takiej ustawy nasiliłyby działania antymugolskie, co mogło naruszyć trwający pokój. Dlatego przy najbliżej okazji, zmęczeni szerzeniem nienawiści i niechęci do mugolów, czarodzieje przegłosowali Parkinsona, a jego kariera jako Ministra Magii zakończyła się w 1733 roku. Zgodnie ze Skorowidzem Czystości Krwi, wydanym na początku lat 30. XX wieku, nazwisko Parkinson znajduje się na tej liście, a skoro 'praprzodek' żył w tamtym czasie, z całą pewnością był czystej krwi. </p>
</div>
</div>



<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2>  Eldritch Diggory</h2>
  <span class="important"> #4 (1733 - 1747)</span>
</div>
</div><Br>
  <p> Został wybrany na Ministra Magii w roku 1733 i był ponownie wybrany przynajmniej raz, w przeciwieństwie do swoich poprzedników był poważanym i lubianym ministrem. Był jednym z czarodziejów, którzy nie popierali pomysłu zrobienia z Azkabanu więzienia, dlatego podczas swojego urzędowania, postanowił odwiedzić Azkaban i sprawdzić jak forteca radzi sobei, jako więzienie. To co tam zobaczył przerosło jego ‘oczekiwania’. Więźniowie co prawda nie uciekali, ale tylko dlatego, że w większości zamieniali się w szaleńców niezdolnych do racjonalnego myślenia, obok fortecy stworzono nawet specjalny cmentarz dla nich, ponieważ liczba zgonów samych skazańców przerosła wszelkie wyliczenia. Diggory nie był tym zachwycony, stworzył on nawet specjalny komitet, który miał za zadanie znaleźć alternatywne rozwiązanie dla Azkabanu. Zanim jednak zdołał cokolwiek zdziałać, w roku 1747 zmarł na smocza ospę, a komitet który niedawno założył, rozpadł się. Był uznawany za jednego z najwybitniejszych Ministrów Magii w historii, to właśnie on ustanowił jako pierwszy program rekrutacyjny na stanowisko Aurora. </p>
  <p>Nie wiadomo dokładnie jakiego statusu krwi był, wiadomo jednak, jak w przypadku Gampa, że musiał być czystej krwi lub półkrwi. W wieku 11 lat nabył on różdżkę w sklepie Ollivanderów, wykonaną z topoli. Sam Diggory z różdżką stanowi argument w obaleniu mitu, jakoby różdżki z topoli nigdy nie wybierali polityków na swoich właścicieli. </p>
</div>
</div>




<div class="row">
<div class="col-6 page-hm">
<div class="row text-center">
<div class="col-5 page-hm4 offset-4">
  <h2>   Albert Boot</h2>
  <span class="important"> #5 (1747 - 1752)</span>
</div>
</div><Br>
  <p>Został wybrany na Ministra Magii w 1747 roku, był też pierwszym ministrem wybranym przez ludność Wielkiej Brytanii i Irlandii po śmierci Eldritcha Diggory. Przez większość uważany za sympatycznego, ale momentami nieudolnego Ministra Magii. Podczas trwania buntów goblinów, nie był w stanie poradzić sobie z narastającymi problemami i dlatego zaraz po tym, w roku 1752 zrezygnował z pełnienia funkcji ministra.  </p>
</div>
<div class="col-6 page-hm">
<div class="row text-center">
<div class="col-5 page-hm4 offset-4">
  <h2> Basil Flack</h2>
  <span class="important"> #6 (1752 - 1752)</span>
</div>
</div><Br>
  <p>Po zrezygnowaniu z pełnienia funkcji ministra przez Boota, nowym Ministrem Magii w roku 1752 został Basil Flack. Uważa się, że mógł zostać wybrany jako tymczasowy minister ze względu na narastający konflikt i problemy związane z buntami goblinów. Niestety jego kadencja nie trwała długa, Flack zrezygnował z pełnienia urzędu zaraz po tym, jak dowiedział się, że wilkołaki dołączyły do powstań goblinów. Do roku 2014 był najkrócej urzędującym Ministrem Magii.</p>
</div>
</div>



<div class="row">
<div class="col-12 page-hm">
<div class="row text-center">
<div class="col-4 page-hm4 offset-4">
  <h2>   Hesphaestus Gore </h2>
  <span class="important"> #7 (1752 - 1770)</span>
</div>
</div><Br>
  <p> Był jednym z pierwszych czarodziejów, którzy zostali aurorami po ustanowieniu programu rekrutacyjnego na to stanowisko przez ministra Diggory’ego. Został wybrany na tymczasowego Ministra Magii w roku 1752, a później został wybrany już na stałe, na to stanowisko, i był wybrany ponownie przynajmniej raz. W przeciwieństwie do swojego poprzednika, udało mu się opanować i udaremnić wybuch kilku powstań magicznych stworzeń (już nie tylko goblinów, bowiem wilkołaki dołączyły do nich). Historycy są jednak niezgodni w przypisaniu mu całkowitego sukcesu nad buntami, niektórzy uważają, że jego odmowa w rozważeniu ustanowienia programu rehabilitacyjnego dla wilkołaków spowodowała wzrost liczby ataków na czarodziejów.  </p>
  <img class="img-fluid lewy" src="../img/mm/mm_7.gif">
  <p>W roku 1754, Gore wraz z Włoskim Ministrem Magii mieli sędziować niesławny, miotlarski wyścig z Aberdeen do Rzymu, pomiędzy najbardziej znanymi ‘lotnikami’ w Europie, Torquil McTavish i Silvio Astolfi. Zebrano się w Rzymie, w Koloseum, gdzie znajdowała się meta wyścigu, wszelkie środki ostrożności zostały zachowana, nawet sam wyścig został zorganizowany w nocy, tak aby nie zakłócić życia mugoli i nie zostać przez nich odkrytym. Ministrowie, wraz z trybunami fanów czekali na przybycie ścigających się rywali, zanim Ci jednak się zjawili, między fanami obu stron wybuchły konflikty, które w szybkim czasie doprowadziły do zniszczenia Koloseum. Na pomoc przerażonych ministrów wyszła Orabella Nuttley, pracownica Urzędu Niewłaściwego Użycia Czarów, której zadaniem podczas wyścigu było przechowywanie oraz odpowiednie zorganizowanie świstoklików. Orabella wyszła naprzeciw ministrów (w dosłownym tego słowa znaczeniu) i zaczęła naprawiać kolumny poprzez zaklęcie Reparo, które sama wynalazła. Zdumieni ministrowie oraz ich współpracownicy poprosili aby Orabella nauczyła ich tego zaklęcia. Wkrótce potem Koloseum wróciła do swojego poprzedniego stanu, a czarodziejom udało się uniknąć odkrycia przez mugoli, którzy niedługo potem zjawili się na miejscu zbudzeni hałasem. Wdzięczny Hesphaestus nagrodził Orabelle Nuttley Orderem Merlina Pierwszej Klasy, za zapobiegnięcie złamania najważniejszej zasady Międzynarodowego Kodeksu Tajności Czarów. Sam Gore był również odpowiedzialny za remont oraz wzmocnienie więzienia Azkaban, a tym samym nie poszedł w ślady Diggory’ego, który chciał znaleźć alternatywę dla twierdzy. W roku 1770 przeszedł na emeryturę albo został zastąpiony poprzez głosowanie, pełnił urząd Ministra Magii przez 18 lat.</p>
</div>
