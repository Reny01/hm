<h1> Koldovstoretz</h1>
  <img class="img-fluid lewy2" src="../img/pod_kol.png">
<p>Jest jedną z jedenastu Szkół Magii funkcjonujących na świecie, mieści sie w Rosji. O tej placówce dowiadujemy się z Księgi Eliksirów, w której jest mowa o jednym z jej uczniów, który brał udział w Międzyszkolnych Mistrzostwach Eliksirów (Wizarding School Potions).</p>

<p>Sama jej nazwa, składa się z dwóch rosyjskich słów: ‘колдовство’ oznaczające czary, oraz ‘творец’ oznaczające kreatora, twórcę. Dosłowne tłumaczenie nazwy szkoły brzmi więc ‘Twórca Czarów’ lub ‘Kreator Czarów’. </p>

