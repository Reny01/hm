<h1> Ordery Merlina</h1>
  <img class="img-fluid lewy" src="../img/pod_ordery.jpg">
<p>Order Merlina (czasem nazywany w skrócie OM) jest najważniejszym odznaczeniem czarodziejskim, przyznawanym od XV wieku. Jego nazwa pochodzi od Merlina, powszechnie uważanego za najsłynniejszego brytyjskiego czarodzieja wszech czasów. Również kolor wstążki, na której wisi Order Pierwszej Klasy, ma upamiętniać tego maga, odnosi się on bowiem do koloru domu w Hogwarcie, do którego należał Merlin.</p>
<p> Nagrody są przyznawane przez Wizengamot, który funkcjonował jeszcze przed powstaniem samego Ministerstwa Magii. Order Pierwszej Klasy przyznawany jest za magiczne czyny wymagające wybitnej odwagi lub dystrakcji, stanowi on medal na zielonej wstążce. Order Drugiej Klasy przyznawany jest za ponadprzeciętne osiągnięcia lub wysiłki wychodzące ponad normę, stanowi medal na purpurowej wstążce. Order Trzeciej Klasy przyznawany jest za wkład do zasobu wiedzy czarodziejów lub ich rozrywki, stanowi medal na białej wstążce.</p>
<p> Jeśli chodzi o Ordery Merlina, często społeczeństwo czarodziejów uważało, że osoby, które otrzymują tę nagrodę, a szczególnie Order Merlina Pierwszej Klasy, są zazwyczaj ulubieńcami Ministerstwa Magii. Nikt nie miał nic przeciwko gdy ten order otrzymał Albus Dumbledore za pokonanie Grindelwalda, a tym samym zatrzymanie wybuchu potencjalnej wojny czarodziejów, do której Gellert miał doprowadzić, jednak gdy Korneliusz Knot, ówczesny Minister Magii sam siebie nagrodził tą nagrodą za karierę, wielu czarodziejów było tym faktem bardzo oburzonych. Podobnie było w przypadku Arcturusa Blacka, dziadka Syriusza, który również otrzymał Order Merlina Pierwszej Klasy i który to, jak powszechnie wiadomo, rzekomo go sobie kupił, pożyczając Ministerstwu Magii sporo złota.<p>
