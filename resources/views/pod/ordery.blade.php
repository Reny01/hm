<p> W momencie gdy jego wiarygodność została podważona (gdy twierdził, że Czarny Pan powrócił), Ministerstwo Magii chciało odebrać mu order. Dumbledore się tym jednak nie przejął, a żartobliwie dodał, że nie martwi się tym dopóki nie wycofają jego wizerunku z kart z Czekoladowymi Żabami.</p>
