<h1> WPROWADZENIE DO PREHISTORII</h1>
<div class="blok-cytat"><center>Prehistoria to początkowy okres dziejów ludzkości do mniej więcej 3300 p.n.e., <Br>to znaczy do momentu, w którym przypuszczalnie wynaleziono pismo. <Br></center><br></div><Br>

<p>Nie jest to okres w historii, w którym dowiadujemy się o bardzo znaczących wydarzeniach, nie ma też jednoznacznych źródeł, które mogłyby zaświadczyć o tym co wtedy się działo. Kości, ułożenie głazów w szczególny sposób, figury kamienne, malowidła naskalne, narzędzia czy inne pozostałości w żaden sposób nie mówią o dokładnych dziejach magów czy samej magii z tego okresu. </p>

<img class="img-fluid lewy" src="../img/prehistoria.jpg">
<p>Dlaczego więc zadajemy sobie trud i poświęcamy tym czasom lekcje?</p>
<p>Wszystko ma swój początek, wszystko się kiedyś zaczyna i tak musiało być również z magią i czarodziejem, gdzieś i kiedyś ktoś po raz pierwszy posłużył się magią, zauważył jej istnieje. Tak samo jak pierwsi ludzie czy pierwsze kultury, one też się rozwijały i gdzieś rozpoczynały swoje egzystowanie. I choć mówiłem w rozdziale ‘Pochodzenie magii’ o kontekście rozwoju samej magii i pochodzenia tego słowa, uważam, że również i w prehistorii można doszukiwać się zalążków występowania czarodziejów. Poświęcam tym czasom lekcje, ponieważ uważam, że trzeba mieć ogólny zarys tego od kiedy zaczyna się nasza historia oraz o początkach istnienia magii właśnie w takiej formie. Trzeba zdać sobie sprawę, że magia jest czymś co otacza nas dookoła, jest w nas i to my nadajemy jej odpowiedni kierunek, jest więc czymś bardzo trudnym do opisania, czymś co było z nami zawsze. Właśnie prehistoria może w pewien sposób dać uczniom do zrozumienia, że magia była z nami od zawsze. </p>

<p>Prawdą jest i to również trzeba mieć na uwadze, że potwierdzonych informacji z tego okresu nie ma. Pojawią się źródła, pojawiają się przedmioty które mogą zaświadczyć o tym co wtedy się działo, nie ma jednak nigdzie powiedziane, że tak było na pewno. Dlatego przytaczane w tym rozdziale legendy, są tylko i wyłącznie legendami, opowieściami, które mogą ale nie muszą zawierać ziarno prawdy. Chodzi jednak o zobrazowania procesu jaki mógł nastąpić i dobrą zabawę, bo legendy są tematem bardzo podatnym pod manipulacje i można tak je rozwinąć by na lekcji zostały przedstawiony w bardzo ciekawy sposób. </p>

<p>Wczasach najdawniejszych mają swój początek wszelakie wierzenia religijne, wszelkie kultury i tradycje. Pozostałości po dawnych ludziach wskazują na stosowanie rytuałów, a więc świadczą o istnieniu magii. Nie są to jednak wiarygodne źródła i o tym również trzeba pamiętać. </p>

<p>Wtedy magia wyglądała troszkę inaczej, była integralną częścią życia człowieka. Była powszechnie akceptowana i zdawano sobie sprawę z jej istnienia. Nie trzeba było tego zjawiska ukrywać, tak jak to dzieje się od średniowiecza do teraz. Działo się tak najprawdopodobniej dlatego, że ludzie nie zdawali sobie sprawy z potęgi magii i ci, którzy się nią nie posługiwali nie mieli powodu, by się jej bać.</p>

