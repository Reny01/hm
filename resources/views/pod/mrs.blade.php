<h1> MAGIA RELIGIJNA</h1>
<img class="img-fluid prawy" src="../img/pod_mrel.gif">
<p>Jest jedną z najstarszych dziedzin magii. Powstała już w czasach starożytnych. Była też początkiem praktykowania i rozpowszechniania magii, mimo że wielu z ‘normalnych’ czyli krótko mówiąc niemagicznych ludzi nie zdawało sobie z tego sprawy. </p>


<p>Religia była sposobnością dla czarodziejów do życia w dostatku i rozwoju swoich własnych umiejętności, ponadto mieli władze nad mugolami, którzy wierzyli w przeróżne bóstwa i silne duchy. Ten rodzaj magii nie zmienił się dużo na przestrzeni wieków. Przez stulecia magia religijna zanikała, odgrywając coraz to mniejszą role. Ostatecznie zanikła w IX wieku. Współcześnie niewiele istnieje zapisanych praktyk związanych z magią religijną, ma ona jednak powiązania z wszelkimi ofiarami i rytuałami, a przede wszystkim jest związana z magią krwi.</p>

