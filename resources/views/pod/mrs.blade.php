<h1> MAGIA ŚWIECKA</h1>
<img class="img-fluid lewy" src="../img/pod_mswie.gif">
<p>Jest przykładem magii twórczej, jej największy rozwój został osiągnięty w średniowieczu. Głównym powodem jej istnienia jest fakt, że rosła liczba ludzi obdarzonych magicznymi mocami. Kapłanów było coraz więcej, a zapotrzebowania na magiczne zdolności w magii religijnej coraz bardziej malały. </p>

<p>Czarodzieje zaczęli więc sami eksperymentować z czarami i powoli ujawniali się, nie jako kapłani którzy swoją moc rzekomo czerpali od bogów i silnych duchów, ale jako ludzie którzy tacy się urodzili. Mugole, którzy nie rozumieli magii oraz nie potrafili sobie wytłumaczyć dlaczego czarodzieje potrafią sami z siebie robić tego typu rzeczy – przestraszyli się. Wcześniej posiadali wytłumaczenie – to moc zesłana od bogów, teraz było to dla nich niepojęte. Postanowili zabijać czarodziejów i czarownice, bardzo popularnym sposobem okazało się wtedy palenie na stosie – widowiskowe i bolesne.</p>


<p>Niestety wiele niewinnych osób, które nie posiadały żadnych umiejętności magicznych zostało spalonych. To właśnie ten okres zapoczątkował nienawiść do osób niemagicznych oraz popularne do dziś, jednak obraźliwe określenie szlamy. Za czasów magii świeckiej rozwinęły się takie dziedziny jak alchemia, medykomagia i magia praktyczna.</p>



