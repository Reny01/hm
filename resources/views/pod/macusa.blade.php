<h1>WYZWANIA MACUSA</h1>
<p>Ameryka pozostała jednym z najbardziej wrogich środowisk dla magicznych ludzi, głównie z powodu potomków Czyścicieli, którzy przenikneli na stałe w społeczność No-Maj i utrzymywali podejrzenia, że magia istnieje. W przeciwieństwie do większości krajów zachodnich, nie było współpracy między rządem No-Maj a MACUSA.</p>
<img class="img-fluid prawy" src="../img/pod_macusa2.jpg">
<p>Początkowo w górach Appalachów utworzono zaklęty gmach jako siedzibę MACUSA, ale z czasem stało się to niedogodnym miejscem, zwłaszcza że czarodzieje coraz częściej gromadzili się w miastach.</p>

<p>W 1760 MACUSA przeniósła się do Williamsburg w stanie Wirginia, gdzie mieszkał ekstrawagancki prezydent Thornton Harkaway. Pośród wielu innych zainteresowań, prezydentowi Harkaway przypisuje się hodowlę Psidwaków - psów, które bardzo przypominają Jack Russellów, z wyjątkiem rozwidlonego ogona. Psidwaki charakteryzuje spora agresja w stosunku do niemagicznych ludzi. Pewna grupa psiadwaków Harkawaya zaatakowała lokalnych niemagów, co doprowadziło do naruszenia Międzynarodowego Kodeksu Tajności, a tym samym zmusiła Thorntona do ustąpienia ze swojego stanowiska w niełasce. (Może nie jest zbiegiem okoliczności, że Williamsburg był pierwszym miastem w USA, w którym mieścił się specjalny szpital psychiatryczny. Obserwacje dziwnych wydarzeń w pobliżu rezydencji prezydenta Harkaway'a mogły tłumaczyć przyjęcie No-Majów, którzy byli w rzeczywistości całkowicie zdrowi.)</p>

<p>MACUSA przeniósł się do Baltimore, gdzie prezydent Able Fleming miał swój dom, ale wybuch wojny o niepodległość, która doprowadziła do przybycia do miasta Kongresu niemagów, zmusiła ponownie MACUSA do zmiany miejsca, postanowili przenieść się do Waszyngtonu.</p>

<p>To tam Prezydent Elizabeth McGilliguddy przewodniczył słynnej debacie "Kraj czy Rodzaj?" Z 1777 roku. Tysiące czarownic i czarodziejów z całej Ameryki przybyło na MACUSA, aby wziąć udział w tym nadzwyczajnym spotkaniu, do którego Wielka Magistrala Zgromadzenia (inaczej też Wielka Izba Spotkań) musiała zostać magicznie powiększona .Kwestią do dyskusji było: czy wspólnota magiczna zawdzięczała swoją najwyższą wierność krajowi, w którym się znaleźli, czy też światowej wspólnocie czarodziejów podziemnych? Czy byli moralnie zobowiązani do przyłączenia się do amerykańskich No-Majów w ich walce o wyzwolenie od brytyjskich mugoli? Czy to była, po prostu, nie ich walka?</p>

<p>Argumenty za i przeciwko interwencji były przedłużane, a walka stawała się błędna. Pro-interwencjoniści twierdzili, że mogą być w stanie uratować życie; antyinterwencjoniści, czarodzieje ryzykowali swoje własne bezpieczeństwo ujawniając się w walce. Posłańcy zostali wysłani do Ministerstwa Magii w Londynie, aby zapytać, czy zamierzają walczyć. Wiadomość w czterech słowach powróciła: "Zasiądź za mną" Sławna odpowiedź McGilliguddy była jeszcze krótsza: "Pamiętaj, że to zrobisz." Podczas gdy oficjalnie amerykańskie czarownice i czarodzieje nie angażowali się w bitwę, nieoficjalnie było wiele przypadków interwencji w celu ochrony niemagów. Społeczność czarodziejów obchodzili Dzień Niepodległości wraz z resztą społeczeństwa amerykańskiego - choć niekoniecznie z nimi.</p>
<img class="img-fluid lewy" src="../img/pod_macusa2.png">
<p>Jedna z najbardziej znaczących amerykańskich magicznych praw została stworzona w 1790 roku, kiedy MACUSA zatwierdziła edykt, aby wymusić całkowitą segregację społeczności czarodziejów i niemagów. Prawo Rappaporta, nazwane na cześć ówczesnej prezydent Emily Rappaport, zostało stworzone w wyniku jednego z najgorszych naruszeń Międzynarodowego Kodeksu Tajności, jaki kiedykolwiek istniał, naruszenia, w wyniku którego córka Strażnika Skarbu i Dragotów w MACUSA oraz potomek Czyściciela prawie wyeksponowana istnienie magii na całym świecie. Wraz z uchwaleniem ustawy Rappaporta mieszanie się, a nawet przyjaźń między czarodziejami i No-Majami stały się nielegalne w Stanach Zjednoczonych.</p>

<p>Baza MACUSA pozostała w Waszyngtonie do 1892 roku, kiedy nieprzewidziane powstanie Wielkich Stóp spowodowało kolejne naruszenie bezpieczeństwa. Historycy przypisują winę za bunt Irene Kneedander, Szefowi Organizacji Ochrony Magicznych Gatunków (Humanoid), która atakowała każdą Wielką Stopę, która zrobiła coś złego, co mogło przyczynić się do wybuchu buntu. Przybycie do Waszyngtonu sporej ilośći Wielkich Stó wymusiło użycie wiele razy zaklęcia zapomnienia oraz rozległych napraw w centralnej siedzibie.</p>

<p>MACUSA potrzebowała nowego schronienia, a w ciągu kilku lat czarodzieje przeniknęli do zespołu budowlanego nowego budynku w Nowym Jorku. Zanim budynek Woolworth został ukończony, mógł on zarówno pomieścić No-Majs, jak i - jeśli zostanie aktywowany przez właściwe zaklęcia - przekształcić się w przestrzeń dla czarodziejów. Jedynym zewnętrznym znakiem nowej tajnej lokacji MACUSA była sowa wyrzeźbiona nad wejściem.</p>

