<h1>MACUSA W latach 20 XX wieku</h1>
<img class="img-fluid prawy" src="../img/pod_macusa3.jpg">
<p>Podobnie jak w przypadku większości innych magicznych organów zarządzających, Departament Magicznego Egzekwowania Prawa jest największym działem w MACUSA.</p>

<p>Ustawa Rappaporta nadal działała w latach dwudziestych, a kilka biur w MACUSA nie miało odpowiednika w Ministerstwie Magii; na przykład, wydział zajmujący się fraternizacją niemagów oraz urzędem wydającym i weryfikującym różdżki zezwala na to, aby wszyscy, obywatele i odwiedzający mogli nosić je w Ameryce.</p>

<p>Znacząca różnica między czarodziejskimi rządami Stanów Zjednoczonych i Wielkiej Brytanii w tym czasie była kara za poważne przestępstwo. Podczas gdy brytyjskie czarownice i czarodzieje zostali wysłani do Azkabanu, najgorszy zbrodniarzy w Ameryce był stracony.</p>

<p>W latach dwudziestych prezydentem MACUSA była Seraphina Picquery z Savannah. Departamentem Egzekwowania Prawa Magicznego kierował Percival Graves, szanowany potomek jednego z dwunastu amerykańskich aurorów.</p>






