<h1>Magiczny Kongres Stanów Zjednoczonych Ameryki</h1>

<img class="img-fluid lewy" src="../img/pod_macusa.jpg">
<p>Magiczny Kongres Stanów Zjednoczonych Ameryki, znany amerykańskim czarownicom i czarodziejom za pomocą skrótu MACUSA (powszechnie wymawianego jako: Mah - cooz - ah) powstał w 1693 r. Po wprowadzeniu Międzynarodowego Kodeksu Tajności Czarodziejów. Czarodzieje na całym świecie doszli do punktu krytycznego, podejrzewając, że mogliby prowadzić szczęśliwsze i szczęśliwsze życia, gdyby zbudowali podziemną społeczność, która oferowała własne wsparcie i miała własne struktury. To uczucie było szczególnie silne w Ameryce, ze względu na niedawne procesy czarownic w Salem.</p>

<p>MACUSA została wzorowana na Radzie Czarodziejów w Wielkiej Brytanii, która funkcjonowała przed Ministerstwem Magii. Przedstawiciele magicznych społeczności w całej Ameryce Północnej zostali wybrani do MACUSA, aby tworzyć prawa, które zarówno nadzorują i chronią amerykańskiego czarodzieja.</p>

<p>Podstawowym celem MACUSA było pozbycie się Czyścicieli, skorumpowanych czarodziejów, którzy polowali na magiczne istoty dla osobistych korzyści. Drugim ważnym wyzwaniem dla MACUSA była liczba czarodziejskich zbrodniarzy, którzy uciekli do Ameryki z Europy i spoza niej, właśnie z powodu braku zorganizowanych organów ścigania, jakie istniały w ich własnych krajach.</p>

<p>Pierwszym prezydentem MACUSA był Josiah Jackson, wojowniczy czarodziej, którego wybrano na stanowisko, ponieważ uważano go za wystarczająco twardego, by poradzić sobie z trudnościami po erze procesów o czary.</p>

<p>W tych pierwszych latach MACUSA nie miała stałego miejsca spotkań. Spotkania odbywały się w różnych miejscach, aby uniknąć wykrycia przez No-Maj, czyli przez mugoli.</p>
