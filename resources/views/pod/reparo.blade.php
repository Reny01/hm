<h1> REPARO</h1>
  <img class="img-fluid lewy" src="../img/pod_reparo.gif">
<p>Za pomocą zaklęcia naprawiającego można w łatwy sposób reperować uszkodzone przedmioty, wypadki często się zdarzają dlatego trzeba wiedzieć jak należy naprawiać swoje błędy. Warto jednak zwrócić uwagę na bardziej bezpieczne obchodzenie się z przedmiotami. W 1754 roku Hesphaestus Gore – Brytyjski Minister Magii, wraz z Włoskim Ministrem Magii mieli sędziować niesławny, miotlarski wyścig z Aberdeen do Rzymu, pomiędzy najbardziej znanymi ‘lotnikami’ w Europie, Torquil McTavish i Silvio Astolﬁ. Zebrano się w Rzymie, w Koloseum, gdzie znajdowała się meta wyścigu, wszelkie środki ostrożności zostały zachowana, nawet sam wyścig został zorganizowany w nocy, tak aby nie zakłócić życia mugoli i nie zostać przez nich odkrytym. Ministrowie, wraz z trybunami fanów czekali na przybycie ścigających się rywali, zanim Ci jednak się zjawili, między fanami obu stron wybuchły konﬂikty, które w szybkim czasie doprowadziły do zniszczenia Koloseum. </p>
  <img class="img-fluid prawy2" src="../img/pod_reparo2.gif">
<p>Na pomoc przerażonych ministrów wyszła Orabella Nuttley, pracownica Urzędu Niewłaściwego Użycia Czarów, której zadaniem podczas wyścigu było przechowywanie oraz odpowiednie zorganizowanie świstoklików. Orabella Nuttley była nadzwyczajnie nieśmiałą czarownicą, która unikała zabierania głosu na zebraniach i peszyła się, gdy tylko ktoś coś do niej mówił, dlatego często dostawała bardzo proste zadania. W swoim domu natomiast eksperymentowała z zaklęciami, mając nadzieję na zwiększenie swojego młodzieńczego uroku. Orabella wyszła naprzeciw ministrów (w dosłownym tego słowa znaczeniu) i zaczęła naprawiać kolumny poprzez zaklęcie Reparo, które sama wynalazła. Zdumieni ministrowie oraz ich współpracownicy poprosili aby Orabella nauczyła ich tego zaklęcia. Wkrótce potem Koloseum wróciła do swojego poprzedniego stanu, a czarodziejom udało się uniknąć odkrycia przez mugoli, którzy niedługo potem zjawili się na miejscu zbudzeni hałasem. Wdzięczny Hesphaestus nagrodził Orabelle Nuttley Orderem Merlina Pierwszej Klasy, za zapobiegnięcie złamania najważniejszej zasady Międzynarodowego Kodeksu Tajności Czarów.</p>





