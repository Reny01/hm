<h1> Polowania na czarownice</h1>

<div class="blok-cytat">Ludzie niemagiczni (znani bardziej jako mugole) lękali się magii szczególnie w czasach średniowiecznych, prawdopodobnie dlatego, że niewiele o niej wiedzieli i nie potrafili rozpoznawać jej przejawów. Od czasu do czasu udawało im się schwytać prawdziwą czarownicę lub czarodzieje, ale nie mieli pojęcia, że palenie ich na stosie jest zupełnie bezsensowne. Ofiary rzucały proste zaklęcie zmrożenia płomieni i udawały, że wrzeszczą z bólu, podczas gdy w rzeczywistości odczuwały przyjemne łaskotanie. Na przykład Czarownica Wendelina, zwana również Dziwożoną, tak polubiła te łaskotki, że przybierała coraz to nowe postacie, aby dać się schwytać i spalić. Udało się jej tego dokonać aż czterdzieści siedem razy.
<p> Bathilda Bagshot, Dzieje Magii</p></div><br>
<img class="img-fluid lewy2" src="../img/pod_polowania.png">
<p>W Średniowieczu bardzo popularne było określenie ‘polowanie na czarownice’. Mugole zaczęli wyłapywać czarownice, a przynajmniej tak sądzili i palić na stosach lub poddawać innym torturom. I choć przejawy niechęci wobec czarodziejów można odkryć już od wczesnego okresu średniowiecza, to najbardziej ‘spektakularne’ i największe prześladowania czarownic można zauważyć po opublikowaniu przez Heinricha Kramera i Jacoba Sprengera ‘Młot na czarownice’ z roku 1487. Książka ta, tak naprawdę była jakby instrukcją dla mugoli w jaki sposób mają łapać czarownice, torturować i przesłuchiwać. Opisywała zabiegi niekiedy bardzo drastyczne i nieprzyjemne, a wszystko to po to, aby zmusić człowieka podejrzewanego o czary o przyznanie się do ich uprawiania. </p>

<p>I niestety ale tak było…. Ludzie oskarżeni o magię, przyznawali się do jej używania. Tyle że wcale nie byli czarodziejami, w większości przypadków (jak nie wszystkich), byli to zwykli mugole, którzy nie mieli po prostu szczęścia i wpadli w sidła innych niemagicznych osób, które za cel obrały sobie wytępienie wszelkiej magii, a w rozmowach z fanatycznymi szaleńcami nijak się zdało jakiekolwiek tłumaczenie.</p>

<p>Wielu mugoli zginęło wtedy z rąk swoich pobratymców. I właśnie ta sytuacja zmusiła czarodziei do podjęcia pewnych środków, które miały umożliwić im spokojne egzystowanie bez ponoszenia ofiar za swoje życie, bo tak było w rzeczywistości. I choć z pozoru nie musieli się ukrywać, to jednak dla zachowania własnego spokoju i czystości sumienia drastycznie zmienili dotychczasowy system funkcjonowania. I po pewnym czasie polowanie na czarownice ustało… </p>

<img class="img-fluid prawy" src="../img/pod_polowania.gif">
<p>Dlaczego w tym temacie nie mówię o torturach czarownic? Nie opisuje szczegółowych zabiegów związanych z nimi?
Uznałem, że nie jest to tak naprawdę tematem tego podręcznika. Tortury czarownic mimo takiej nazwy tak naprawdę nie dotyczyły czarownic, w większości były to zabiegi przeprowadzane na bogu winnych mugolach, którzy mieli pecha. Czarodzieje w większości przypadków unikali łapania, a sama Bathilda podaje tutaj przykład Wendeliny Dziwożony, która w bardzo prosty sposób potrafiła przeciwstawić się mugolom wyciągając z tego jeszcze dodatkowe korzyści – przyjemne łaskotki. Dlatego właśnie pisanie o torturach, o wymyślnych zabiegach i najdziwniejszych narzędziach według mnie po prostu mija się z celem. O samym paleniu na stosie w większości dowiecie się na zajęciach historii w mugolskich szkołach i mimo że wielu nauczycieli stosuje i wdraża ten temat do swojego programu w Świecie Magii, ja wole go uniknąć i skupić się na innych tematach, dużo ciekawszych i potwierdzonych przez samą autorkę. Choć nie ukrywam, że temat tortur może być zrealizowany na rozszerzeniu jako ciekawostka i dodatkowe wiadomości, które bardziej pozwolą wyobrazić sobie sytuacje jaka panowała w średniowieczu oraz sam stosunek, skutki i najważniejsze powody dla których czarodzieje zmusili się do ukrycia mimo że nie dotyczyło to utraty ich własnego życia.</p>

