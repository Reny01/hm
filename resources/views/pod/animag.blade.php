<h1> PIERWSZY ANIMAG</h1>
<img class="img-fluid lewy" src="../img/pod_animag.png">
<p>Pewnego razu po stanowczym zakazie Boga odnośnie zbliżania się Adama i Ewy do zakazanego drzewa, pierwszy czarodziej tamtego okres postanowił skusić kobietę, tak by ta złamała obietnice daną stwórcy. Pod postacią węża, Diego Di Ablo zaczął podstępem przekonywać Ewę do zjedzenia jabłka, kusząc ją na przeróżne możliwe sposoby. Ewa będąc ślepo zapatrzona w niezwykłą postać mówiącego węża oraz w zaczarowane jabłka, które przyciągały ją ku sobie własną mocą, złamała daną obietnice. Niczego nieświadoma zagrożenia jakie mogło czyhać ze strony węża, Ewa zjadła zakazany owoc, co wywołało niemiłe konsekwencje. Została wyrzucona wraz ze swoim partnerem z cudownego ogrodu. </p>

<p>Di Ablo przeszedł do historii jako pierwszy animag w świecie czarodziejów, ale także jako pierwszy czarodziej, który oszukał pierwszych w świecie mugoli – Adama i Ewę. </p>

<p>Jest to też przykład odwiecznej zmagań i relacji, jakie będą w przyszłości występowały między czarodziejami, a mugolami. Pierwszy raz pokazane jest, jak czarodziej w łatwy sposób może przechytrzyć zwykłych mugoli. Mimo użycia niewielkiej mocy, pokonał ich i upokorzył. Historia pokaże, że będzie jeszcze wiele podobnych sytuacji, w których to czarodzieje wygrają nad mugolami.</p>

<p>Sytuacja ta obrazuje także niegodziwą stronę mocy czarodziejów, chęć do dominacji nad osobami niemagicznymi oraz podświadomą potrzebę ich upokorzenia jak i pokazanie swojej prawdziwej władzy nad nimi oraz własnych możliwości. Ktoś może to uznać za zbytnią krytykę, jednak im dalej w przyszłość tym coraz gorsi i bardziej niegodziwi czarodzieje. Co ciekawe, mówimy o pierwszym czarodzieju, który upokorzył mugoli i ich wykorzystał, był on animagiem który przybierał formę węża, a właśnie takie zwierzę znajduje się w herbie Slytherinu, z którym kojarzone jest większość czarnoksiężników i niegodziwych czarodziejów, którym takie wykorzystanie mugoli jak najbardziej przypadłoby do gustu. Analogia jest więc zachowana, a to nadaje historii dodatkową autentyczność.</p>
