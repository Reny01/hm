<h1> ACCIO</h1>

<img class="img-fluid lewy" src="../img/pod_accio2.gif">
<p>Zaklęcie przywołujące jest jednym z najstarszych oraz najbardziej przydatnych czarów w świecie czarodziejów. Wydaje się ono dość prostym i pomocnym zaklęciem, które nie powinno stwarzać zagrożenia. Jednak w historii Świata Czarodziejów pojawił się przypadek, który ukazał złe strony tego zaklęcia. W roku 1743, pewna grupa czarodziejów nazywając siebie "Accionitami" wypowiedziała wojnę "mugolskim zwyczajom i zachowaniom". Postawili oni sobie za cel aby żyć w sposób wykraczający poza doczesne i zwyczajne problemy mugoli. Przywódcą grupy był Gideon Flatworthy, którego Prorok Codzienny opisywał jako głupiego, leniwego grubasa, który leży całymi dniami na cuchnącej poduszcze i oczekuje, że za to inni ludzie będą go podziwiać. </p>
<p>Flatworthy ogłosił, że jego grupa nigdy nie będzie nic sobie przynosić, podnosić ani nosić ponieważ jako czarodzieje są istotami wyższego rzędu i w odróżnieniu od mugoli, wszystko co jest im potrzebne powinno zostać przywołane za pomocą magii, a nie przez fizyczną czynność. Oczywiście jak można się domyślić, Accionici szybko wpadli w kłopoty. Odmawiając w pewien sposób pracy szybko napotkali problemy, przestali odwiedzać bank Gringotta, szybko też stracili wszystkie swoje pieniądze. Odmawiając pracy uznali, że najlepszym sposobem na 'zarobienie' jest przywołanie złota. Gideon wskazał więc różdżką w kierunku pobliskiego warsztatu goblinów i przywołał złoto, po części jego plan się powiódł, bowiem udało mu się zyskać kilka drogocennych sztuk goblińskiej biżuterni, która nie była jeszcze wykończona, a więc nie miała na sobie zaklęć chroniących przed kradzieżą. Po części ponieważ niedługo później do kryjówki Accionitów wpadła grupa rozłoszczonych goblinów złotników, uzbrojonych w miecze i żądnych krwi Accionitów. Banda Flatworthy'ego musiała szybko deportować się, obierając za nową siedzibę pewną piwnicę. W krótkim czasie grupie skończyły się zapasy żywności, a mając świadomość, że gobliny nadal pałają do nich zemstą, postanowili przywołać jedzenie oraz picie do piwnicy. Niestety na widok lecącego wina oraz ciast, gobliny od razu odnalazły kryjówkę Accionitów i po raz kolejny zmusili ich do deportacji, tym razem do jaskini w pobliżu Pembrokeshire. </p>
<img class="img-fluid prawy2" src="../img/pod_accio.gif">
<p>Członkowie Accionitów po tych wydarzeniach zrozumieli, że nawet proste czynności jak zakupy czy praca muszą być wykonywane również przez czarodziejów, dlatego szybko pozostawili Gideona Flatworthy'ego samemu sobie. Ten jednak nie poddawał się i pozostawał nadal przy swoich postanowieniach. Szybko jednak stracił zmysły z głodu i zimna, i 18 września 1743 roku Gideon wprowadził w życie swój ostatni plan, który okazał się dla niego zabójczy. Postanowił przywołać całą farmę, włącznie ze zwierzętami, przytulną chatką i dobrze zaopatrzoną spiżarnią. Jak można było się spodziewać, budynki nie ruszyły się z miejscą, jednak inne gospodarcze zwierzęta udało się przywołać. Właściciel farmy na widok lecących krów ruszył za nimi w pościg, trafił do jaskini, w której zastał martwego Flatworthy'ego, przygniecionego stogami siana i zwierzętami.</p>
